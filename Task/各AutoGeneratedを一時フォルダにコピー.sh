#!/bin/bash

PROJECT_ROOT="$1"
OUT_DIR="$2"

if [ -z "$PROJECT_ROOT" ]; then
  read -p "プロジェクトルートパス: " PROJECT_ROOT
fi

if [ -z "$OUT_DIR" ]; then
  read -p "出力先フォルダ名: " OUT_DIR
fi

if [ -z "$PROJECT_ROOT" ]; then
  echo "プロジェクトルートパスが指定されていません。"
  exit 1
fi

if [ -z "$OUT_DIR" ]; then
  echo "出力先フォルダ名が指定されていません。"
  exit 1
fi

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
OUT_DIR_FULLPATH="$SCRIPT_DIR/../temp_ソース現新比較用/$OUT_DIR"

# フォルダが存在する場合は一度削除して、作り直す（古いファイルを残さないため）
if [ -d "$OUT_DIR_FULLPATH" ]; then
  rm -rf "$OUT_DIR_FULLPATH"
fi
mkdir -p "$OUT_DIR_FULLPATH"

# xcopy /I /S /E /Y 相当
# -r: recursive, -f: force (overwrite)
cp -rf "$PROJECT_ROOT/Core.AutoGenerated/__AutoGenerated" "$OUT_DIR_FULLPATH/CORE"
cp -rf "$PROJECT_ROOT/WebApi/__AutoGenerated"             "$OUT_DIR_FULLPATH/WEBAPI"
cp -rf "$PROJECT_ROOT/client/src/__autoGenerated"         "$OUT_DIR_FULLPATH/CLIENT"
cp -rf "$PROJECT_ROOT/UnitTest/__AutoGenerated"           "$OUT_DIR_FULLPATH/TEST"

open "$SCRIPT_DIR/../temp_ソース現新比較用"
