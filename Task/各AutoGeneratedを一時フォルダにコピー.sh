#!/bin/bash

# $1: 出力先フォルダ名
OUT_DIR="$1"

if [ -z "$OUT_DIR" ]; then
  read -p "出力先フォルダ名: " OUT_DIR
fi

if [ -z "$OUT_DIR" ]; then
  echo "出力先フォルダ名が指定されていません。"
  exit 1
fi

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
REPO_ROOT="$SCRIPT_DIR/.."
DEMO_ROOT="$REPO_ROOT/demo"
OUT_DIR_FULLPATH="$REPO_ROOT/temp_ソース現新比較用/$OUT_DIR"

# フォルダが存在する場合は一度削除して、作り直す（古いファイルを残さないため）
if [ -d "$OUT_DIR_FULLPATH" ]; then
  rm -rf "$OUT_DIR_FULLPATH"
fi
mkdir -p "$OUT_DIR_FULLPATH"

# デモフォルダをループ
for demo_path in "$DEMO_ROOT"/*; do
  if [ -d "$demo_path" ]; then
    demo_name=$(basename "$demo_path")
    echo "Processing: $demo_name"
    
    # 各デモの出力先作成
    DEMO_OUT_DIR="$OUT_DIR_FULLPATH/$demo_name"
    mkdir -p "$DEMO_OUT_DIR"

    # Core
    if [ -d "$demo_path/Core.AutoGenerated/__AutoGenerated" ]; then
        cp -rf "$demo_path/Core.AutoGenerated/__AutoGenerated" "$DEMO_OUT_DIR/CORE"
    fi
    
    # WebApi
    if [ -d "$demo_path/WebApi/__AutoGenerated" ]; then
        cp -rf "$demo_path/WebApi/__AutoGenerated" "$DEMO_OUT_DIR/WEBAPI"
    fi
    
    # Client
    if [ -d "$demo_path/client/src/__autoGenerated" ]; then
        cp -rf "$demo_path/client/src/__autoGenerated" "$DEMO_OUT_DIR/CLIENT"
    fi
  fi
done

echo "完了しました。"

