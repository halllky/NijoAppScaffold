#!/bin/bash

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
SOURCE_ROOT="$SCRIPT_DIR/../Nijo.NewProjectTemplate"
NIJO_EXE_PATH="$SCRIPT_DIR/../Nijo/bin/Debug/net9.0/nijo"

# nijo.exe リビルド
dotnet build "$SCRIPT_DIR/../Nijo" -c Debug

if [ $? -ne 0 ]; then
    echo "エラーが発生しました"
    exit 1
fi

# テンプレートプロジェクトのソースコード自動生成かけなおし
"$NIJO_EXE_PATH" generate "$SOURCE_ROOT"

# デモフォルダをループし全デモフォルダに対して処理を行う
for d in "$SCRIPT_DIR/../demo/"*; do
    if [ -d "$d" ]; then
        TARGET_ROOT="$d"
        echo "Processing $TARGET_ROOT"

        # ソースコード自動生成かけなおし
        "$NIJO_EXE_PATH" generate "$TARGET_ROOT"

        if [ $? -eq 0 ]; then
            # React
            rsync -av --delete-excluded --exclude={".*","example.ts"} "$SOURCE_ROOT/client/src/example/" "$TARGET_ROOT/client/src/example/"

            # Core
            cp "$SOURCE_ROOT/Core/JsonSerializerExtensions.cs" "$TARGET_ROOT/Core/JsonSerializerExtensions.cs"
            cp "$SOURCE_ROOT/Core/MigrationEntryPoint.cs" "$TARGET_ROOT/Core/MigrationEntryPoint.cs"
            cp "$SOURCE_ROOT/Core/PresentationContextExtensions.cs" "$TARGET_ROOT/Core/PresentationContextExtensions.cs"
            cp "$SOURCE_ROOT/Core/MessageContainerExtension.cs" "$TARGET_ROOT/Core/MessageContainerExtension.cs"

            # Core.AutoGenerated
            # 特になし

            # WebApi
            rsync -av --delete-excluded --exclude=".*" "$SOURCE_ROOT/WebApi/Base/" "$TARGET_ROOT/WebApi/Base/"
            cp "$SOURCE_ROOT/WebApi/Properties/launchSettings.json" "$TARGET_ROOT/WebApi/Properties/launchSettings.json"
            cp "$SOURCE_ROOT/WebApi/Properties/PublishProfiles/本番用ビルド.pubxml" "$TARGET_ROOT/WebApi/Properties/PublishProfiles/本番用ビルド.pubxml"

            # UnitTest
            cp "$SOURCE_ROOT/UnitTest/PresentationContextInUnitTest.cs" "$TARGET_ROOT/UnitTest/PresentationContextInUnitTest.cs"
            cp "$SOURCE_ROOT/UnitTest/TestUtilImpl.cs" "$TARGET_ROOT/UnitTest/TestUtilImpl.cs"

            # Management
            cp "$SOURCE_ROOT/Management/README.md" "$TARGET_ROOT/Management/README.md"
            cp "$SOURCE_ROOT/Management/Program.cs" "$TARGET_ROOT/Management/Program.cs"
            rsync -av --delete-excluded --exclude=".*" "$SOURCE_ROOT/Management/Commands/" "$TARGET_ROOT/Management/Commands/"

            # Task
            rsync -av --delete-excluded --exclude=".*" "$SOURCE_ROOT/Task/" "$TARGET_ROOT/Task/"
        fi
    fi
done

echo "コピー完了しました"
exit 0
