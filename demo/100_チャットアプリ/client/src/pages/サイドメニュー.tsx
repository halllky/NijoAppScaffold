import React from "react"
import { Link, useLocation } from "react-router-dom"
import { callComplexPostEndpointAsync } from "../example/callComplexPostEndpointAsync"
import * as AutoGenerated from "../__autoGenerated"
import { チャンネル画面DisplayData } from "../__autoGenerated/チャンネル画面"

/**
 * サイドメニュー。
 * ユーザーが参加しているチャンネルの一覧が表示される。
 * 下部にはユーザー自身のアカウントの情報が表示される。
 */
export default function サイドメニュー() {
  const [channels, setChannels] = React.useState<チャンネル画面DisplayData[]>([])
  const [loading, setLoading] = React.useState(true)
  const [error, setError] = React.useState<string | null>(null)
  const location = useLocation()

  // チャンネル一覧を取得
  const loadChannels = React.useCallback(async () => {
    try {
      setLoading(true)
      setError(null)

      // チャンネル一覧を検索するための検索条件を作成
      const searchCondition = AutoGenerated.SearchCondition.create['チャンネル画面']()

      // APIを呼び出してチャンネル一覧を取得
      const result = await callComplexPostEndpointAsync(
        'チャンネル画面',
        searchCondition
      )

      if (result.type === "ok") {
        setChannels(result.returnValue.currentPageItems)
      } else if (result.type === 'error') {
        setError('チャンネル一覧の取得に失敗しました')
      } else if (result.type === 'unknown') {
        setError(result.message)
      } else {
        setError('チャンネル一覧の取得がキャンセルされました')
      }
    } catch (err) {
      setError('チャンネル一覧の取得中にエラーが発生しました')
      console.error(err)
    } finally {
      setLoading(false)
    }
  }, [])

  // コンポーネントマウント時にチャンネル一覧を読み込む
  React.useEffect(() => {
    loadChannels()
  }, [loadChannels])

  // 現在のチャンネルIDを取得
  const currentChannelId = React.useMemo(() => {
    const match = location.pathname.match(/^\/channel\/(.+)$/)
    return match ? match[1] : null
  }, [location.pathname])

  return (
    <div style={{ height: '100%', display: 'flex', flexDirection: 'column' }}>
      {/* チャンネル一覧セクション */}
      <div style={{ flex: 1, padding: '16px' }}>
        <h3 style={{
          margin: '0 0 16px 0',
          fontSize: '16px',
          color: '#bdc3c7',
          textTransform: 'uppercase',
          letterSpacing: '1px'
        }}>
          チャンネル
        </h3>

        {loading && (
          <div style={{ color: '#95a5a6', fontSize: '14px' }}>
            読み込み中...
          </div>
        )}

        {error && (
          <div style={{ color: '#e74c3c', fontSize: '14px', marginBottom: '16px' }}>
            {error}
            <button
              onClick={loadChannels}
              style={{
                marginLeft: '8px',
                background: 'none',
                border: '1px solid #e74c3c',
                color: '#e74c3c',
                padding: '4px 8px',
                fontSize: '12px',
                borderRadius: '4px',
                cursor: 'pointer'
              }}
            >
              再試行
            </button>
          </div>
        )}

        {!loading && !error && channels.length === 0 && (
          <div style={{ color: '#95a5a6', fontSize: '14px' }}>
            チャンネルがありません
          </div>
        )}

        {channels.map((channel) => {
          const isActive = currentChannelId === channel.values.チャンネルID
          return (
            <Link
              key={channel.values.チャンネルID}
              to={`/channel/${channel.values.チャンネルID}`}
              style={{
                display: 'block',
                padding: '8px 12px',
                marginBottom: '4px',
                borderRadius: '4px',
                textDecoration: 'none',
                color: isActive ? '#ffffff' : '#bdc3c7',
                backgroundColor: isActive ? '#3498db' : 'transparent',
                fontSize: '14px',
                transition: 'all 0.2s ease',
              }}
              onMouseEnter={(e) => {
                if (!isActive) {
                  e.currentTarget.style.backgroundColor = '#34495e'
                  e.currentTarget.style.color = '#ffffff'
                }
              }}
              onMouseLeave={(e) => {
                if (!isActive) {
                  e.currentTarget.style.backgroundColor = 'transparent'
                  e.currentTarget.style.color = '#bdc3c7'
                }
              }}
            >
              # {channel.values.チャンネル名}
            </Link>
          )
        })}
      </div>

      {/* ユーザー情報セクション */}
      <div style={{
        padding: '16px',
        borderTop: '1px solid #34495e',
        backgroundColor: '#34495e'
      }}>
        <div style={{
          display: 'flex',
          alignItems: 'center',
          fontSize: '14px',
          color: '#ecf0f1'
        }}>
          <div style={{
            width: '32px',
            height: '32px',
            borderRadius: '50%',
            backgroundColor: '#3498db',
            display: 'flex',
            alignItems: 'center',
            justifyContent: 'center',
            marginRight: '12px',
            fontWeight: 'bold'
          }}>
            U
          </div>
          <div>
            <div style={{ fontWeight: 'bold' }}>ユーザー</div>
            <div style={{ fontSize: '12px', color: '#95a5a6' }}>オンライン</div>
          </div>
        </div>
      </div>
    </div>
  )
}
