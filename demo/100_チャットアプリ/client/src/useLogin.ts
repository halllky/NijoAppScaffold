import React from "react"
import useEvent from "react-use-event-hook"
import * as AutoGenerated from "./__autoGenerated"
import { callComplexPostEndpointAsync, DetailMessagesContainer } from "./callComplexPostEndpointAsync"

/**
 * ログイン、ログアウト、ログイン中ユーザー情報を扱う。
 */
export const useLogin = (): LoginStatus => {
  const [status, setStatus] = React.useState<LoginStatus["status"]>("logged-out")
  const [processing, setProcessing] = React.useState(false)
  const [loginUser, setLoginUser] = React.useState(() => AutoGenerated.DisplayData.create["アカウントView"]())

  const loginAsync: LoginFunction = useEvent(async (param, onError) => {
    if (status === "logged-in") return;
    if (processing) return;
    setProcessing(true)

    try {
      const result = await callComplexPostEndpointAsync("ログイン", param)
      if (result.type === "canceled") {
        return;
      }
      if (result.type === "error") {
        onError(result.detail)
        return;
      }
      if (result.type === "unknown") {
        window.alert(result.message)
        return;
      }
      setLoginUser(result.returnValue)
      setStatus("logged-in")
    } finally {
      setProcessing(false)
    }
  })

  const logoutAsync: LogoutFunction = useEvent(async onError => {
    if (status === "logged-out") return;
    if (processing) return;
    setProcessing(true)

    try {
      const result = await callComplexPostEndpointAsync("ログアウト", {})
      if (result.type === "canceled") {
        return;
      }
      if (result.type === "error") {
        onError(result.detail)
        return;
      }
      if (result.type === "unknown") {
        window.alert(result.message)
        return;
      }
      setLoginUser(AutoGenerated.DisplayData.create["アカウントView"]())
      setStatus("logged-out")
    } finally {
      setProcessing(false)
    }
  })

  return {
    status,
    processing,
    loginUser,
    loginAsync,
    logoutAsync,
  }
}

type LoginStatus = {
  /** ログイン状態 */
  status: "logged-in" | "logged-out"
  /** ログインまたはログアウト処理中かどうか */
  processing: boolean
  /** ログイン中のユーザー情報 */
  loginUser: AutoGenerated.DisplayData.TypeMap["アカウントView"]
  /** ログインを試みる処理 */
  loginAsync: LoginFunction
  /** ログアウトを試みる処理 */
  logoutAsync: LogoutFunction
}

type LoginFunction = (
  param: AutoGenerated.StructureModelDisplayData.TypeMap["ログインParameter"],
  onError: (errorDetail: DetailMessagesContainer) => void,
) => Promise<void>

type LogoutFunction = (
  onError: (errorDetail: DetailMessagesContainer) => void,
) => Promise<void>
