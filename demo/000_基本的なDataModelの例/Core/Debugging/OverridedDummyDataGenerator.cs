namespace MyApp;

/// <summary>
/// デバッグ用のダミーデータ生成。
/// DbContext を直接使ってデータを登録します。
/// </summary>
public class OverridedDummyDataGenerator {
    private const string CONTRACT_TYPE = "010";
    private const string ATTENDANCE_TYPE = "020";

    public async Task GenerateAsync(AutoGeneratedApplicationService app) {
        var db = app.DbContext;

        // 旧システム部署情報 (80件: 部署20件 × 課4件分)
        for (var i = 1; i <= 80; i++) {
            db.旧システム部署情報DbSet.Add(new 旧システム部署情報DbEntity {
                旧システムコード = $"LEGACY-{i:000}",
                名称 = $"旧部署{i:000}",
                Version = 0,
            });
        }
        await db.SaveChangesAsync();

        // 汎用マスタ: 契約区分
        for (var i = 1; i <= 10; i++) {
            db.汎用マスタDbSet.Add(new 汎用マスタDbEntity {
                汎用種別 = CONTRACT_TYPE,
                区分値 = $"CONTRACT-{i:000}",
                表示名称 = $"契約区分{i}",
                Version = 0,
            });
        }
        // 汎用マスタ: 勤怠管理区分
        for (var i = 1; i <= 10; i++) {
            db.汎用マスタDbSet.Add(new 汎用マスタDbEntity {
                汎用種別 = ATTENDANCE_TYPE,
                区分値 = $"ATTEND-{i:000}",
                表示名称 = $"勤怠管理区分{i}",
                Version = 0,
            });
        }
        await db.SaveChangesAsync();

        // 部署 (課・係含む)
        var oldDeptIndex = 1;
        for (var deptIdx = 1; deptIdx <= 20; deptIdx++) {
            var dept = new 部署DbEntity {
                部署ID = deptIdx,
                部署名 = $"部署{deptIdx:000}",
                Version = 0,
            };
            for (var kIdx = 1; kIdx <= 4; kIdx++) {
                var kaCode = $"KA-{deptIdx:00}-{kIdx}";
                var ka = new 課DbEntity {
                    Parent_部署ID = deptIdx,
                    コード = kaCode,
                    旧システムコード_旧システムコード = $"LEGACY-{oldDeptIndex++:000}",
                    課名称 = $"第{kIdx}課",
                };
                for (var keiIdx = 1; keiIdx <= 4; keiIdx++) {
                    ka.係.Add(new 係DbEntity {
                        Parent_Parent_部署ID = deptIdx,
                        Parent_コード = kaCode,
                        連番 = keiIdx,
                        係名称 = $"第{keiIdx}係",
                        勤怠管理区分_区分値 = $"ATTEND-{((keiIdx - 1) % 10) + 1:000}",
                    });
                }
                dept.課.Add(ka);
            }
            db.部署DbSet.Add(dept);
        }
        await db.SaveChangesAsync();

        // 社員
        for (var i = 1; i <= 20; i++) {
            db.社員DbSet.Add(new 社員DbEntity {
                社員ID = i,
                氏名 = $"社員{i:000}",
                所属部署_部署ID = ((i - 1) % 20) + 1,
                契約種別_区分値 = $"CONTRACT-{((i - 1) % 10) + 1:000}",
                Version = 0,
            });
        }
        await db.SaveChangesAsync();
    }
}

#endif
