// アプリ全体のスタイル。不要なら削除可能
import "./main.css"

// Allotment を使用する場合は必須。不要な場合は削除可能
import "allotment/dist/style.css"
import { Allotment, LayoutPriority } from "allotment"

// React 関連のライブラリ
import React from "react"
import ReactDOM from "react-dom/client"

// Nijo App Scaffold のモジュール
import * as AutoGenerated from "./__autoGenerated" // 自動生成されたソースコード。ここで定義されているモジュールを利用してアプリを作ってください。
import ReflectionDebugger from "./reflection-debugger" // 標準のデバッグツール。不要な場合は削除可能
import { callAspNetCoreApiAsync } from "./callAspNetCoreApiAsync"

/**
 * 自動生成されたソースコードを用いたアプリケーションの空の雛形。
 * このコンポーネントの内容を自由に編集して、実際に利用されるアプリケーションを作成してください。
 */
function App() {

  // 利用可能なモデルの名前の一覧
  const availableQueryModels = React.useMemo(() => {
    return AutoGenerated.getQueryModelTypeList()
  }, [])
  const availableCommandModels = React.useMemo(() => {
    return AutoGenerated.getCommandModelTypeList()
  }, [])

  // データベース再作成処理
  const [isRecreating, setIsRecreating] = React.useState(false)
  const handleRecreateDatabase = async () => {
    if (!confirm('データベースを削除して再作成します。よろしいですか?')) {
      return
    }

    setIsRecreating(true)
    try {
      const response = await callAspNetCoreApiAsync('example/destroy-and-recreate-database', {
        method: 'POST',
      })

      if (response.ok) {
        alert('データベースの再作成が完了しました')
      } else {
        alert('エラーが発生しました: ' + response.statusText)
      }
    } catch (error) {
      alert('エラーが発生しました: ' + error)
    } finally {
      setIsRecreating(false)
    }
  }

  return (
    <Allotment
      proportionalLayout={false}
      className="h-full w-full"
    >

      <Allotment.Pane
        priority={LayoutPriority.Low}
        preferredSize={200}
      >
        <div className="w-full h-full flex flex-col gap-2 p-2 overflow-y-auto bg-gray-200">
          <h1 className="font-bold">
            サンプル
          </h1>

          <button
            onClick={handleRecreateDatabase}
            disabled={isRecreating}
            className="px-4 py-2 bg-red-500 text-white rounded hover:bg-red-600 disabled:bg-gray-400 disabled:cursor-not-allowed"
          >
            {isRecreating ? '処理中...' : 'データベース再作成 & ダミーデータ投入'}
          </button>

          <p className="text-xs">
            これは自動生成されたソースコードを利用したWebアプリケーションの雛形です。<br />
            例えばここにサイドメニューを作り、右の欄にコンテンツ欄を作るなどして、実際に利用される画面を組み上げてください。<br />
            あなたは JavaScript で画面を作り上げるにあたり、自動生成された下記の各モデルのモジュールを利用することができます。<br />
            自動生成されたモジュールは <code>__autoGenerated</code> フォルダ内に生成されます。
          </p>

          <h2>
            Query Model
          </h2>

          <ul className="list-disc pl-4 text-xs">
            {availableQueryModels.length === 0 && (
              <li>
                利用可能な Query Model はありません。
              </li>
            )}
            {availableQueryModels.map(model => (
              <li key={model}>
                {model}
              </li>
            ))}
          </ul>

          <h2>
            Command Model
          </h2>

          <ul className="list-disc pl-4 text-xs">
            {availableCommandModels.length === 0 && (
              <li>
                利用可能な Command Model はありません。
              </li>
            )}
            {availableCommandModels.map(model => (
              <li key={model}>
                {model}
              </li>
            ))}
          </ul>
        </div>
      </Allotment.Pane>

      <Allotment.Pane
        className="flex flex-col gap-2 p-2"
      >
        {/* Vite の仕組みを用いて、デバッグツールを開発環境(DEV)でのみ表示されるようにしています。 */}
        {import.meta.env.DEV && (
          <>
            <p className="text-xs">
              以下に見えるのはデバッグ用のツールです。
              このツールは以下の機能を提供します。
            </p>
            <ul className="text-xs list-disc pl-4">
              <li>Query Model : 実際に一覧検索機能を実行する</li>
              <li>Command Model : 実際にコマンドを実行する</li>
              <li>Data Model : データベースの各テーブルの内容表示。既定の Query Model を生成する Data Model の場合は、それに加えて実際に一覧検索機能を実行する機能。</li>
            </ul>
            <ReflectionDebugger className="flex-1 border-2 border-orange-500" />
          </>
        )}

      </Allotment.Pane>

    </Allotment>
  )
}

ReactDOM.createRoot(document.getElementById('root')!).render(
  <React.StrictMode>
    <App />
  </React.StrictMode>,
)
