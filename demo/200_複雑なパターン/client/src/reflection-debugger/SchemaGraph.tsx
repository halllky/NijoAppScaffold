import React, { useState, useMemo } from 'react';
import { GraphView2, GraphViewRef, Node } from '@nijo/ui-components/layout/GraphView2';
import getNijoXmlViewState from '../__autoGenerated/util/get-nijo-xml-view-state';
import useEvent from 'react-use-event-hook';
import cytoscape from 'cytoscape';

/**
 * nijo.xmlのビュー状態をGraphViewで表示するコンポーネント
 */
export default function ({
  onSelectedRootAggregateChange,
  className
}: {
  onSelectedRootAggregateChange: (rootAggregateId: string | null) => void
  className: string
}) {

  const graphViewRef = React.useRef<GraphViewRef>(null);

  // データ
  const viewStateData = useMemo(() => getNijoXmlViewState().schemaDefinition, []);
  const selectedViewData = React.useMemo(() => {
    return {
      ...viewStateData,
      nodes: Object.values(viewStateData.nodes).map(d => ({ ...d, locked: true }) satisfies Node),
    }
  }, [viewStateData])

  // 選択
  const onSelectedNodeChange = useEvent((e: cytoscape.EventObject) => {
    const selected = e.cy.nodes().filter(n => n.selected())
    if (selected.length === 0) {
      onSelectedRootAggregateChange(null)
      return
    }
    // 必ずしもルート集約が選択されたとは限らないので祖先を辿る
    let node = selected[0]
    while (true) {
      // 親を辿る
      const parentId = node.data('parent' satisfies keyof Node)
      if (!parentId) break // このノードがルート集約の場合

      // 親があるならば次のループへ
      node = e.cy.$id(parentId)

      // ありえないが念のため
      if (!node) {
        onSelectedRootAggregateChange(null)
        return
      }
    }

    onSelectedRootAggregateChange(node.id())
  })

  return (
    <div className={`relative flex flex-col border border-gray-300 ${className ?? ''}`}>
      {/* グラフ */}
      <GraphView2
        ref={graphViewRef}
        nodes={selectedViewData.nodes}
        edges={selectedViewData.edges}
        defaultNodePositions={viewStateData.nodePositions}
        onSelectionChange={onSelectedNodeChange}
        className="w-full h-full"
      />
    </div>
  )
}
