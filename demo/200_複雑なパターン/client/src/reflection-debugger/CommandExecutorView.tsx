/**
 * コマンド実行と履歴の管理。
 * コマンド実行履歴はlocalStorage に保存される。
 */
const useCommandExecutor = (
  localStorageUniqueKey: string,
  rootAggregateType: string,
) => {
  const localStorageKey = `EXECUTE_HISTORY::${localStorageUniqueKey}`
  const [commandHistory, setCommandHistory] = React.useState<ExecutionHistory[]>([])

  // 履歴をlocalStorage から読み込む
  React.useEffect(() => {
    const history = localStorage.getItem(localStorageKey)
    if (history) {
      setCommandHistory(JSON.parse(history))
    }
  }, [])

  // コマンド実行
  const executeCommand = React.useCallback(async (parameter: object): Promise<ExecutionHistory | undefined> => {
    let newHistory: ExecutionHistory
    const newHistoryId = UUID.generate()
    const createdAt = dayjs().format('YYYY-MM-DD HH:mm:ss')


    // コマンド実行
    const result = await callComplexPostEndpointAsync(
      rootAggregateType as AutoGenerated.QueryModelType, // 型をごまかす。なんでもよい
      parameter as AutoGenerated.LoadFeature.ParamType[AutoGenerated.QueryModelType], // 型をごまかす。なんでもよい
    )
    if (result.type === 'canceled') {
      // キャンセルされた場合は履歴に追加しない
      return undefined

    } else if (result.type === 'error') {
      // パラメータエラー
      newHistory = {
        id: newHistoryId,
        parameter,
        errors: toFlattenStringList(result.detail),
        createdAt,
        ok: false,
      }

    } else {
      // 正常終了
      newHistory = {
        id: newHistoryId,
        createdAt,
        ok: true,
        returnValue: result.returnValue,
        parameter,
      }
    }

    // 履歴をstateに追加
    setCommandHistory(prev => [newHistory, ...prev])

    // 履歴をlocalStorage に保存
    let currentHistory: ExecutionHistory[]
    try {
      currentHistory = JSON.parse(localStorage.getItem(localStorageKey) ?? '[]')
    } catch {
      currentHistory = []
    }
    localStorage.setItem(localStorageKey, JSON.stringify([newHistory, ...currentHistory]))

    return newHistory
  }, [rootAggregateType, localStorageKey])

  // 履歴をクリア
  const clearCommandHistory = React.useCallback(() => {
    localStorage.removeItem(localStorageKey)
    setCommandHistory([])
  }, [localStorageKey])

  return {
    /** コマンド実行履歴 */
    commandHistory,
    /** コマンド実行 */
    executeCommand,
    /** コマンド実行履歴をクリア */
    clearCommandHistory,
  }
}

/** コマンド実行履歴 */
export type ExecutionHistory = {
  id: string
  parameter: object
  createdAt: string
} & ({
  ok: true
  returnValue: object
} | {
  ok: false
  errors: string[]
})
