import React from "react"
import { CommandModelType, ExecuteFeature } from "../__autoGenerated"
import * as Icon from "@heroicons/react/24/solid"
import buildDynamicFormSchema from "./buildDynamicFormSchema"
import { MetadataContext } from "./MetadataContext"
import { MetadataForPage } from "../__autoGenerated/util"
import CommandExecutorView, { ExecutionHistory } from "./CommandExecutorView"
import { DynamicForm } from "@nijo/ui-components/layout"
import { Allotment } from "allotment"

/**
 * Command Model のWeb側エンドポイントの呼び出しと結果の閲覧を行なうデバッグ用コンポーネント。
 */
export default function ({ rootAggregateType, className }: {
  rootAggregateType: CommandModelType
  className?: string
}) {
  const helper = React.useContext(MetadataContext)

  // 自動生成されたメタデータをもとにした引数の入力フォーム定義
  const parameterFormSchema = React.useMemo(() => {
    const root = helper.metadata[rootAggregateType]
    const parameter = Object
      .entries(helper.metadata)
      .find(([name]) => name === root.parameter)?.[1]
    if (!parameter) throw new Error(`${root.parameter} が見つかりません`) // ありえないが念のため
    return buildDynamicFormSchema(parameter as MetadataForPage.StructureMetadata, 'structure-model', false, helper)
  }, [rootAggregateType, helper])

  // 自動生成されたメタデータをもとにした戻り値の入力フォーム定義
  const returnValueFormSchema = React.useMemo(() => {
    const root = helper.metadata[rootAggregateType]
    const returnValue = Object
      .entries(helper.metadata)
      .find(([name]) => name === root.returnValue)?.[1]
    if (!returnValue) throw new Error(`${root.returnValue} が見つかりません`) // ありえないが念のため
    return buildDynamicFormSchema(returnValue as MetadataForPage.StructureMetadata, 'structure-model', false, helper)
  }, [rootAggregateType, helper])

  // 履歴表示コンポーネント
  const [selectedHistory, setSelectedHistory] = React.useState<ExecutionHistory | null>(null)
  const [parameterOpened, setParameterOpened] = React.useState(false)
  const handleButtonClick = () => {
    setParameterOpened(prev => !prev)
  }

  return (
    <CommandExecutorView
      title={rootAggregateType}
      parameterFormSchema={parameterFormSchema}
      onSelectedHistoryChanged={setSelectedHistory}
      localStorageUniqueKey={helper.metadata[rootAggregateType].uniqueId}
      rootAggregateType={rootAggregateType}
      className={className}
    >
      {selectedHistory && (
        <div key={selectedHistory.id} className="w-full h-full flex flex-col">

          {/* パラメータ欄折りたたみボタン */}
          <button type="button" onClick={handleButtonClick} className="flex items-center gap-px text-sm bg-gray-300 cursor-pointer">
            {parameterOpened
              ? <Icon.ChevronDownIcon className="w-4 h-4" />
              : <Icon.ChevronRightIcon className="w-4 h-4" />}
            パラメータ
          </button>

          <Allotment
            vertical
            className="flex-1"
          >

            {/* パラメータ表示欄 */}
            <Allotment.Pane
              visible={parameterOpened}
              snap
            >
              <div className="w-full h-full p-1 overflow-y-auto">
                <DynamicForm
                  root={parameterFormSchema}
                  defaultValues={selectedHistory.parameter}
                  isReadOnly
                />
              </div>
            </Allotment.Pane>

            {/* 戻り値表示欄 */}
            <Allotment.Pane>
              <div className="flex items-center gap-1 pl-4 text-sm bg-gray-300">
                処理結果
              </div>
              {selectedHistory.ok ? (
                <div className="w-full h-full p-1 overflow-y-auto">
                  <DynamicForm
                    root={returnValueFormSchema}
                    defaultValues={selectedHistory.returnValue}
                    isReadOnly
                  />
                </div>
              ) : (
                <div className="w-full h-full p-1 overflow-y-auto">
                  {selectedHistory.errors.map((error) => (
                    <div key={error}>{error}</div>
                  ))}
                </div>
              )}
            </Allotment.Pane>
          </Allotment>
        </div>
      )}
    </CommandExecutorView>
  )
}
