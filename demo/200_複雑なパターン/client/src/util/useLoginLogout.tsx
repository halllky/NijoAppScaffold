import React from "react"
import { callAspNetCoreApiAsync } from "../example/callAspNetCoreApiAsync"
import { ログインユーザー情報 } from "../__autoGenerated/ログインユーザー情報"

const SESSION_STORAGE_KEY = "nijo-demo200-login-user"

type LoginUserContextType = {
  loginUser: ログインユーザー情報 | null
  loginAsync: (userId: string, password: string) => Promise<string | null>
  logoutAsync: () => Promise<void>
  initializing: boolean
}

const LoginUserContext = React.createContext<LoginUserContextType>({
  loginUser: null,
  loginAsync: async () => null,
  logoutAsync: async () => { },
  initializing: true,
})

export function useLoginLogout() {
  return React.useContext(LoginUserContext)
}

export function LoginUserProvider({ children }: { children: React.ReactNode }) {
  const [loginUser, setLoginUser] = React.useState<ログインユーザー情報 | null>(() => {
    const json = sessionStorage.getItem(SESSION_STORAGE_KEY)
    return json ? JSON.parse(json) as ログインユーザー情報 : null
  })
  const [initializing, setInitializing] = React.useState(() => !sessionStorage.getItem(SESSION_STORAGE_KEY))

  React.useEffect(() => {
    window.addEventListener("storage", handleStorageEvent)
    return () => window.removeEventListener("storage", handleStorageEvent)
  }, [])

  function handleStorageEvent(e: StorageEvent) {
    if (e.key !== SESSION_STORAGE_KEY) return
    if (e.newValue) {
      setLoginUser(JSON.parse(e.newValue))
    } else {
      setLoginUser(null)
    }
  }

  React.useEffect(() => {
    if (sessionStorage.getItem(SESSION_STORAGE_KEY)) {
      setInitializing(false)
      return
    }
    callAspNetCoreApiAsync("example/login-status", { method: "GET" })
      .then(res => {
        if (res.ok && res.status !== 204) return res.json() as Promise<ログインユーザー情報>
        return null
      })
      .then(user => {
        if (user) {
          sessionStorage.setItem(SESSION_STORAGE_KEY, JSON.stringify(user))
          setLoginUser(user)
        }
      })
      .catch(() => { })
      .finally(() => setInitializing(false))
  }, [])

  const contextValue = React.useMemo((): LoginUserContextType => {
    const loginAsync = async (userId: string, password: string): Promise<string | null> => {
      try {
        const res = await callAspNetCoreApiAsync("example/login", {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ userId, password }),
        })
        if (!res.ok) {
          const text = await res.text()
          return text || "ログインに失敗しました。"
        }
        const user = await res.json() as ログインユーザー情報
        sessionStorage.setItem(SESSION_STORAGE_KEY, JSON.stringify(user))
        setLoginUser(user)
        return null
      } catch {
        return "サーバーに接続できませんでした。"
      }
    }

    const logoutAsync = async () => {
      try {
        await callAspNetCoreApiAsync("example/logout", { method: "POST" })
      } catch { /* ignore */ }
      sessionStorage.removeItem(SESSION_STORAGE_KEY)
      setLoginUser(null)
    }

    return { loginUser, loginAsync, logoutAsync, initializing }
  }, [loginUser, initializing])

  return (
    <LoginUserContext.Provider value={contextValue}>
      {children}
    </LoginUserContext.Provider>
  )
}
