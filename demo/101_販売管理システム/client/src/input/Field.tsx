export type FieldProps<
  TFormValues extends ReactHookForm.FieldValues,
  TFieldPath extends ReactHookForm.Path<TFormValues>
> = Omit<React.InputHTMLAttributes<HTMLInputElement>, "name" | "ref"> & {
  name: TFieldPath
  control: ReactHookForm.Control<TFormValues>
}

/**
 * nijoが自動生成するメタデータを参照して
 * テキストボックスやチェックボックスなどの入力フォームを自動的に判別して
 * 適切なUIを表示するコンポーネント。
 * 最大桁数の参照などを自動的に行う。
 */
export function Field<
  TFormValues extends ReactHookForm.FieldValues,
  TFieldPath extends ReactHookForm.Path<TFormValues>
>({
  name: propsName,
  control,
  className,
  ...restProps
}: FieldProps<TFormValues, TFieldPath>): React.ReactNode {

  const contextValue = React.useContext(FieldUiContext)
  const fieldMetadata = React.useMemo(() => {
    return contextValue.getMetadataOfField(propsName)
  }, [contextValue.getMetadataOfField, propsName])

  // エラー
  if (!contextValue.isInContext) return (
    <div className={`${ERROR_CLASS_NAME} ${className ?? ""}`}>
      FieldContextProvider が配置されていません。
    </div>
  )
  if (!fieldMetadata) {
    console.error(`Field - メタデータが見つかりません: ${propsName}`, { contextValue })
    return (
      <div className={`${ERROR_CLASS_NAME} ${className ?? ""}`}>
        指定されたフィールドのメタデータが見つかりません: {propsName}
      </div>
    )
  }
  if (fieldMetadata.type === "RootAggregate"
    || fieldMetadata.type === "ChildAggregate"
    || fieldMetadata.type === "ChildrenAggregate"
    || fieldMetadata.type === "bytearray"
  ) return (
    <div className={`${ERROR_CLASS_NAME} ${className ?? ""}`}>
      {fieldMetadata.type} 型のフィールドは Field コンポーネントでは表示できません: {propsName}
    </div>
  )

  let renderUiElement: ReactHookForm.ControllerProps<TFormValues, TFieldPath>["render"] | undefined = undefined

  //#region フォーム - 単語
  if (fieldMetadata.type === "word") renderUiElement = ({ field: { value, ...restField } }) => (
    <WordTextBox
      maxLength={fieldMetadata.customMaxLength}
      className={className}
      value={value ?? ""}
      {...restField}
      {...restProps}
    />
  )
  //#endregion フォーム - 単語

  //#region フォーム - 文章
  if (fieldMetadata.type === "description") {
    if (contextValue.objectType === 'SearchCondition') {
      // 検索条件オブジェクトの場合は部分一致の言葉が入力されるだけなので単純にテキストボックス
      renderUiElement = ({ field: { value, ...restField } }) => (
        <WordTextBox
          maxLength={fieldMetadata.customMaxLength}
          className={className}
          value={value ?? ""}
          {...restField}
          {...restProps}
        />
      )
    } else {
      renderUiElement = ({ field: { value, ...restField } }) => (
        <DescriptionTextArea
          value={value ?? ""}
          {...restField}
          className={`flex-1 overflow-y-auto ${className ?? ""}`}
          // input要素ではなくtextarea要素なのでpropsの型が違うのだが、
          // classNameなどよく使う属性については問題ないので強制的にキャスト
          {...restProps as unknown as React.TextareaHTMLAttributes<HTMLTextAreaElement>}
        />
      )
    }
  }
  //#endregion フォーム - 文章

  //#region フォーム - 真偽値
  if (fieldMetadata.type === "bool") {
    renderUiElement = ({ field: { value, ...restField } }) => (
      <CheckBox
        checked={!!value}
        className={className}
        {...restField}
        {...restProps}
      />
    )
  }
  //#endregion フォーム - 真偽値

  //#region フォーム - 日付
  if (fieldMetadata.type === "date"
    || fieldMetadata.type === "datetime"
    || fieldMetadata.type === "yearmonth"
  ) {
    const appearance: "datetime" | "date" | "yearmonth" = fieldMetadata.type

    if (contextValue.objectType === 'SearchCondition' && !fieldMetadata.onlySearchCondition) {
      // 検索条件オブジェクトの場合は範囲検索
      renderUiElement = ({ field: { name, onBlur, onChange, ref, value } }) => (
        <div className={`flex items-center gap-2 ${className ?? ""}`} onBlur={onBlur}>
          <DateInput
            appearance={appearance}
            name={name + ".from"}
            onChange={e => onChange({ from: e.target.value, to: value?.to })}
            ref={ref}
            value={value?.from ?? ""} // input type="date" は null を受け付けないので空文字列
            {...restProps}
          />
          <span>～</span>
          <DateInput
            appearance={appearance}
            name={name + ".to"}
            onChange={e => onChange({ to: e.target.value, from: value?.from })}
            value={value?.to ?? ""} // input type="date" は null を受け付けないので空文字列
            {...restProps}
          />
        </div>
      )

    } else {
      renderUiElement = ({ field: { value, ...restField } }) => (
        <DateInput
          appearance={appearance}
          className={className}
          value={value ?? ""}
          {...restField}
          {...restProps}
        />
      )
    }
  }
  //#endregion フォーム - 日付

  //#region フォーム - 数値
  if (fieldMetadata.type === "decimal"
    || fieldMetadata.type === "int"
    || fieldMetadata.type === "sequence"
    || fieldMetadata.type === "year"
  ) {
    let decimalDigit: number | undefined = undefined
    if (fieldMetadata.decimalPlace !== undefined) {
      decimalDigit = fieldMetadata.decimalPlace
    } else if (fieldMetadata.type === "decimal") {
      decimalDigit = 7
    } else if (fieldMetadata.type === "int" || fieldMetadata.type === "sequence" || fieldMetadata.type === "year") {
      decimalDigit = 0
    }

    let integerDigit: number | undefined = undefined
    if (fieldMetadata.totalDigit !== undefined) {
      integerDigit = fieldMetadata.totalDigit - (decimalDigit || 0)
    } else if (fieldMetadata.type === "year") {
      integerDigit = 4
    } else {
      integerDigit = 18 - (decimalDigit || 0)
    }

    let suffix: string | undefined = undefined
    let commaSeparated: boolean = false
    if (fieldMetadata.isCurrency) {
      suffix = "円"
      commaSeparated = true
    } else if (fieldMetadata.isQuantity) {
      suffix = "個"
      commaSeparated = true
    }

    if (contextValue.objectType === 'SearchCondition' && !fieldMetadata.onlySearchCondition) {
      // 検索条件オブジェクトの場合は範囲検索
      renderUiElement = ({ field: { name, onBlur, onChange, ref, value } }) => (
        <div className={`flex items-center gap-2 ${className ?? ""}`} onBlur={onBlur}>
          <NumericTextBox
            integerDigit={integerDigit}
            decimalDigit={decimalDigit}
            name={name + ".from"}
            onChange={e => onChange({ from: e.target.value, to: value?.to })}
            ref={ref}
            value={value?.from ?? ""} // input要素は null を受け付けないので空文字列
            suffix={suffix}
            commaSeparated={commaSeparated}
            {...restProps}
          />
          <span>～</span>
          <NumericTextBox
            integerDigit={integerDigit}
            decimalDigit={decimalDigit}
            name={name + ".to"}
            onChange={e => onChange({ to: e.target.value, from: value?.from })}
            value={value?.to ?? ""} // input要素は null を受け付けないので空文字列
            suffix={suffix}
            commaSeparated={commaSeparated}
            {...restProps}
          />
        </div>
      )
    } else {
      renderUiElement = ({ field: { value, ...restField } }) => (
        <NumericTextBox
          integerDigit={integerDigit}
          decimalDigit={decimalDigit}
          className={className}
          suffix={suffix}
          commaSeparated={commaSeparated}
          value={value ?? ""}
          {...restField}
          {...restProps}
        />
      )
    }
  }
  //#endregion フォーム - 数値

  //#region フォーム - 列挙体
  if (('enumType' in fieldMetadata) && fieldMetadata.enumType) {
    const enumValues = EnumValueMap[fieldMetadata.enumType]()
    if (contextValue.objectType === 'SearchCondition') {
      renderUiElement = ({ field }) => (
        <div className={`flex flex-wrap gap-2 items-center ${className ?? ""}`}>
          {enumValues.map(value => (
            <label key={value} className="flex items-center gap-1">
              <input
                type="checkbox"
                checked={field.value?.[value] ?? false}
                onChange={e => {
                  const newValue = { ...field.value, [value]: e.target.checked }
                  field.onChange(newValue)
                }}
                onBlur={field.onBlur}
                ref={field.ref}
                {...restProps}
              />
              {value}
            </label>
          ))}
        </div>
      )
    } else {
      renderUiElement = ({ field }) => (
        <select
          {...field}
          // input要素ではなくselect要素なのでpropsの型が違うのだが、
          // classNameなどよく使う属性については問題ないので強制的にキャスト
          {...restProps as unknown as React.SelectHTMLAttributes<HTMLSelectElement>}
          className={`p-1 border ${restProps.readOnly || restProps.disabled ? "border-transparent" : "border-gray-600"} ${className ?? ""}`}
        >
          <option key="" value=""></option>
          {enumValues.map(value => (
            <option key={value} value={value}>{value}</option>
          ))}
        </select>
      )
    }
  }
  //#endregion フォーム - 列挙体

  // 未実装
  if (!renderUiElement) return (
    <div className={`${ERROR_CLASS_NAME} ${className ?? ""}`}>
      {fieldMetadata.type} 型のフィールドは未実装です: {propsName}
    </div>
  )

  return (
    <ReactHookForm.Controller
      name={propsName}
      control={control}
      render={renderUiElement}
    />
  )
}

