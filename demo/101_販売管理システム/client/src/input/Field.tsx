import React from "react"
import * as ReactHookForm from "react-hook-form"
import { QueryModelType, StructureModelType } from "../__autoGenerated"
import { MetadataForPage } from "../__autoGenerated/util/metadata-for-page"
import * as DetailMessage from "../util/DetailMessageContext"
import { WordTextBox } from "./WordTextBox"
import { CheckBox } from "./CheckBox"
import { DateInput } from "./DateInput"
import { NumericTextBox } from "./NumericTextBox"
import { DescriptionTextArea } from "./DescriptionTextArea"

/**
 * nijoが自動生成するメタデータを参照して
 * テキストボックスやチェックボックスなどの入力フォームを自動的に判別して
 * 適切なUIを表示するコンポーネントを利用するために必要なコンテキスト情報を提供する。
 */
export function FieldContextProvider({
  type,
  children,
}: {
  /** このコンテキストがどのデータ構造と対応するか */
  type: QueryModelType | StructureModelType
  children?: React.ReactNode
}) {

  const getMetadataOfField = React.useMemo((): FieldUiContextValue => {
    const metadata: MetadataForPage.RootAggregateMetadata = MetadataForPage.getAll()[type]
    return createGetMetadataOfField(metadata)
  }, [type])

  return (
    <FieldUiContext.Provider value={getMetadataOfField}>
      {children}
    </FieldUiContext.Provider>
  );
}

// ----------------------------------------

export type FieldProps<
  TFormValues extends ReactHookForm.FieldValues,
  TFieldPath extends ReactHookForm.Path<TFormValues>
> = Omit<React.InputHTMLAttributes<HTMLInputElement>, "name" | "ref"> & {
  name: TFieldPath
  control: ReactHookForm.Control<TFormValues>
}

/**
 * nijoが自動生成するメタデータを参照して
 * テキストボックスやチェックボックスなどの入力フォームを自動的に判別して
 * 適切なUIを表示するコンポーネント。
 * 最大桁数の参照やエラーメッセージ表示などを自動的に行う。
 */
export function Field<
  TFormValues extends ReactHookForm.FieldValues,
  TFieldPath extends ReactHookForm.Path<TFormValues>
>({
  name: propsName,
  control,
  ...rest
}: FieldProps<TFormValues, TFieldPath>): React.ReactNode {

  const getMetadataOfField = React.useContext(FieldUiContext)
  const fieldMetadata = React.useMemo(() => {
    return getMetadataOfField(propsName as string)
  }, [getMetadataOfField, propsName])

  // エラー
  if (!fieldMetadata) return (
    <div className={ERROR_CLASS_NAME}>
      指定されたフィールドのメタデータが見つかりません: {propsName as string}
    </div>
  )
  if (fieldMetadata.type === "RootAggregate"
    || fieldMetadata.type === "ChildAggregate"
    || fieldMetadata.type === "ChildrenAggregate"
    || fieldMetadata.type === "bytearray"
  ) return (
    <div className={ERROR_CLASS_NAME}>
      {fieldMetadata.type} 型のフィールドは Field コンポーネントでは表示できません: {propsName as string}
    </div>
  )

  let renderUiElement: ReactHookForm.ControllerProps<TFormValues, TFieldPath>["render"] | undefined = undefined

  // 単語
  if (fieldMetadata.type === "word") renderUiElement = ({ field }) => (
    <WordTextBox
      maxLength={fieldMetadata.customMaxLength}
      {...field}
      {...rest}
    />
  )

  // 真偽値
  if (fieldMetadata.type === "bool") renderUiElement = ({ field }) => (
    <CheckBox
      {...field}
      {...rest}
    />
  )

  // 日付
  if (fieldMetadata.type === "date"
    || fieldMetadata.type === "datetime"
    || fieldMetadata.type === "yearmonth"
  ) renderUiElement = ({ field }) => (
    <DateInput
      appearance={fieldMetadata.type as "datetime" | "date" | "yearmonth"}
      {...field}
      {...rest}
    />
  )

  // 数値
  if (fieldMetadata.type === "decimal"
    || fieldMetadata.type === "int"
    || fieldMetadata.type === "sequence"
    || fieldMetadata.type === "year"
  ) {
    const decimalDigit = fieldMetadata.decimalPlace === undefined ? undefined : Number(fieldMetadata.decimalPlace)
    const integerDigit = fieldMetadata.totalDigit === undefined ? undefined : Number(fieldMetadata.totalDigit) - (decimalDigit || 0)

    renderUiElement = ({ field }) => (
      <NumericTextBox
        integerDigit={integerDigit}
        decimalDigit={decimalDigit}
        {...field}
        {...rest}
      />
    )
  }

  // 複数行テキスト
  if (fieldMetadata.type === "description") renderUiElement = ({ field }) => (
    <DescriptionTextArea
      {...field}
      // input要素ではなくtextarea要素なのでpropsの型が違うのだが、
      // classNameなどよく使う属性については問題ないので強制的にキャスト
      {...rest as unknown as React.TextareaHTMLAttributes<HTMLTextAreaElement>}
    />
  )

  // 未実装
  if (!renderUiElement) return (
    <div className={ERROR_CLASS_NAME}>
      {fieldMetadata.type} 型のフィールドは未実装です: {propsName as string}
    </div>
  )

  return (
    <>
      {/* 入力コンポーネント本体 */}
      <ReactHookForm.Controller
        name={propsName}
        control={control}
        render={renderUiElement}
      />
      {/* 該当のフィールドに対するエラーメッセージ */}
      <DetailMessage.Of name={propsName} control={control} />
    </>
  )
}

const ERROR_CLASS_NAME = "text-white bg-rose-600 p-1"

// ----------------------------------------

/**
 * 内部利用コンテキスト内容の型定義
 *
 * @param fieldName フォームで扱うデータのルートから当該フィールドまでのパス。htmlのname属性および react-hook-formのフィールド名と同じ。
 * @return 当該フィールドのメタデータ情報
 */
type FieldUiContextValue = (fieldName: string) => (MetadataForPage.StructureMetadataMember | undefined)

/**
 * 内部利用コンテキスト
 */
const FieldUiContext = React.createContext<FieldUiContextValue>(() => undefined)

/**
 * フィールド名からルート集約内の該当メタデータを取得する関数を生成する。
 */
const createGetMetadataOfField = (rootAggregateMetadata: MetadataForPage.RootAggregateMetadata): FieldUiContextValue => {
  return fieldName => {
    const parts = fieldName.split('.')

    // メタデータはスキーマ定義上の名前のみで構成されるが、
    // 実際のオブジェクトでは一部不要なものが含まれるので除外する。
    const filteredParts: string[] = []
    for (let i = 0; i < parts.length; i++) {
      const part = parts[i]

      // 検索条件オブジェクトの場合は最初が "filter" で始まる
      if (i === 0 && part === "filter") continue;

      // 編集可能オブジェクトの場合は最後から2番目が "values" になっている
      if (i === parts.length - 2 && part === "values") continue;

      // 半角数値のみから構成される部分は配列インデックスなので除外する
      if (/^\d+$/.test(part)) continue;

      filteredParts.push(part)
    }

    // メタデータを辿る。
    // 子要素は "members" プロパティに格納されている。
    let currentMetadata: MetadataForPage.StructureMetadataMember | MetadataForPage.RootAggregateMetadata | undefined = rootAggregateMetadata
    for (const part of filteredParts) {
      if (!currentMetadata || !('members' in currentMetadata)) {
        currentMetadata = undefined
        break
      }
      currentMetadata = currentMetadata.members[part]
    }

    return currentMetadata
  }
}
