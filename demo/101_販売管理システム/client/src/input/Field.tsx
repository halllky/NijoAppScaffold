/**
 * nijoが自動生成するメタデータを参照して
 * テキストボックスやチェックボックスなどの入力フォームを自動的に判別して
 * 適切なUIを表示するコンポーネントを利用するために必要なコンテキスト情報を提供する。
 */
export function useFieldUiContextProvider(type: QueryModelType, objectType: FieldUiContextValue['objectType']): FieldUiContextValue
export function useFieldUiContextProvider(type: StructureModelType): FieldUiContextValue
export function useFieldUiContextProvider(
  type: QueryModelType | StructureModelType,
  propsObjectType?: FieldUiContextValue['objectType'] | undefined
): FieldUiContextValue {
  return React.useMemo((): FieldUiContextValue => {
    const allMetadata = MetadataForPage.getAll()
    const metadata: MetadataForPage.RootAggregateMetadata = allMetadata[type]
    const objectType = propsObjectType ?? 'EditableObject'

    // 参照先のメタデータを取得する関数
    const getRefTo: FieldUiContextValue['getRefTo'] = (refTo) => {
      // 参照先が子孫集約の場合はスラッシュ区切りになっている
      const [refToRoot, ...refToDescendantPath] = refTo.slice('ref-to:'.length).split('/')
      const trace: {}[] = [allMetadata[refToRoot as keyof typeof allMetadata]]

      // 参照先の子孫集約を辿る
      for (const part of refToDescendantPath) {
        const current = trace[trace.length - 1]
        if (!('members' in current)
          || typeof current.members !== 'object'
          || current.members === null
          || !(part in current.members)) {
          console.error(`getRefTo - ${refTo}`, { trace, part })
          throw new Error(`参照先のメタデータが見つかりません: ${refTo}`)
        }
        const next = current[part as keyof typeof current] as MetadataForPage.StructureMetadata
        trace.push(next)
      }

      return trace[trace.length - 1] as MetadataForPage.StructureMetadata
    }

    return {
      isInContext: true,
      objectType,
      getRefTo,
      getMetadataOfField: createGetMetadataOfField(objectType, metadata, getRefTo),
    }
  }, [type, propsObjectType])
}

