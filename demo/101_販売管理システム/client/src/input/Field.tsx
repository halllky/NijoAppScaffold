export type DataTableColumnDefinitionHelper<TGridRow> = <TPath extends ReactHookForm.Path<TGridRow>>(
  pathFromGridRow: TPath,
  options?: Partial<DataTableColumn<TGridRow>>,
) => DataTableColumn<TGridRow>

/**
 * グリッドのカラム定義を書きやすくするためのヘルパー関数
 */
export function useGridColumnHelper<TGridRow>(
  pathFromFormRootToGridRow: string,
  fieldContext: FieldUiContextValue,
): DataTableColumnDefinitionHelper<TGridRow> {

  const helper: DataTableColumnDefinitionHelper<TGridRow> = React.useCallback(<TPath extends ReactHookForm.Path<TGridRow>>(
    pathFromGridRow: TPath,
    options?: Partial<DataTableColumn<TGridRow>>,
  ) => {
    const fullPath = pathFromFormRootToGridRow === ''
      ? pathFromGridRow
      : `${pathFromFormRootToGridRow}.${pathFromGridRow}`
    const fieldMetadata = fieldContext.getMetadataOfField(fullPath)

    // エラー
    if (!fieldContext.isInContext) return {
      header: pathFromGridRow,
      render: () => <span className={ERROR_CLASS_NAME}>FieldContextProvider が配置されていません。</span>,
      ...options,
    }
    if (!fieldMetadata) return {
      header: pathFromGridRow,
      render: () => <span className={ERROR_CLASS_NAME}>指定されたフィールドのメタデータが見つかりません: {fullPath}</span>,
      ...options,
    }

    //#region グリッド - 単語
    if (fieldMetadata.type === "word") return {
      header: fieldMetadata.displayName,
      render: row => ReactHookForm.get(row, pathFromGridRow),
      ...options,
    }
    //#endregion グリッド - 単語

    //#region グリッド - 文章
    if (fieldMetadata.type === "description") return {
      header: fieldMetadata.displayName,
      render: row => ReactHookForm.get(row, pathFromGridRow),
      ...options,
    }
    //#endregion グリッド - 文章

    //#region グリッド - 真偽値
    if (fieldMetadata.type === "bool") return {
      header: fieldMetadata.displayName,
      render: row => {
        const value = ReactHookForm.get(row, pathFromGridRow)
        return value ? '✔' : ''
      },
      ...options,
    }
    //#endregion グリッド - 真偽値

    //#region グリッド - 日付
    if (fieldMetadata.type === "date"
      || fieldMetadata.type === "datetime"
      || fieldMetadata.type === "yearmonth"
    ) {
      return {
        header: fieldMetadata.displayName,
        render: row => ReactHookForm.get(row, pathFromGridRow),
        widthPx: 200,
        ...options,
      }
    }
    //#endregion グリッド - 日付

    //#region グリッド - 数値
    if (fieldMetadata.type === "decimal"
      || fieldMetadata.type === "int"
      || fieldMetadata.type === "sequence"
      || fieldMetadata.type === "year"
    ) {
      let commaSeparated: boolean = false
      let suffix: string | undefined = undefined
      if (fieldMetadata.isCurrency) {
        suffix = "円"
        commaSeparated = true
      } else if (fieldMetadata.isQuantity) {
        suffix = "個"
        commaSeparated = true
      }

      return {
        header: fieldMetadata.displayName,
        render: row => {
          let value = ReactHookForm.get(row, pathFromGridRow)
          if (commaSeparated) {
            const num = Number(value)
            if (Number.isFinite(num)) value = num.toLocaleString()
          }
          return (
            <span className="block w-full py-px px-1 truncate text-right">
              {value}{suffix}
            </span>
          )
        },
        ...options,
      }
    }
    //#endregion グリッド - 数値

    //#region グリッド - 列挙体
    if (('enumType' in fieldMetadata) && fieldMetadata.enumType) {
      return {
        header: fieldMetadata.displayName,
        render: row => ReactHookForm.get(row, pathFromGridRow),
        ...options,
      }
    }
    //#endregion グリッド - 列挙体

    // 未実装
    return {
      header: fieldMetadata.displayName,
      render: () => (
        <span className={ERROR_CLASS_NAME}>
          {fieldMetadata.type} 型のフィールドは未実装です
        </span>
      ),
      ...options,
    }
  }, [pathFromFormRootToGridRow, fieldContext])

  return helper
}

