/**
 * 内部利用コンテキスト内容の型定義
 *
 * @param fieldName フォームで扱うデータのルートから当該フィールドまでのパス。htmlのname属性および react-hook-formのフィールド名と同じ。
 * @return 当該フィールドのメタデータ情報
 */
type FieldUiContextValue = {
  /** このコンテキストが有効な範囲内であるかどうか */
  isInContext: boolean
  /** このコンテキストが扱うオブジェクトの種類 */
  objectType: 'EditableObject' | 'SearchCondition'
  /** フィールド名からルート集約内の該当メタデータを取得する関数 */
  getMetadataOfField: (fieldName: string) => (MetadataForPage.StructureMetadataMember | undefined)
  /** 参照先のメタデータを取得する関数。見つからない場合は例外を投げる */
  getRefTo: (refTo: `ref-to:${string}`) => MetadataForPage.StructureMetadata
}

/**
 * 内部利用コンテキスト
 */
export const FieldUiContext = React.createContext<FieldUiContextValue>({
  isInContext: false,
  objectType: 'EditableObject',
  getMetadataOfField: () => undefined,
  getRefTo: () => { throw new Error() },
})

/**
 * フィールド名からルート集約内の該当メタデータを取得する関数を生成する。
 */
const createGetMetadataOfField = (
  objectType: FieldUiContextValue['objectType'],
  rootAggregateMetadata: MetadataForPage.RootAggregateMetadata,
  getRefTo: FieldUiContextValue['getRefTo'],
): FieldUiContextValue['getMetadataOfField'] => {

  return fieldName => {
    const parts = fieldName.split('.')

    // メタデータはスキーマ定義上の名前のみで構成されるが、
    // 実際のオブジェクトでは一部不要なものが含まれるので除外する。
    const filteredParts: string[] = []
    for (let i = 0; i < parts.length; i++) {
      const part = parts[i]

      // 検索条件オブジェクトの場合は最初が "filter" で始まる
      if (objectType === 'SearchCondition' && i === 0 && part === "filter") continue;

      // 半角数値のみから構成される部分は配列インデックスなので除外する
      if (/^\d+$/.test(part)) continue;

      filteredParts.push(part)
    }

    // メタデータを辿る。
    // 子要素は "members" プロパティに格納されている。
    const trace: (MetadataForPage.StructureMetadataMember | MetadataForPage.RootAggregateMetadata)[] = [rootAggregateMetadata]
    while (true) {
      const part = filteredParts.shift()

      // 最後まで辿ったら終了
      if (!part) break

      const current = trace[trace.length - 1]

      // 最後でないならば絶対にmembersが存在するはず
      if (!('members' in current)
        || typeof current.members !== 'object'
        || current.members === null
        || !(part in current.members)) {
        console.error(`getMetadataOfField - ${fieldName}`, { objectType, filteredParts, trace, part, current })
        throw new Error(`フィールドのメタデータが見つかりません: ${fieldName}`)
      }

      const next = current.members[part]
      trace.push(next)

      // いま見つけたメンバーが外部参照であり、
      // かつまだ辿るべき部分が残っている場合、
      // 参照先のメタデータを取得してそこから辿る
      if (filteredParts.length > 0 && next.type === "ref-to") {
        trace.push(getRefTo(next.refTo))
      }
    }

    return trace[trace.length - 1] as MetadataForPage.StructureMetadataMember
  }
}
