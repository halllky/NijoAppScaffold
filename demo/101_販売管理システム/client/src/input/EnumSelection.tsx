import React from "react"
import * as AutoGenerated from "../__autoGenerated/enum-defs"

export type EnumSelectionProps<
  T extends keyof AutoGenerated.EnumTypeMap
> = React.SelectHTMLAttributes<HTMLSelectElement> & {
  type: T
  /** 選択されている値 */
  value?: AutoGenerated.EnumTypeMap[T] | null
  /** SelectHTMLAttibutesにはreadOnlyがないため独自に追加 */
  readOnly?: boolean
}

/**
 * 列挙型の選択コンポーネント。
 * 選択肢をドロップダウンで表示し、1つ選択できるようにします。
 * 以下の機能をもちます。
 *
 * - 基本的な共通レイアウト（以下、例）
 *   - 入力可能なら枠と背景色を変更
 *   - 入力不可能なら枠なし背景色なし
 * - 未選択状態（空行）をサポート
 */
export const EnumSelection = React.forwardRef(<T extends keyof AutoGenerated.EnumTypeMap>(props: EnumSelectionProps<T>, ref: React.Ref<HTMLSelectElement>) => {
  const { type, className, readOnly, disabled, value, ...rest } = props

  const options = AutoGenerated.EnumValueMap[type]()

  const baseStyle = "block py-px px-1 border outline-blue-500 focus:ring-blue-500"
  const editableStyle = "border-gray-700 bg-white"
  const readOnlyStyle = "border-transparent bg-transparent shadow-none cursor-default focus:ring-0"

  const isEditable = !readOnly && !disabled

  return (
    <select
      ref={ref}
      disabled={disabled || readOnly}
      className={`${baseStyle} ${isEditable ? editableStyle : readOnlyStyle} ${className ?? ''} ${readOnly ? 'pointer-events-none' : ''}`}
      value={value ?? ""}
      {...rest}
    >
      <option value=""></option>
      {options.map(opt => (
        <option key={opt} value={opt}>{opt}</option>
      ))}
    </select>
  )
}) as (<T extends keyof AutoGenerated.EnumTypeMap>(props: EnumSelectionProps<T> & { ref?: React.Ref<HTMLSelectElement> }) => React.ReactNode)
