import { createNew従業員RefSearchCondition, 従業員RefRefTarget, 従業員RefSearchCondition, 従業員RefSearchConditionFilter } from "../../__autoGenerated/従業員Ref"
import { defineSearchDialog } from "./SearchDialogBase"
import { callComplexPostEndpointAsync } from "../../example/callComplexPostEndpointAsync"
import { FormLabel } from "../../layout/FormLabel"
import * as UI from "../Field"

export const [
  /**
   * `ref-to:従業員Ref` の検索ダイアログコンポーネント。
   * 検索条件欄ではなく詳細画面の入力で使用します。
   */
  RefTo従業員,

  /**
   * `ref-to:従業員Ref` の検索ダイアログコンポーネント。
   * 詳細画面ではなく検索条件欄の入力で使用します。
   */
  RefTo従業員SearchCondition,

] = defineSearchDialog<
  従業員RefSearchCondition,
  従業員RefRefTarget,
  従業員RefSearchConditionFilter,
  {}
>({
  title: '従業員',
  queryModelType: '従業員Ref',

  // ダイアログ内部でなくその呼び出し元で使用する情報
  codePathFromRefTarget: '従業員番号',
  namePathFromRefTarget: '氏名',
  codePathFromSearchCondition: '従業員番号',
  namePathFromSearchCondition: '氏名',
  codeWidthRem: 10,
  convertCodeToSearchCondition: code => {
    const searchCondition = createNew従業員RefSearchCondition()
    searchCondition.filter.従業員番号 = code
    return searchCondition
  },

  // 初期検索条件
  getDefaultSearchCondition: () => {
    const searchCondition = createNew従業員RefSearchCondition()
    searchCondition.sort = ['従業員Ref.従業員番号（昇順）']
    return searchCondition
  },

  // 検索処理
  onSearch: async (searchCondition, signal) => {
    return await callComplexPostEndpointAsync('ref-to:従業員Ref', searchCondition, {
      ignoreConfirm: true,
      signal,
    })
  },

  // このコンポーネントが検索条件欄で使用されるときのための実装
  convertRefTargetToFilter: refTarget => {
    const filter = createNew従業員RefSearchCondition().filter
    filter.従業員番号 = refTarget.従業員番号
    filter.氏名 = refTarget.氏名
    return filter
  },

  // 検索結果グリッドの列定義
  getSearchResultGridColumns: columnFor => [
    columnFor('従業員番号', { header: '従業員番号', widthPx: 120 }),
    columnFor('氏名', { header: '氏名', widthPx: 200 }),
  ],

  // 検索条件欄のレンダリング
  SearchCondition: ({ formMethods }) => {
    const contextValue = UI.useFieldUiContextProvider('従業員Ref', 'SearchCondition')

    return (
      <UI.FieldUiContext.Provider value={contextValue}>
        <div className="grid grid-cols-[10rem_1fr] gap-2">

          <FormLabel>従業員番号</FormLabel>
          <UI.Field name="filter.従業員番号" control={formMethods.control} />

          <FormLabel>氏名</FormLabel>
          <UI.Field name="filter.氏名" control={formMethods.control} />

        </div>
      </UI.FieldUiContext.Provider>
    )
  },
})
