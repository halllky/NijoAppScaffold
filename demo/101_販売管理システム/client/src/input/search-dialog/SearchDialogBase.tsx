/**
 * 検索ダイアログ自体ではなくダイアログ定義関数の引数の型
 */
export type DefineSearchDialogProps<
  TSearchCondition extends ReactHookForm.FieldValues,
  TSearchResultRefTarget,
  TSearchResultFilter,
  TDialogProps,
> = {
  /** ダイアログのタイトル */
  title: string
  /** 編集画面用のオブジェクトからコード値までのパス */
  codePathFromRefTarget: ReactHookForm.Path<TSearchResultRefTarget>
  /** 検索条件欄用のオブジェクトから名称までのパス */
  namePathFromRefTarget: ReactHookForm.Path<TSearchResultRefTarget>
  /** 検索条件欄用のオブジェクトからコード値までのパス */
  codePathFromSearchCondition: ReactHookForm.Path<TSearchResultFilter>
  /** 検索条件欄用のオブジェクトから名称までのパス */
  namePathFromSearchCondition: ReactHookForm.Path<TSearchResultFilter>
  /** コード値のテキストボックスの横幅（CSSの「rem」単位） */
  codeWidthRem: number
  /** 検索結果欄のメタデータ参照に使用するクエリモデルの種類 */
  queryModelType: QueryModelType

  /**
   * ダイアログ表示時の初期検索条件を取得します。
   * なお、 skip, take は既定値が設定されるため、ここで設定する必要はありません。
   */
  getDefaultSearchCondition: (dialogProps: TDialogProps) => TSearchCondition
  /**
   * 検索条件欄のレンダリング。
   * form タグの内側にレンダリングされます。
   */
  SearchCondition: (props: SearchDialogConditionProops<TSearchCondition, TDialogProps>) => React.ReactNode
  /**
   * 検索結果欄のグリッドの列定義を取得します。
   * なお、グリッド左端の選択ボタンは自動で追加されるため、ここで定義する必要はありません。
   */
  getSearchResultGridColumns: (
    columnFor: DataTableColumnDefinitionHelper<TSearchResultRefTarget>,
    dialogProps: TDialogProps,
  ) => DataTableColumn<TSearchResultRefTarget>[]
  /**
   * 検索処理関数
   */
  onSearch: (
    formValues: TSearchCondition,
    signal: AbortSignal | undefined,
  ) => Promise<ComplexPostResult<LoadReturnType<TSearchResultRefTarget>>>
  /**
   * コード値から検索条件を作成します。
   * 詳細画面でフォーカスアウトした際の自動検索に使用します。
   */
  convertCodeToSearchCondition: (code: string) => TSearchCondition
  /**
   * ダイアログ内部の検索結果はRefTarget型だが、
   * 検索画面の検索条件欄で使用する場合はその画面のSearchConditionのFilter型になるため、
   * 前者から後者への変換関数。
   */
  convertRefTargetToFilter: (refTarget: TSearchResultRefTarget) => TSearchResultFilter
}

export type SearchDialogConditionProops<
  TSearchCondition extends ReactHookForm.FieldValues,
  TDialogProps,
> = {
  /** 検索条件欄の useForm メソッド */
  formMethods: ReactHookForm.UseFormReturn<TSearchCondition>
  /** ダイアログ呼び出し元で指定されたオプション */
  dialogProps: TDialogProps
}

/**
 * 検索ダイアログコンポーネントの props の型
 */
export type SearchDialogComponentProps<
  TFormRoot extends ReactHookForm.FieldValues,
  TSearchResultItem,
  TPath extends ReactHookForm.FieldPathByValue<TFormRoot, TSearchResultItem>,
  TDialogProps,
> = TDialogProps & {
  name: TPath
  control: ReactHookForm.Control<TFormRoot>
  readOnly?: boolean
  className?: string
}

/**
 * 検索ダイアログコンポーネントを定義します。
 * 参照先のデータが、１個のコードと１個の名称を持っているという前提で実装されています。
 *
 * ## このベースの責務
 *
 * - 呼び出し元コンポーネントの定義
 *   - コード値
 *     - readOnly 時のコード値入力不可対応
 *     - 詳細画面ではフォーカスアウト時に検索処理を自動的に実行する
 *     - 検索条件欄では手入力された値をそのまま検索条件として使用する
 *   - 名称値
 *     - 詳細画面では手入力不可。検索条件欄では手入力可能
 *     - もし検索条件欄でコードと名称で不一致がある場合、検索結果としては何もヒットしないので注意
 * - ダイアログの表示/非表示管理
 * - 検索結果欄のレンダリング、選択ボタン、ページング
 * - 検索ボタン、クリアボタン、ダイアログを閉じるUI
 * - 検索条件欄の useForm 状態管理
 *
 * ## カスタマイズポイント
 *
 * - 呼び出し元コンポーネント
 *   - このベースが呼び出し元画面から name, control, readOnly, className を受け取って
 *     呼び出し元のコンポーネントをレンダリングするために必要な情報
 * - 検索ダイアログ
 *   - 検索条件欄のレンダリング
 *   - 検索条件の初期値の定義
 *   - 検索処理の実装
 *   - 検索結欄のグリッドの列定義
 */
export function defineSearchDialog<
  TSearchCondition extends ReactHookForm.FieldValues,
  TSearchResultRefTarget,
  TSearchResultFilter,
  TDialogProps,
>(def: DefineSearchDialogProps<TSearchCondition, TSearchResultRefTarget, TSearchResultFilter, TDialogProps>) {

  const SearchDialog = ({
    isOpen,
    onClose,
    onSelect,
    dialogProps,
  }: {
    isOpen: boolean
    onClose: () => void
    onSelect: (item: TSearchResultRefTarget) => void
    dialogProps: TDialogProps
  }) => {
    const formMethods = ReactHookForm.useForm<TSearchCondition>({
      defaultValues: def.getDefaultSearchCondition(dialogProps) as ReactHookForm.DefaultValues<TSearchCondition>,
    })
    const { register, handleSubmit, control, reset, getValues } = formMethods

    const [searchResults, setSearchResults] = React.useState<TSearchResultRefTarget[]>([])
    const [totalCount, setTotalCount] = React.useState(0)
    const [pageIndex, setPageIndex] = React.useState(0)
    const [isLoading, setIsLoading] = React.useState(false)
    const [searchConditionVisible, setSearchConditionVisible] = React.useState(true)
    const pageSize = 20
    const searchResultFieldContext = useFieldUiContextProvider(def.queryModelType, 'EditableObject')
    const columnFor = useGridColumnHelper<TSearchResultRefTarget>('', searchResultFieldContext)

    const executeSearch = React.useCallback(async (data: TSearchCondition, page: number) => {
      setIsLoading(true)
      try {
        const searchData = { ...data, skip: page * pageSize, take: pageSize }
        const result = await def.onSearch(searchData, undefined)
        if (result.type === 'ok') {
          setSearchResults(result.returnValue.currentPageItems)
          setTotalCount(result.returnValue.totalCount)
        } else {
          setSearchResults([])
          setTotalCount(0)
        }
      } finally {
        setIsLoading(false)
      }
    }, [def])

    React.useEffect(() => {
      if (isOpen) {
        const defaultCondition = def.getDefaultSearchCondition(dialogProps)
        reset(defaultCondition)
        setSearchResults([])
        setTotalCount(0)
        setPageIndex(0)
        executeSearch(defaultCondition, 0)
      }
      // dialogProps はフォームリセットのたびに参照が変わる可能性があるが、
      // フォームを使用中に勝手にリセットされては困るので依存配列には含めない。
    }, [isOpen, reset, executeSearch])

    const onSearchSubmit = (data: TSearchCondition) => {
      setPageIndex(0)
      executeSearch(data, 0)
    }

    const handlePageChange = (newPageIndex: number) => {
      setPageIndex(newPageIndex)
      const data = getValues()
      executeSearch(data, newPageIndex)
    }

    const columns = React.useMemo(() => {
      const cols = def.getSearchResultGridColumns(columnFor, dialogProps)
      return [
        {
          header: "選択",
          widthPx: 64,
          render: (row: TSearchResultRefTarget) => (
            <Button mini onClick={() => onSelect(row)}>選択</Button>
          ),
        },
        ...cols,
      ]
    }, [dialogProps, columnFor])

    const totalPages = Math.ceil(totalCount / pageSize)
    const canPrev = pageIndex > 0
    const canNext = pageIndex < totalPages - 1

    const titleNode = (
      <button
        type="button"
        className="flex items-center gap-1 cursor-pointer select-none font-bold text-lg"
        onClick={() => setSearchConditionVisible(prev => !prev)}
      >
        {searchConditionVisible ? (
          <ChevronDownIcon className="h-5 w-5" />
        ) : (
          <ChevronRightIcon className="h-5 w-5" />
        )}
        {def.title} 検索
      </button>
    )

    return (
      <Modal
        isOpen={isOpen}
        onClose={onClose}
        title={titleNode}
        className="h-full"
      >
        {/* 検索条件欄 */}
        {searchConditionVisible && (
          <div className="p-4">
            <form onSubmit={handleSubmit(onSearchSubmit)}>
              <def.SearchCondition
                formMethods={formMethods}
                dialogProps={dialogProps}
              />
              <div className="mt-4 flex justify-end gap-2">
                <Button onClick={() => reset(def.getDefaultSearchCondition(dialogProps))}>クリア</Button>
                <Button submit fill icon={MagnifyingGlassIcon} loading={isLoading}>検索</Button>
              </div>
            </form>
          </div>
        )}

        {/* 検索結果欄 */}
        <div className="flex-1 flex flex-col overflow-hidden p-4 pt-2">
          <div className="flex flex-wrap items-center py-1 min-h-[2rem]">
            <div className="min-w-48 text-sm text-gray-600">
              {isLoading && (
                <>検索中...</>
              )}
              {!isLoading && totalCount > 0 && (
                <>
                  全{totalCount}件中 {pageIndex * pageSize + 1} - {Math.min((pageIndex + 1) * pageSize, totalCount)} 件を表示
                </>
              )}
              {!isLoading && totalCount === 0 && (
                <>全0件</>
              )}
            </div>
          </div>

          <DataTable
            rows={searchResults}
            columns={columns}
            className="flex-1 border border-gray-300 min-w-full"
          />
        </div>

        {/* フッタ */}
        <div className="flex items-center justify-between p-2">
          <div className="w-20"></div>
          <div className="flex gap-2 items-center">
            <Button outline disabled={!canPrev || isLoading} onClick={() => handlePageChange(pageIndex - 1)}>前へ</Button>
            <span className="px-2 py-px select-none text-sm">
              {pageIndex + 1} / {Math.max(1, totalPages)}
            </span>
            <Button outline disabled={!canNext || isLoading} onClick={() => handlePageChange(pageIndex + 1)}>次へ</Button>
          </div>
          <div className="w-20 flex justify-end">
            <Button onClick={onClose}>閉じる</Button>
          </div>
        </div>
      </Modal>
    )
  }

  // 詳細画面用のダイアログ
  function SearchDialogComponentForEditPage<
    TFormRoot extends ReactHookForm.FieldValues,
    TPath extends ReactHookForm.FieldPathByValue<TFormRoot, TSearchResultRefTarget>,
  >(props: SearchDialogComponentProps<TFormRoot, TSearchResultRefTarget, TPath, TDialogProps>): React.ReactNode {
    const { name, control, readOnly, className, ...dialogProps } = props
    const [isOpen, setIsOpen] = React.useState(false)

    const { field: parentField } = ReactHookForm.useController({ name, control })
    const currentValue = parentField.value

    const handleBlur = async (e: React.FocusEvent<HTMLInputElement>) => {
      parentField.onBlur()
      if (readOnly) return
      const code = e.target.value

      // 入力が空なら名称クリア
      if (!code) {
        const newVal = currentValue ? structuredClone(currentValue) : {}
        ReactHookForm.set(newVal, def.codePathFromRefTarget, code)
        ReactHookForm.set(newVal, def.namePathFromRefTarget, "")
        parentField.onChange(newVal)
        return
      }

      // 検索中表示のために、入力されたコードだけでも反映させておく
      {
        const newVal = currentValue ? structuredClone(currentValue) : {}
        ReactHookForm.set(newVal, def.codePathFromRefTarget, code)
        parentField.onChange(newVal)
      }

      const condition = def.convertCodeToSearchCondition(code)
      const result = await def.onSearch(condition, undefined)
      if (result.type === 'ok' && result.returnValue.currentPageItems.length === 1) {
        const item = result.returnValue.currentPageItems[0]
        parentField.onChange(item)
      } else {
        // ヒットしなかったので名称クリア
        const newVal = currentValue ? structuredClone(currentValue) : {}
        ReactHookForm.set(newVal, def.codePathFromRefTarget, code)
        ReactHookForm.set(newVal, def.namePathFromRefTarget, "")
        parentField.onChange(newVal)
      }
    }

    const handleChangeCode = (e: React.ChangeEvent<HTMLInputElement>) => {
      const val = e.target.value
      const newVal = currentValue ? structuredClone(currentValue) : {}
      ReactHookForm.set(newVal, def.codePathFromRefTarget, val)
      parentField.onChange(newVal)
    }

    const handleSelect = (item: TSearchResultRefTarget) => {
      parentField.onChange(item)
      setIsOpen(false)
    }

    const codeValue = ReactHookForm.get(currentValue, def.codePathFromRefTarget)
    const nameValue = ReactHookForm.get(currentValue, def.namePathFromRefTarget)

    return (
      <div className={`flex items-center gap-1 ${className ?? ""}`}>
        <div style={{ width: `${def.codeWidthRem}rem` }}>
          <WordTextBox
            name={`${name}.${def.codePathFromRefTarget}`}
            value={codeValue ?? ''}
            onChange={handleChangeCode}
            onBlur={handleBlur}
            ref={parentField.ref}
            readOnly={readOnly}
            className="w-full"
          />
        </div>
        <Button
          icon={MagnifyingGlassIcon}
          onClick={() => setIsOpen(true)}
          disabled={readOnly}
          tabIndex={-1}
          mini
        />
        <div className="flex-1">
          <WordTextBox
            name={`${name}.${def.namePathFromRefTarget}`}
            value={nameValue ?? ''}
            readOnly={true}
            tabIndex={-1}
            className="w-full bg-gray-100 text-gray-600"
          />
        </div>
        <SearchDialog
          isOpen={isOpen}
          onClose={() => setIsOpen(false)}
          onSelect={handleSelect}
          dialogProps={dialogProps as unknown as TDialogProps}
        />
      </div>
    )
  }

  // 検索条件欄用のダイアログ
  function SearchDialogComponentForSearchCondition<
    TFormRoot extends ReactHookForm.FieldValues,
    TPath extends ReactHookForm.FieldPathByValue<TFormRoot, TSearchResultFilter>,
  >(props: SearchDialogComponentProps<TFormRoot, TSearchResultFilter, TPath, TDialogProps>): React.ReactNode {
    const { name, control, readOnly, className, ...dialogProps } = props
    const [isOpen, setIsOpen] = React.useState(false)

    const { field: parentField } = ReactHookForm.useController({ name, control })
    const currentValue = parentField.value

    const handleChangeCode = (e: React.ChangeEvent<HTMLInputElement>) => {
      const val = e.target.value
      const newVal = currentValue ? structuredClone(currentValue) : {}
      ReactHookForm.set(newVal, def.codePathFromSearchCondition, val)
      parentField.onChange(newVal)
    }

    const handleChangeName = (e: React.ChangeEvent<HTMLInputElement>) => {
      const val = e.target.value
      const newVal = currentValue ? structuredClone(currentValue) : {}
      ReactHookForm.set(newVal, def.namePathFromSearchCondition, val)
      parentField.onChange(newVal)
    }

    const handleSelect = (item: TSearchResultRefTarget) => {
      const filter = def.convertRefTargetToFilter(item)
      parentField.onChange(filter)
      setIsOpen(false)
    }

    const codeValue = ReactHookForm.get(currentValue, def.codePathFromSearchCondition)
    const nameValue = ReactHookForm.get(currentValue, def.namePathFromSearchCondition)

    return (
      <div className={`flex items-center gap-1 ${className ?? ""}`}>
        <div style={{ width: `${def.codeWidthRem}rem` }}>
          <WordTextBox
            name={`${name}.${def.codePathFromSearchCondition}`}
            value={codeValue ?? ''}
            onChange={handleChangeCode}
            onBlur={parentField.onBlur}
            ref={parentField.ref}
            readOnly={readOnly}
            className="w-full"
          />
        </div>
        <Button
          icon={MagnifyingGlassIcon}
          onClick={() => setIsOpen(true)}
          disabled={readOnly}
          tabIndex={-1}
          mini
        />
        <div className="flex-1">
          <WordTextBox
            name={`${name}.${def.namePathFromSearchCondition}`}
            value={nameValue ?? ''}
            onChange={handleChangeName}
            onBlur={parentField.onBlur}
            readOnly={readOnly}
            className="w-full"
          />
        </div>
        <SearchDialog
          isOpen={isOpen}
          onClose={() => setIsOpen(false)}
          onSelect={handleSelect}
          dialogProps={dialogProps as unknown as TDialogProps}
        />
      </div>
    )
  }

  return [
    SearchDialogComponentForEditPage,
    SearchDialogComponentForSearchCondition,
  ] as const
}
