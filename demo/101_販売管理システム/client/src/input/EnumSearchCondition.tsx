import React from "react"
import * as ReactHookForm from "react-hook-form"
import * as AutoGenerated from "../__autoGenerated/enum-defs"
import { CheckBox } from "./CheckBox"

type EnumSearchConditionProps<
  TFieldValues extends ReactHookForm.FieldValues,
  TEnum extends keyof AutoGenerated.EnumTypeMap
> = {
  control: ReactHookForm.Control<TFieldValues>
  name: ReactHookForm.Path<TFieldValues>
  type: TEnum
  readOnly?: boolean
  className?: string
}

/**
 * 列挙型の検索条件指定コンポーネント。
 * 指定された列挙体の全要素をチェックボックスとして羅列し、
 * どの区分値を検索条件に含めるかを選択できるようにします。
 */
export const EnumSearchCondition = <
  TFieldValues extends ReactHookForm.FieldValues,
  TEnum extends keyof AutoGenerated.EnumTypeMap
>(props: EnumSearchConditionProps<TFieldValues, TEnum>) => {
  const { control, name, type, readOnly, className } = props

  const { field } = ReactHookForm.useController({
    name,
    control,
  })

  const options = AutoGenerated.EnumValueMap[type]()
  // 値が未設定の場合は空オブジェクトとして扱う
  const currentValue: Record<string, boolean> = field.value || {}

  const handleChange = (option: string, checked: boolean) => {
    const newValue = { ...currentValue }
    if (checked) {
      newValue[option] = true
    } else {
      // falseを設定するのではなくキー自体を削除する（undefined扱いにする）
      // これにより、APIリクエスト時に余計なパラメータが送信されるのを防ぐ
      delete newValue[option]
    }
    field.onChange(newValue)
  }

  return (
    <div className={`flex flex-wrap gap-x-4 gap-y-2 ${className ?? ''}`}>
      {options.map(option => (
        <CheckBox
          key={option}
          checked={!!currentValue[option]}
          onChange={e => handleChange(option, e.target.checked)}
          disabled={readOnly}
        >
          {option}
        </CheckBox>
      ))}
    </div>
  )
}
