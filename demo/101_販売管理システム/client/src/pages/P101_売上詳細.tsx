/**
 * P101 売上詳細
 */
function P101_売上詳細(props: {
  mode: 'new' | 'edit'
}) {

  // この画面で編集するオブジェクトのメタデータ
  const modelMetadata = UI.useFieldUiContextProvider("売上詳細")

  // react-hook-form のフォーム管理を利用
  const loaderData = ReactRouter.useLoaderData() as 売上詳細DisplayData
  const formMethods = ReactHookForm.useForm<売上詳細DisplayData>({
    defaultValues: loaderData,
  })
  const { register, control, formState: { errors }, getValues, setValue } = formMethods
  const { fields, append, remove } = ReactHookForm.useFieldArray({
    control,
    name: "売上詳細の売上明細"
  })

  // useFormの引数としてdefaultValuesを渡すだけだと
  // isDirtyがtrueになってしまうため、useEffectで再度初期化している。
  // またログインユーザー情報はこの時点でないと取得できない
  const { loginUser } = useLoginLogout()
  React.useLayoutEffect(() => {
    const clone = window.structuredClone(loaderData)
    // 新規登録モードの場合
    if (props.mode === 'new') {
      clone.values.売上日時 = dayjs().format('YYYY-MM-DD HH:mm:ss')
      clone.values.担当者.従業員番号 = loginUser?.values.従業員番号 ?? ''
      clone.values.担当者.氏名 = loginUser?.values.氏名 ?? ''
    }
    formMethods.reset(clone)
  }, [loaderData, loginUser])

  // 画面タイトル
  const [browserTitle, pageTitle] = React.useMemo(() => {
    if (props.mode === 'new') {
      return ["売上登録", "売上登録"]
    } else {
      const date = dayjs(loaderData.values.売上日時).format('YYYY-MM-DD')
      return [
        `売上 No.${loaderData.values.売上SEQ} (${date})`,
        `売上 No.${loaderData.values.売上SEQ}`
      ]
    }
  }, [props.mode, loaderData])

  // 明細行追加処理
  const handleCreateNewItem = useEvent((newItem: 商品RefTarget, quantity: number) => {
    const newDetail = createNew売上詳細の売上明細DisplayData()
    newDetail.values.商品 = newItem
    newDetail.values.区分 = '売上'
    newDetail.values.売上数量 = quantity.toString()

    // 商品マスタの単価は税抜きなので、ここで消費税区分を考慮して税込みに変換する
    let unitPriceInTax: Decimal
    if (newItem.消費税区分 === '一般税率') {
      const taxRate = new Decimal(1.1)
      unitPriceInTax = new Decimal(newItem.売値単価_税抜 ?? '0').mul(taxRate)

    } else if (newItem.消費税区分 === '軽減税率') {
      const taxRate = new Decimal(1.08)
      unitPriceInTax = new Decimal(newItem.売値単価_税抜 ?? '0').mul(taxRate)

    } else {
      unitPriceInTax = new Decimal(newItem.売値単価_税抜 ?? '0')
    }
    newDetail.values.売上総額_税込 = unitPriceInTax.mul(quantity).toFixed(0)

    append(newDetail)
  })

  // 保存時処理
  const handleSave: EditPageSaveEvent = useEvent(async () => {
    if (props.mode === 'new') {
      const result = await callComplexPostEndpointAsync('売上新規登録', formMethods.getValues())
      return { reload: result.type === 'ok' }

    } else {
      const result = await callComplexPostEndpointAsync('売上修正', formMethods.getValues())
      return { reload: result.type === 'ok' }
    }
  })

  return (
    <EditPageBase
      pageTitle={browserTitle}
      isDirty={formMethods.formState.isDirty}
      onSave={handleSave}
    >
      {({ save, saving }) => (
        <UI.FieldUiContext.Provider value={modelMetadata}>
          <div className="h-full flex flex-col gap-y-2">

            {/* ヘッダ */}
            <div className="flex items-start gap-1">
              <div className="flex-1 flex flex-wrap items-center gap-1 py-1">
                <h1 className="text-lg font-bold">
                  {pageTitle}
                </h1>

                <div className="flex gap-2 items-center">
                  <FormLabel className="basis-20 shrink-0 text-right">売上日時</FormLabel>
                  <UI.Field name="values.売上日時" control={control} />
                </div>

                <div className="flex gap-2 items-center">
                  <FormLabel className="basis-20 shrink-0 text-right">担当者</FormLabel>
                  <div className="flex gap-2">
                    <WordTextBox {...register("values.担当者.従業員番号")} className="w-32" />
                    {getValues("values.担当者.氏名")}
                  </div>
                </div>
              </div>

              <Button fill onClick={save} loading={saving}>
                保存
              </Button>
            </div>

            <UI.Field
              name="values.備考"
              placeholder="備考"
              className="min-h-16 max-h-28"
              control={control}
            />

            {/* 新規明細行追加欄 */}
            <NewItemArea formMethods={formMethods} onCreate={handleCreateNewItem} />

            {/* 明細欄 */}
            <div className="flex-1 overflow-auto">
              <table className="min-w-full border-collapse border border-gray-300">
                <thead className="bg-gray-100">
                  <tr>
                    <th className="border border-gray-300 p-2 w-10"></th>
                    <th className="border border-gray-300 p-2 w-40">商品コード</th>
                    <th className="border border-gray-300 p-2">商品名</th>
                    <th className="border border-gray-300 p-2 w-32">区分</th>
                    <th className="border border-gray-300 p-2 w-32">数量</th>
                    <th className="border border-gray-300 p-2 w-40">総額(税込)</th>
                    <th className="border border-gray-300 p-2">備考</th>
                  </tr>
                </thead>
                <tbody>
                  {fields.map((field, index) => (
                    <tr key={field.id}>
                      <td className="border border-gray-300 p-2 text-center">
                        <Button mini onClick={() => remove(index)}>削除</Button>
                      </td>
                      <td className="border border-gray-300 p-2">
                        <WordTextBox {...register(`売上詳細の売上明細.${index}.values.商品.外部システム側ID`)} className="w-full" />
                      </td>
                      <td className="border border-gray-300 p-2 bg-gray-50">
                        {getValues(`売上詳細の売上明細.${index}.values.商品.商品名`)}
                      </td>
                      <td className="border border-gray-300 p-2">
                        <ReactHookForm.Controller
                          control={control}
                          name={`売上詳細の売上明細.${index}.values.区分`}
                          render={({ field }) => (
                            <EnumSelection type="売上明細区分" {...field} />
                          )}
                        />
                      </td>
                      <td className="border border-gray-300 p-2">
                        <NumericTextBox {...register(`売上詳細の売上明細.${index}.values.売上数量`)} className="w-full text-right" />
                      </td>
                      <td className="border border-gray-300 p-2">
                        <NumericTextBox {...register(`売上詳細の売上明細.${index}.values.売上総額_税込`)} className="w-full text-right" />
                      </td>
                      <td className="border border-gray-300 p-2">
                        <WordTextBox {...register(`売上詳細の売上明細.${index}.values.備考`)} className="w-full" />
                      </td>
                    </tr>
                  ))}
                </tbody>
              </table>
            </div>
          </div>
        </UI.FieldUiContext.Provider>
      )}
    </EditPageBase>
  )
}

/**
 * 新規明細行追加エリア。
 * 商品と数量だけを選択する。金額調整などは追加後に行う。
 */
function NewItemArea(props: {
  formMethods: ReactHookForm.UseFormReturn<売上詳細DisplayData>
  onCreate: (newItem: 商品RefTarget, quantity: number) => void
}) {
  const [searchCode, setSearchCode] = React.useState('')
  const [quantity, setQuantity] = React.useState('1')
  const [foundItem, setFoundItem] = React.useState<商品RefTarget | null>(null)
  const [errorMessage, setErrorMessage] = React.useState<string | null>(null)
  const [searching, setSearching] = React.useState(false)

  const handleSearchCode = async () => {
    if (!searchCode) return
    setSearching(true)
    setErrorMessage(null)
    setFoundItem(null)
    try {
      const searchCondition = createNew商品SearchCondition()
      searchCondition.filter.外部システム側ID = searchCode
      searchCondition.take = '2' // ありえないが複数ヒットを考慮

      const result = await callComplexPostEndpointAsync('ref-to:商品', searchCondition)
      if (result.type === 'ok') {
        if (result.returnValue.totalCount === 0) {
          setErrorMessage('該当する商品がありません')
        } else if (result.returnValue.totalCount === 1) {
          setFoundItem(result.returnValue.currentPageItems[0])
        } else {
          setErrorMessage('複数の商品がヒットしました。コードを確認してください。')
        }
      } else {
        setErrorMessage('検索エラーが発生しました')
      }
    } catch (e) {
      setErrorMessage('検索中にエラーが発生しました')
      console.error(e)
    } finally {
      setSearching(false)
    }
  }

  const handleAdd = () => {
    if (foundItem && quantity) {
      props.onCreate(foundItem, Number(quantity))
      // リセット
      setSearchCode('')
      setQuantity('1')
      setFoundItem(null)
    }
  }

  return (
    <div className="border border-gray-300 p-2 bg-gray-50 flex flex-col gap-2">
      <div className="flex items-end gap-2">
        <div className="flex flex-col">
          <label className="text-xs text-gray-500">商品コード</label>
          <div className="flex gap-1">
            <WordTextBox
              value={searchCode}
              onChange={(e) => setSearchCode(e.target.value)}
              onKeyDown={(e) => {
                if (e.key === 'Enter') {
                  e.preventDefault()
                  handleSearchCode()
                }
              }}
              className="w-32"
            />
            <Button onClick={handleSearchCode} loading={searching} disabled={!searchCode}>
              検索
            </Button>
          </div>
        </div>

        {foundItem && (
          <>
            <div className="flex flex-col flex-1">
              <label className="text-xs text-gray-500">商品名</label>
              <div className="p-1 bg-white border border-gray-200 rounded h-[34px] flex items-center px-2">
                {foundItem.商品名}
              </div>
            </div>
            <div className="flex flex-col">
              <label className="text-xs text-gray-500">数量</label>
              <NumericTextBox
                value={quantity}
                onChange={(e) => setQuantity(e.target.value)}
                className="w-20 text-right"
                onKeyDown={(e) => {
                  if (e.key === 'Enter') {
                    e.preventDefault()
                    handleAdd()
                  }
                }}
              />
            </div>
            <Button onClick={handleAdd} fill>
              明細追加
            </Button>
          </>
        )}
      </div>
      {errorMessage && (
        <div className="text-red-600 text-sm">{errorMessage}</div>
      )}
    </div>
  )
}
