/**
 * P101_売上詳細 へ遷移するためのリンクURLを取得する関数
 */
export function getLinkUrlToP101売上詳細(売上SEQ?: string | null) {
  if (売上SEQ) {
    return `/uriage/edit/${売上SEQ}`
  } else {
    return `/uriage/new`
  }
}

/**
 * ルーティング定義
 */
export default [
  {
    path: "/uriage/new",
    element: <P101_売上詳細 mode="new" />,
    loader: async () => {
      const parameter = createNew売上詳細画面初期表示ParameterDisplayData()
      parameter.新規登録モード = true
      const result = await callComplexPostEndpointAsync('売上詳細画面初期表示', parameter, {
        ignoreConfirm: true,
      })
      if (result.type !== 'ok') throw result
      return result.returnValue
    },
  },
  {
    path: "/uriage/edit/:id",
    element: <P101_売上詳細 mode="edit" />,
    loader: async ({ params }) => {
      const parameter = createNew売上詳細画面初期表示ParameterDisplayData()
      parameter.新規登録モード = false
      parameter.売上SEQ = params.id ?? ''

      const result = await callComplexPostEndpointAsync('売上詳細画面初期表示', parameter, {
        ignoreConfirm: true,
      })
      if (result.type !== 'ok') throw result
      return result.returnValue
    },
  },
] satisfies ReactRouter.RouteObject[]

/**
 * P101 売上詳細
 */
function P101_売上詳細(props: {
  mode: 'new' | 'edit'
}) {

  // この画面で編集するオブジェクトのメタデータ
  const modelMetadata = UI.useFieldUiContextProvider("売上詳細")

  // react-hook-form のフォーム管理を利用
  const loaderData = ReactRouter.useLoaderData() as 売上詳細DisplayData
  const formMethods = ReactHookForm.useForm<売上詳細DisplayData>({
    defaultValues: loaderData,
  })
  const { register, control, getValues, setValue, reset } = formMethods
  const { revalidate } = useRevalidator()
  const navigate = ReactRouter.useNavigate()
  const [saving, setSaving] = React.useState(false)
  const { fields, append, remove } = ReactHookForm.useFieldArray({
    control,
    name: "売上詳細の売上明細"
  })

  // 画面タイトル
  const [browserTitle, pageTitle] = React.useMemo(() => {
    if (props.mode === 'new') {
      return ["売上登録", "売上登録"]
    } else {
      const date = dayjs(loaderData.売上日時).format('YYYY-MM-DD')
      return [
        `売上 No.${loaderData.売上SEQ} (${date})`,
        `売上 No.${loaderData.売上SEQ}`
      ]
    }
  }, [props.mode, loaderData])

  // 自動計算金額（計算処理の実体はサーバー側）
  const totalAmount = ReactHookForm.useWatch({ name: '合計金額', control })
  const abortControllerRef = React.useRef<AbortController | null>(null)
  const triggerRecalculation = React.useCallback(async () => {
    // 計算処理の重複実行対策
    abortControllerRef.current?.abort()
    abortControllerRef.current = new AbortController()

    const result = await callComplexPostEndpointAsync('売上金額シミュレート', getValues(), {
      ignoreConfirm: true,
      signal: abortControllerRef.current.signal,
    })
    if (result.type !== 'ok') return;
    if (abortControllerRef.current?.signal.aborted) return;

    setValue('合計金額', result.returnValue.合計金額)

    const currentArray = getValues().売上詳細の売上明細
    for (const detail of result.returnValue.売上詳細の売上明細) {
      const index = currentArray.findIndex(d => d.明細ID === detail.明細ID)
      if (index === -1) continue;
      setValue(`売上詳細の売上明細.${index}.売上総額_税込_自動計算`, detail.売上総額_税込_自動計算)
    }
  }, [getValues, setValue])

  // 自動計算のトリガーとなる項目が何かしら変更されたら再計算を実行
  const watchedFields = ReactHookForm.useWatch({ name: "売上詳細の売上明細", control })
  const recalculateTrigger = JSON.stringify((watchedFields ?? []).flatMap(field => [
    field.明細ID,
    field.区分,
    field.商品.外部システム側ID,
    field.売上数量,
    field.売上総額_税込_手修正,
  ]))
  React.useEffect(() => {
    triggerRecalculation()
  }, [recalculateTrigger, triggerRecalculation])

  // 明細行追加処理
  const handleCreateNewItem = useEvent((newItem: 商品RefTarget) => {
    const newDetail = createNew売上詳細の売上明細DisplayData()
    newDetail.明細ID = UUID.generate()
    newDetail.商品 = newItem
    newDetail.区分 = '売上'
    newDetail.売上数量 = '1'
    append(newDetail, { shouldFocus: false })
  })

  // 保存時処理
  const { replaceMessages, clearMessages } = DetailMessage.useSetter()
  const handleSave = useEvent(async () => {
    try {
      clearMessages()
      setSaving(true)

      // 画面上で編集されたインスタンスに willBeChanged フラグを更新する（ヘッダ）
      const currentValues = window.structuredClone(formMethods.getValues())
      if (formMethods.formState.dirtyFields) {
        currentValues.willBeChanged = true
      }

      // 画面上で編集されたインスタンスに willBeChanged フラグを更新する（明細）
      const dirtyDetails = formMethods.formState.dirtyFields.売上詳細の売上明細
      if (Array.isArray(dirtyDetails)) {
        dirtyDetails.forEach((dirtyRow, index) => {
          if (dirtyRow && currentValues.売上詳細の売上明細[index]) {
            currentValues.売上詳細の売上明細[index].willBeChanged = true
          }
        })
      }

      if (props.mode === 'new') {
        const result = await callComplexPostEndpointAsync('売上新規登録', currentValues)
        if (result.type === 'ok') {
          replaceMessages(result.detail)

          // 保存されたデータでリセットして isDirty を false にする。
          // reset による State 更新が反映され、ブロックが解除されるのを待ってから遷移する
          reset(result.returnValue)
          const waitAndNavigate = () => {
            if (formMethods.formState.isDirty) {
              window.setTimeout(waitAndNavigate, 10)
            } else {
              navigate(getLinkUrlToP101売上詳細(result.returnValue.売上SEQ))
            }
          }
          waitAndNavigate()

        } else if (result.type === 'canceled') {
          // 何もしない
        } else if (result.type === 'error') {
          replaceMessages(result.detail)
        }

      } else {
        const result = await callComplexPostEndpointAsync('売上修正', currentValues)
        if (result.type === 'ok') {
          replaceMessages(result.detail)
          revalidate()
        } else if (result.type === 'canceled') {
          // 何もしない
        } else if (result.type === 'error') {
          replaceMessages(result.detail)
        }
      }
    } finally {
      setSaving(false)
    }
  })

  // 保存後に loaderData のインスタンスが更新されるのでフォーム内容をリセットする
  React.useEffect(() => {
    reset(loaderData)
  }, [loaderData, reset])

  return (
    <PageBase
      browserTitle={browserTitle}
      isDirty={formMethods.formState.isDirty}
      className="gap-2 px-8 py-2"
      contents={(
        <UI.FieldUiContext.Provider value={modelMetadata}>
          <div className="max-h-full max-w-6xl flex flex-col gap-y-2">

            {/* ヘッダ */}
            <div className="flex items-start gap-1">
              <div className="flex-1 flex flex-wrap items-center gap-1 py-1">
                <h1 className="text-lg font-bold">
                  {pageTitle}
                </h1>

                <div className="flex gap-2 items-center">
                  <FormLabel className="basis-20 shrink-0 text-right">売上日時</FormLabel>
                  <UI.Field name="売上日時" control={control} />
                </div>

                <div className="flex gap-2 items-center">
                  <FormLabel className="basis-20 shrink-0 text-right">担当者</FormLabel>
                  <div className="flex gap-2">
                    <WordTextBox {...register("担当者.従業員番号")} className="w-32" />
                    {getValues("担当者.氏名")}
                  </div>
                </div>
              </div>

              <Button fill onClick={handleSave} loading={saving}>
                保存
              </Button>
            </div>

            <UI.Field
              name="備考"
              placeholder="備考"
              className="min-h-16 max-h-28"
              control={control}
            />

            <div className="flex justify-end font-bold">
              合計: {totalAmount}円
            </div>

            {/* 明細欄 */}
            <DetailMessage.Of name="売上詳細の売上明細" control={control} />
            {fields.length === 0 && (
              <div className="select-none text-gray-500 py-1">
                明細がありません。
              </div>
            )}
            <div className="overflow-x-auto">
              <table className="table-fixed w-full">
                <tbody>
                  {fields.map((field, index) => (
                    <React.Fragment key={field.id}>
                      <tr className={`border-t border-gray-300 whitespace-nowrap ${field.区分 === '取消' ? 'text-rose-700' : ''}`}>
                        <td className="p-1 w-16 align-top">
                          <UI.Field
                            name={`売上詳細の売上明細.${index}.区分`}
                            control={control}
                            className="shrink-0"
                            readOnly={field.existsInDatabase}
                          />
                        </td>
                        <td className="p-1 w-32 align-top font-bold">
                          <UI.Field
                            name={`売上詳細の売上明細.${index}.商品.外部システム側ID`}
                            className="shrink-0"
                            control={control}
                            readOnly={field.existsInDatabase}
                          />
                        </td>
                        <td className="p-1 align-middle font-bold max-w-[16rem]">
                          <div
                            title={field.商品.商品名}
                            className="truncate"
                          >
                            {field.商品.商品名}
                          </div>
                        </td>
                        <td className="p-1 align-middle text-right w-32">
                          {field.商品.売値単価_税抜}
                          円
                        </td>
                        <td className="p-1 w-8 align-middle text-center">
                          *
                        </td>
                        <td className="p-1 w-24 text-right align-top">
                          <UI.Field
                            name={`売上詳細の売上明細.${index}.売上数量`}
                            className="w-full"
                            readOnly={field.existsInDatabase}
                            control={control}
                          />
                        </td>
                        <td className="p-1 w-8 align-middle text-center">
                          *
                        </td>
                        <td className="p-1 w-20 align-middle">
                          {field.商品.消費税区分}
                        </td>
                        <td className="p-1 w-8 align-middle text-center">
                          =
                        </td>
                        <td className="p-1 w-32 align-middle text-right">
                          <span style={{
                            // 手修正が入っている場合は打ち消し線で表示
                            textDecoration: field.売上総額_税込_手修正 ? 'line-through' : undefined,
                            opacity: field.売上総額_税込_手修正 ? 0.3 : undefined,
                          }}>
                            {field.売上総額_税込_自動計算}円
                          </span>
                        </td>
                        <td className="p-1 w-40 align-middle">
                          (手修正:
                          <UI.Field
                            name={`売上詳細の売上明細.${index}.売上総額_税込_手修正`}
                            className="w-full"
                            readOnly={field.existsInDatabase}
                            control={control}
                          />
                          )
                        </td>
                        <td className="p-1 align-middle w-16 text-center">
                          {!field.existsInDatabase && (
                            <Button outline mini onClick={() => remove(index)}>
                              削除
                            </Button>
                          )}
                        </td>
                      </tr>
                      <tr>
                        <td colSpan={12} className="p-0 border-none">
                          <DetailMessage.Of name={`売上詳細の売上明細.${index}`} includeDescendants control={control} />
                        </td>
                      </tr>
                    </React.Fragment>
                  ))}
                </tbody>
              </table>
            </div>

            {/* 新規明細行追加欄 */}
            <NewItemArea formMethods={formMethods} onCreate={handleCreateNewItem} />
          </div>
        </UI.FieldUiContext.Provider>
      )}
    />
  )
}

/**
 * 新規明細行追加エリア。
 * 商品と数量だけを選択する。金額調整などは追加後に行う。
 */
function NewItemArea(props: {
  formMethods: ReactHookForm.UseFormReturn<売上詳細DisplayData>
  onCreate: (newItem: 商品RefTarget) => void
}) {
  const [searchCode, setSearchCode] = React.useState('')
  const [searchName, setSearchName] = React.useState('')
  const [errorMessage, setErrorMessage] = React.useState<string | null>(null)
  const [searching, setSearching] = React.useState(false)

  const handleSearchCode = async () => {
    if (!searchCode) return
    setSearching(true)
    setErrorMessage(null)
    try {
      const searchCondition = createNew商品SearchCondition()
      searchCondition.filter.外部システム側ID = searchCode
      searchCondition.take = '2' // ありえないが複数ヒットを考慮

      const result = await callComplexPostEndpointAsync('ref-to:商品', searchCondition, {
        ignoreConfirm: true,
      })
      if (result.type === 'ok') {
        if (result.returnValue.totalCount === 0) {
          setErrorMessage('該当する商品がありません')
        } else if (result.returnValue.totalCount === 1) {
          props.onCreate(result.returnValue.currentPageItems[0])
          setSearchCode('')
        } else {
          setErrorMessage('複数の商品がヒットしました。コードを確認してください。')
        }
      } else {
        setErrorMessage('検索エラーが発生しました')
      }
    } catch (e) {
      setErrorMessage('検索中にエラーが発生しました')
      console.error(e)
    } finally {
      setSearching(false)
    }
  }

  const handleSearchName = React.useCallback(async (keyword: string) => {
    const searchCondition = createNew商品SearchCondition()
    searchCondition.filter.商品名 = keyword

    const result = await callComplexPostEndpointAsync('ref-to:商品', searchCondition, {
      ignoreConfirm: true,
    })
    if (result.type === 'ok') {
      return result.returnValue.currentPageItems
    }
    return []
  }, [])

  return (
    <div className="border border-gray-300 p-2 bg-gray-50 flex flex-col gap-2">

      <div className="flex gap-x-8 gap-y-2 flex-col md:flex-row">

        <h2 className="self-start font-bold text-sm text-gray-600">
          新規明細行追加
        </h2>
        <div className="basis-2"></div>

        <div className="flex flex-col">
          <label className="text-xs text-gray-500">商品コード</label>
          <div className="flex gap-1">
            <WordTextBox
              value={searchCode}
              onChange={(e) => setSearchCode(e.target.value)}
              onKeyDown={(e) => {
                if (e.key === 'Enter') {
                  e.preventDefault()
                  handleSearchCode()
                }
              }}
              placeholder="商品コードで検索"
              className="w-32"
            />
            <Button outline onClick={handleSearchCode} loading={searching} disabled={!searchCode}>
              検索
            </Button>
          </div>
        </div>

        <div className="flex flex-col">
          <label className="text-xs text-gray-500">商品名</label>
          <AsyncComboBox
            value={searchName}
            onChange={(e) => setSearchName(e.target.value)}
            onSearch={handleSearchName}
            onSelectItem={(item) => {
              props.onCreate(item)
              setSearchName('')
            }}
            renderItem={(item) => (
              <div>
                <div className="text-sm font-bold">{item.商品名}</div>
                <div className="text-xs text-gray-500">{item.外部システム側ID}</div>
              </div>
            )}
            itemKey={(item) => item.商品SEQ ?? ''}
            className="w-64"
            placeholder="商品名で検索"
          />
        </div>

      </div>
      {errorMessage && (
        <div className="text-red-600 text-sm">{errorMessage}</div>
      )}
    </div>
  )
}
