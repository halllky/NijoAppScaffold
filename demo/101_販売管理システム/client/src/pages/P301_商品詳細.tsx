import React from "react"
import * as ReactRouter from "react-router"
import { Link, useSearchParams } from "react-router-dom"
import { PageBase } from "../layout/PageBase"
import { Pager } from "../layout/Pager"
import { callComplexPostEndpointAsync } from "../example/callComplexPostEndpointAsync"
import { 商品一覧DisplayData, createNew商品一覧SearchCondition } from "../__autoGenerated/商品一覧"
import { 商品在庫増減履歴DisplayData, createNew商品在庫増減履歴SearchCondition } from "../__autoGenerated/商品在庫増減履歴"
import { getLinkUrlToP101売上詳細 } from "./P101_売上詳細"
import { getLinkUrlToP201入荷詳細 } from "./P201_入荷詳細"
import dayjs from "dayjs"
import { Button } from "../input/Button"
import { StockAdjustmentDialog } from "./StockAdjustmentDialog"
import { createNew商品RefTarget } from "../__autoGenerated/商品"
import * as ReactRouterDom from "react-router-dom"
import { UUID } from "uuidjs"

/**
 * P301_商品詳細 へ遷移するためのフック
 */
export function useNavigateToP301商品詳細() {
  const navigate = ReactRouter.useNavigate()
  return React.useCallback((id: string) => {
    navigate(`/shohin/${id}`)
  }, [navigate])
}

type LoaderData = {
  product: 商品一覧DisplayData
  history: 商品在庫増減履歴DisplayData[]
  historyTotalCount: number
}

const PAGING_HISTORY_TAKE = 20

/**
 * ルーティング定義
 */
export default {
  path: "/shohin/:id",
  element: <P301_商品詳細 />,
  loader: async ({ params, request }) => {
    const id = params.id
    if (!id) throw new Error("商品IDが指定されていません")

    // 商品情報の取得
    const productCondition = createNew商品一覧SearchCondition()
    productCondition.filter.商品SEQ = { from: id, to: id }
    const productPromise = callComplexPostEndpointAsync('商品一覧', productCondition, {
      ignoreConfirm: true
    })

    // 履歴情報の取得
    const url = new URL(request.url)
    const historySkip = url.searchParams.get("historySkip") || "0"
    const historyCondition = createNew商品在庫増減履歴SearchCondition()
    historyCondition.filter.商品SEQ = { from: id, to: id }
    historyCondition.sort = ['商品在庫増減履歴.日時（降順）'] // 降順
    historyCondition.skip = historySkip
    historyCondition.take = PAGING_HISTORY_TAKE.toString()
    const historyPromise = callComplexPostEndpointAsync('商品在庫増減履歴', historyCondition, {
      ignoreConfirm: true
    })

    const [productResult, historyResult] = await Promise.all([productPromise, historyPromise])

    if (productResult.type !== 'ok' || historyResult.type !== 'ok') {
      throw new Error("データの取得に失敗しました")
    }

    if (productResult.returnValue.totalCount === 0) {
      throw new Error("商品が見つかりません")
    }

    return {
      product: productResult.returnValue.currentPageItems[0],
      history: historyResult.returnValue.currentPageItems,
      historyTotalCount: historyResult.returnValue.totalCount,
    } satisfies LoaderData
  },
} satisfies ReactRouter.RouteObject

/**
 * P301 商品詳細
 */
function P301_商品詳細() {
  const data = ReactRouter.useLoaderData() as LoaderData
  const { product, history, historyTotalCount } = data
  const [searchParams, setSearchParams] = useSearchParams()
  const [stockAdjDialogState, setStockAdjDialogState] = React.useState<{
    isOpen: boolean
    key: string | undefined
  }>({ isOpen: false, key: undefined })
  const revalidator = ReactRouterDom.useRevalidator()

  const historySkip = Number(searchParams.get("historySkip") || "0")
  const historyPageIndex = Math.floor(historySkip / PAGING_HISTORY_TAKE)

  const handleHistoryPageChange = (newPage: number) => {
    setSearchParams(prev => {
      const newSearchParams = new URLSearchParams(prev)
      newSearchParams.set("historySkip", (newPage * PAGING_HISTORY_TAKE).toString())
      return newSearchParams
    })
  }

  const pageTitle = `商品詳細: ${product.values.商品名}`

  return (
    <PageBase
      browserTitle={pageTitle}
      className="max-w-5xl gap-2 overflow-hidden pb-1"
      header={
        <>
          <h1 className="text-xl font-bold flex items-center gap-2">
            {product.values.商品名}
            <span className="text-gray-500 text-sm font-normal">
              商品コード: {product.values.外部システム側ID}
            </span>
          </h1>
          <Button outline onClick={() => setStockAdjDialogState({ isOpen: true, key: UUID.generate() })}>在庫調整</Button>
        </>
      }
      contents={
        <>
          <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
            <div className="flex flex-col">
              <span className="text-xs text-gray-500">標準売単価</span>
              <span className="text-lg font-bold">{product.values.売値単価_税抜} 円</span>
            </div>
            <div className="flex flex-col">
              <span className="text-xs text-gray-500">消費税</span>
              <span className="text-lg">{product.values.消費税区分}</span>
            </div>
            <div className="flex flex-col">
              <span className="text-xs text-gray-500">現在庫数</span>
              <span className="text-lg font-bold">{product.values.在庫数}</span>
            </div>
          </div>

          {/* 在庫増減履歴一覧 */}
          <div className="flex-1 flex flex-col gap-2 overflow-hidden mt-2">
            <h2 className="text-xs text-gray-500">在庫増減履歴</h2>

            {history.length === 0 ? (
              <div className="text-gray-500 p-4 text-center">履歴はありません</div>
            ) : (
              <div className="overflow-x-auto">
                <table className="text-left text-sm whitespace-nowrap">
                  <thead>
                    <tr>
                      <th className="sticky top-0 pr-3 py-1 font-medium text-gray-500 bg-gray-50">日時</th>
                      <th className="sticky top-0 px-3 py-1 font-medium text-gray-500 bg-gray-50">事由</th>
                      <th className="sticky top-0 px-3 py-1 font-medium text-gray-500 bg-gray-50 text-right">増減数</th>
                      <th className="sticky top-0 px-3 py-1 font-medium text-gray-500 bg-gray-50">関連伝票</th>
                    </tr>
                  </thead>
                  <tbody className="divide-y divide-gray-100">
                    {history.map((row, i) => (
                      <tr key={i} className="hover:bg-gray-50">
                        <td className="pr-3 py-1">
                          {row.values.日時 ? dayjs(row.values.日時).format('YYYY-MM-DD HH:mm') : ''}
                        </td>
                        <td className="px-3 py-1">
                          {row.values.事由}
                        </td>
                        <td className="px-3 py-1 text-right">
                          <span>
                            {row.values.増減数}
                          </span>
                        </td>
                        <td className="px-3 py-1">
                          <div className="flex flex-wrap gap-4 items-start">
                            {/* 入荷へのリンク */}
                            {row.増減履歴引当元売上一覧.length === 0 && row.values.入荷ID && (
                              <Link
                                to={getLinkUrlToP201入荷詳細(row.values.入荷ID)}
                                className="text-blue-600 hover:underline flex items-center gap-1"
                              >
                                <span>入荷 No.{row.values.入荷ID}</span>
                              </Link>
                            )}

                            {/* 売上へのリンク */}
                            {row.増減履歴引当元売上一覧.map((sales, j) => (
                              <Link
                                key={j}
                                to={getLinkUrlToP101売上詳細(sales.values.売上SEQ)}
                                className="text-blue-600 hover:underline flex items-center gap-1"
                              >
                                <span>売上 No.{sales.values.売上SEQ}</span>
                              </Link>
                            ))}
                          </div>
                        </td>
                      </tr>
                    ))}
                  </tbody>
                </table>
              </div>
            )}

          </div>

          <Pager
            pageIndex={historyPageIndex}
            pageSize={PAGING_HISTORY_TAKE}
            totalCount={historyTotalCount}
            onPageChange={handleHistoryPageChange}
          />

          {/* 在庫調整ダイアログ */}
          <StockAdjustmentDialog
            key={stockAdjDialogState.key}
            isOpen={stockAdjDialogState.isOpen}
            onClose={() => setStockAdjDialogState({ isOpen: false, key: undefined })}
            onSuccess={() => revalidator.revalidate()}
            product={(() => {
              const ref = createNew商品RefTarget()
              ref.商品SEQ = product.values.商品SEQ ?? undefined
              ref.商品名 = product.values.商品名 ?? undefined
              ref.外部システム側ID = product.values.外部システム側ID ?? undefined
              return ref
            })()}
          />
        </>
      } />
  )
}
