import React from 'react'
import { useForm, SubmitHandler } from 'react-hook-form'
import { Modal } from '../layout/Modal'
import { Button } from '../input/Button'
import { FormLabel } from '../layout/FormLabel'
import * as UI from "../input/Field"
import * as DetailMessage from "../util/DetailMessageContext"
import { callComplexPostEndpointAsync, toFlattenStringList } from '../example/callComplexPostEndpointAsync'
import { 在庫調整Parameter, createNew在庫調整Parameter, 在庫調整ParameterDisplayData, createNew在庫調整ParameterDisplayData } from '../__autoGenerated/在庫調整Parameter'
import { 商品RefTarget } from '../__autoGenerated/商品'

type Props = {
  isOpen: boolean
  onClose: () => void
  onSuccess?: () => void
  product: 商品RefTarget
}

export function StockAdjustmentDialog({ isOpen, onClose, onSuccess, product }: Props) {

  const contextvalue = UI.useFieldUiContextProvider("在庫調整Parameter")

  const { handleSubmit, control, reset, formState: { isSubmitting } } = useForm<在庫調整Parameter>({
    defaultValues: createNew在庫調整Parameter(),
  })

  React.useEffect(() => {
    if (isOpen) {
      const init = createNew在庫調整Parameter()
      init.商品 = product
      reset(init)
    }
  }, [isOpen, product, reset])

  const { replaceMessages } = DetailMessage.useSetter()
  const onSubmit: SubmitHandler<在庫調整Parameter> = async (data) => {
    const parameter = createNew在庫調整ParameterDisplayData()
    parameter.values.商品 = data.商品
    parameter.values.増減数 = data.増減数
    parameter.values.絶対数 = data.絶対数
    parameter.values.在庫調整理由 = data.在庫調整理由

    const result = await callComplexPostEndpointAsync('在庫調整登録', parameter)

    if (result.type === 'ok') {
      onSuccess?.()
      onClose()
    } else if (result.type === 'error') {
      replaceMessages(result.detail)
    } else if (result.type === 'unknown') {
      replaceMessages({ error: [result.message] })
    } else if (result.type === 'canceled') {
      // 何もしない
    }
  }

  return (
    <UI.FieldUiContext.Provider value={contextvalue}>
      <Modal
        isOpen={isOpen}
        onClose={onClose}
        title={`在庫調整: ${product.商品名 ?? ''}`}
        widthClass="max-w-xl"
      >
        <div className="p-4">
          <form onSubmit={handleSubmit(onSubmit)} className="flex flex-col gap-4">

            <div className="grid grid-cols-[8rem_1fr] gap-2 items-center">
              <FormLabel className="text-right">商品コード</FormLabel>
              <div className="px-2 py-1 bg-gray-100 rounded text-sm">
                {product.外部システム側ID}
              </div>

              <FormLabel className="text-right">商品名</FormLabel>
              <div className="px-2 py-1 bg-gray-100 rounded text-sm">
                {product.商品名}
              </div>

              <div className="col-span-2 border-t my-2"></div>

              <DetailMessage.Rest className="col-span-2" />

              <FormLabel className="text-right">増減数</FormLabel>
              <UI.Field name="増減数" type="numeric" control={control} />

              <FormLabel className="text-right">絶対数</FormLabel>
              <UI.Field name="絶対数" type="numeric" control={control} />

              <FormLabel className="text-right">在庫調整理由</FormLabel>
              <UI.Field name="在庫調整理由" control={control} />
            </div>

            <div className="flex justify-end gap-2 mt-4">
              <Button outline onClick={onClose} disabled={isSubmitting}>キャンセル</Button>
              <Button fill submit loading={isSubmitting}>実行</Button>
            </div>
          </form>
        </div>
      </Modal>
    </UI.FieldUiContext.Provider>
  )
}
