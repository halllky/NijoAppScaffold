import React from "react"
import * as ReactRouter from "react-router"
import { useNavigate } from "react-router-dom"
import { useForm } from "react-hook-form"
import { SearchPageBase } from "../layout/SearchPageBase"
import { FormLabel } from "../layout/FormLabel"
import { DataTable, DataTableColumn } from "../layout/DataTable"
import { NumericTextBox } from "../input/NumericTextBox"
import { DateInput } from "../input/DateInput"
import { WordTextBox } from "../input/WordTextBox"
import { 売上一覧SearchCondition, createNew売上一覧SearchCondition, 売上一覧DisplayData } from "../__autoGenerated/売上一覧"
import { callComplexPostEndpointAsync } from "../example/callComplexPostEndpointAsync"

export const URL = "/uriage"

/**
 * P100_売上 へ遷移するためのフック
 */
export function useNavigateToP100売上() {
  const navigate = useNavigate()
  return React.useCallback(() => {
    navigate(URL)
  }, [navigate])
}

/**
 * ルーティング定義
 */
export default {
  path: URL,
  element: <P100_売上 />,
  loader: undefined,
} satisfies ReactRouter.RouteObject

/**
 * P100 売上
 */
function P100_売上() {
  const { register, getValues } = useForm<売上一覧SearchCondition>({
    defaultValues: createNew売上一覧SearchCondition(),
  })

  // 検索処理
  const onSearch = React.useCallback(async (condition: 売上一覧SearchCondition, pageIndex: number, pageSize: number) => {
    // ページング情報を設定
    condition.skip = pageIndex * pageSize
    condition.take = pageSize

    const result = await callComplexPostEndpointAsync('売上一覧', condition)

    if (result.type === 'ok') {
      return {
        items: result.returnValue.currentPageItems,
        totalCount: result.returnValue.totalCount,
      }
    } else {
      // エラーハンドリング
      console.error(result)
      return { items: [], totalCount: 0 }
    }
  }, [])

  // 検索結果のカラム定義
  const columns: DataTableColumn<売上一覧DisplayData>[] = React.useMemo(() => [
    { header: "売上SEQ", render: row => row.values.売上SEQ, widthPx: 100 },
    { header: "売上日時", render: row => row.values.売上日時, widthPx: 150 },
    { header: "担当者", render: row => `${row.values.担当者.従業員番号 ?? ''} ${row.values.担当者.氏名 ?? ''}` },
    { header: "備考", render: row => row.values.備考 },
    { header: "売上総額(税込)", render: row => row.values.売上総額_税込?.toLocaleString(), widthPx: 120, className: "text-right" },
    { header: "売上数量合計", render: row => row.values.売上数量合計?.toLocaleString(), widthPx: 120, className: "text-right" },
  ], [])

  return (
    <SearchPageBase
      pageTitle="売上"
      searchConditionDefaultSize={240}
      onSearch={onSearch}
      searchCondition={({ search }) => (
        <form
          id="search-form"
          onSubmit={e => {
            e.preventDefault()
            search(getValues())
          }}
        >
          <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4">
            {/* 売上SEQ */}
            <div className="space-y-1">
              <FormLabel>売上SEQ</FormLabel>
              <div className="flex items-center gap-2">
                <NumericTextBox {...register("filter.売上SEQ.from")} placeholder="From" />
                <span>～</span>
                <NumericTextBox {...register("filter.売上SEQ.to")} placeholder="To" />
              </div>
            </div>

            {/* 売上日時 */}
            <div className="space-y-1">
              <FormLabel>売上日時</FormLabel>
              <div className="flex items-center gap-2">
                <DateInput {...register("filter.売上日時.from")} />
                <span>～</span>
                <DateInput {...register("filter.売上日時.to")} />
              </div>
            </div>

            {/* 担当者 */}
            <div className="space-y-1">
              <FormLabel>担当者</FormLabel>
              <div className="flex gap-2">
                <WordTextBox {...register("filter.担当者.従業員番号")} placeholder="従業員番号" className="w-1/3" />
                <WordTextBox {...register("filter.担当者.氏名")} placeholder="氏名" className="flex-1" />
              </div>
            </div>

            {/* 備考 */}
            <div className="space-y-1">
              <FormLabel>備考</FormLabel>
              <WordTextBox {...register("filter.備考")} />
            </div>

            {/* 売上総額_税込 */}
            <div className="space-y-1">
              <FormLabel>売上総額(税込)</FormLabel>
              <div className="flex items-center gap-2">
                <NumericTextBox {...register("filter.売上総額_税込.from")} placeholder="From" commaSeparated />
                <span>～</span>
                <NumericTextBox {...register("filter.売上総額_税込.to")} placeholder="To" commaSeparated />
              </div>
            </div>

            {/* 売上数量合計 */}
            <div className="space-y-1">
              <FormLabel>売上数量合計</FormLabel>
              <div className="flex items-center gap-2">
                <NumericTextBox {...register("filter.売上数量合計.from")} placeholder="From" commaSeparated />
                <span>～</span>
                <NumericTextBox {...register("filter.売上数量合計.to")} placeholder="To" commaSeparated />
              </div>
            </div>
          </div>
        </form>
      )}
      searchResult={({ items }) => (
        <DataTable
          rows={items}
          columns={columns}
          className="h-full"
        />
      )}
    />
  )
}
