/**
 * P400 従業員
 */
function P400_従業員() {
  // 検索条件
  const searchConditionMetadata = UI.useFieldUiContextProvider('従業員マスタ', 'SearchCondition')
  const searchConditionMethods = useForm<従業員マスタSearchCondition>({
    defaultValues: createNew従業員マスタSearchCondition()
  })
  const { register: searchRegister, control: searchControl, handleSubmit: handleSearchSubmit } = searchConditionMethods

  // 編集用データ（検索結果が入る）
  const editDataMetadata = UI.useFieldUiContextProvider('従業員一括更新Parameter')
  const [key, setKey] = React.useState(0) // フォーム再生成用
  const [defaultValues, setDefaultValues] = React.useState<従業員一括更新ParameterDisplayData>(() => createNew従業員一括更新ParameterDisplayData())
  const methods = useForm<従業員一括更新ParameterDisplayData>({
    defaultValues
  })
  const { formState: { isDirty, dirtyFields } } = methods

  // defaultValues が更新されたらフォームをリセット
  React.useEffect(() => {
    methods.reset(defaultValues)
  }, [defaultValues, methods])

  // 検索実行
  const onSearch = React.useCallback(async (condition: 従業員マスタSearchCondition) => {
    const result = await callComplexPostEndpointAsync('従業員マスタ', condition, {
      ignoreConfirm: true,
    })
    if (result.type === 'ok') {
      const newData = createNew従業員一括更新ParameterDisplayData()
      newData.更新対象従業員一覧 = result.returnValue.currentPageItems.map(item => {
        const row = createNew更新対象従業員一覧DisplayData()
        row.values.従業員 = item
        row.existsInDatabase = true
        row.willBeChanged = false // 初期ロード時は変更なし扱い（必要であれば後で監視）
        return row
      })
      setDefaultValues(newData)
      setKey(prev => prev + 1)
    }
  }, [])

  React.useEffect(() => {
    // 初期表示時に空条件で検索実行
    const initialCondition = createNew従業員マスタSearchCondition()
    onSearch(initialCondition)
  }, [onSearch])

  // 保存実行
  const { replaceMessages } = DetailMessage.useSetter()
  const handleSave = React.useCallback(async () => {
    const data = methods.getValues()

    // 変更検知ロジック
    // defaultValues（検索直後の状態）と比較して、変更がある行の willBeChanged を true にする

    // instanceId をキーにしたMapを作成
    const originalMap = new Map(defaultValues.更新対象従業員一覧.map(item => [item.instanceId, item]))

    for (const item of data.更新対象従業員一覧) {
      if (!item.existsInDatabase) continue
      if (item.willBeDeleted) continue

      const original = originalMap.get(item.instanceId)
      if (!original) {
        // ここに来ることは通常ない
        continue
      }

      // 比較対象の値を抽出
      const currentVal = item.values.従業員.values
      const originalVal = original.values.従業員.values

      const isChanged =
        currentVal.氏名 !== originalVal.氏名 ||
        currentVal.入荷担当 !== originalVal.入荷担当 ||
        currentVal.販売担当 !== originalVal.販売担当 ||
        currentVal.システム管理者 !== originalVal.システム管理者

      item.willBeChanged = isChanged
    }

    // 従業員一括更新コマンドの呼び出し
    const result = await callComplexPostEndpointAsync('従業員一括更新', data)

    if (result.type === 'ok') {
      // 保存成功後、再検索して最新状態（Version等）を反映
      replaceMessages({ info: ['保存しました。'] })
      const currentCondition = searchConditionMethods.getValues()
      await onSearch(currentCondition)
    } else if (result.type === 'canceled') {
      // 何もしない
    } else if (result.type === 'error') {
      replaceMessages(result.detail)
    } else {
      replaceMessages({ error: [result.message] })
    }

    // 標準のリロード処理は使わない
    return { reload: false }
  }, [defaultValues, onSearch, searchConditionMethods, methods])

  return (
    <EditPageBase
      pageTitle="従業員"
      isDirty={isDirty}
      onSave={handleSave}
      header={({ save, saving }) => (
        <>
          <PageTitle>従業員</PageTitle>
          <div className="flex-1"></div>
          <Button fill loading={saving} onClick={save}>保存</Button>
        </>
      )}
    >
      {() => (
        <div className="flex-1 flex flex-col gap-4">
          {/* 検索条件エリア */}
          <UI.FieldUiContext.Provider value={searchConditionMetadata}>
            <div className="border p-2 rounded bg-gray-50">
              <h2 className="font-bold mb-2">検索条件</h2>
              <div className="flex gap-4 items-end">
                <div>
                  <FormLabel>氏名</FormLabel>
                  <UI.Field name="filter.氏名" control={searchControl} />
                </div>
                <div>
                  <Button outline onClick={handleSearchSubmit(onSearch)}>検索</Button>
                </div>
              </div>
            </div>
          </UI.FieldUiContext.Provider>

          {/* 一覧編集エリア */}
          <UI.FieldUiContext.Provider value={editDataMetadata}>
            <div className="flex-1 overflow-auto">
              <EmployeeGrid methods={methods} />
            </div>
          </UI.FieldUiContext.Provider>
        </div>
      )}
    </EditPageBase>
  )
}

function EmployeeGrid({ methods }: { methods: UseFormReturn<従業員一括更新ParameterDisplayData> }) {
  const { fields, append, remove } = useFieldArray({
    control: methods.control,
    name: "更新対象従業員一覧"
  })

  // 行追加
  const handleAdd = () => {
    const newRow = createNew更新対象従業員一覧DisplayData()
    append(newRow)
  }

  // 行削除（論理削除または物理削除）
  const handleRemove = (index: number) => {
    const item = fields[index]
    if (item.existsInDatabase) {
      // 既にDBにある場合は論理削除フラグを立てる
      methods.setValue(`更新対象従業員一覧.${index}.willBeDeleted`, true)
    } else {
      // 新規行なら物理削除
      remove(index)
    }
  }

  // 削除取り消し
  const handleUndoRemove = (index: number) => {
    methods.setValue(`更新対象従業員一覧.${index}.willBeDeleted`, false)
  }

  return (
    <div>
      <div className="mb-2">
        <Button outline mini onClick={handleAdd}>＋ 行追加</Button>
      </div>
      <table className="table-fixed border-collapse border border-gray-300">
        <thead className="bg-gray-100">
          <tr>
            <th className="border px-1 py-px">操作</th>
            <th className="border px-1 py-px">従業員番号</th>
            <th className="border px-1 py-px">氏名</th>
            <th className="border px-1 py-px">入荷担当</th>
            <th className="border px-1 py-px">販売担当</th>
            <th className="border px-1 py-px">管理者</th>
            <th className="border px-1 py-px w-80">{/* メッセージ列 */}</th>
          </tr>
        </thead>
        <tbody>
          {fields.map((field, index) => {
            const isLogicalDeleted = methods.watch(`更新対象従業員一覧.${index}.willBeDeleted`)

            return (
              <tr key={field.id} className={isLogicalDeleted ? "bg-red-50 opacity-60" : ""}>
                <td className="border px-1 py-px text-center">
                  {isLogicalDeleted ? (
                    <Button mini onClick={() => handleUndoRemove(index)}>復元</Button>
                  ) : (
                    <Button mini onClick={() => handleRemove(index)}>削除</Button>
                  )}
                </td>
                <td className="border px-1 py-px">
                  <UI.Field
                    name={`更新対象従業員一覧.${index}.values.従業員.values.従業員番号`}
                    control={methods.control}
                    readOnly={field.existsInDatabase}
                    className="w-40"
                  />
                </td>
                <td className="border p-px">
                  <UI.Field
                    name={`更新対象従業員一覧.${index}.values.従業員.values.氏名`}
                    readOnly={isLogicalDeleted}
                    control={methods.control}
                    className="w-full"
                  />
                </td>
                <td className="border px-1 py-px text-center">
                  <UI.Field
                    name={`更新対象従業員一覧.${index}.values.従業員.values.入荷担当`}
                    readOnly={isLogicalDeleted}
                    control={methods.control}
                  />
                </td>
                <td className="border px-1 py-px text-center">
                  <UI.Field
                    name={`更新対象従業員一覧.${index}.values.従業員.values.販売担当`}
                    readOnly={isLogicalDeleted}
                    control={methods.control}
                  />
                </td>
                <td className="border px-1 py-px text-center">
                  <UI.Field
                    name={`更新対象従業員一覧.${index}.values.従業員.values.システム管理者`}
                    readOnly={isLogicalDeleted}
                    control={methods.control}
                  />
                </td>
                <td className="border px-1 py-px text-left">
                  <DetailMessage.Of name={`更新対象従業員一覧.${index}`} control={methods.control} />
                </td>
              </tr>
            )
          })}
        </tbody>
      </table>
    </div>
  )
}

