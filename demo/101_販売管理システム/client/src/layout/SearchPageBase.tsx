import React from "react"
import * as Icon from "@heroicons/react/24/solid"
import { Allotment, LayoutPriority } from "allotment"
import { PageBase } from "./PageBase"
import { PageTitle } from "./PageTitle"
import { Button } from "../input/Button"
import { DataTable, DataTableColumn } from "./DataTable"
import * as DetailMessage from "../util/DetailMessageContext"
import { NowLoading } from "./NowLoading"
import { callComplexPostEndpointAsync } from "../example/callComplexPostEndpointAsync"
import { QueryModelType, LoadFeature, DisplayData, SearchCondition, ASC_SUFFIX, DESC_SUFFIX } from "../__autoGenerated"
import * as UI from "../input/Field"
import { useSearchParamsAsCondition } from "../util/useSearchParamsAsCondition"
import { MultiSelect } from "../input/MultiSelect"

export type SearchPageBaseProps<
  TQueryModelType extends QueryModelType,
  TItem,
  TCondition extends LoadFeature.ParamType[TQueryModelType]
> = {
  /** このページと対応するクエリモデルの種類 */
  queryModelType: TQueryModelType
  /** ページタイトル。ブラウザのタイトルバーに表示されます。 */
  pageTitle?: string
  /** 検索条件欄のデフォルトサイズ（ピクセル） */
  searchConditionDefaultSize?: number
  /** 検索条件のクリア処理 */
  onClear: () => void
  /** ヘッダー部分のページタイトルの横の部分のレイアウト */
  header?: React.ReactNode
  /** 検索条件エリア。この欄はformタグの内側に配置されます。 */
  searchCondition: React.ReactNode
  /** useFormのhandleSubmit */
  onSubmit: (onValid: (data: TCondition) => void) => (e?: React.BaseSyntheticEvent) => Promise<void>
  /** 画面初期表示時、ブラウザバック、F5リロードなどでURLパラメータから検索条件が復元された際の処理 */
  onUrlChanged: (condition: TCondition) => void
  /** 検索結果エリアのグリッドの列定義 */
  defineSearchResultColumns: [
    (columnFor: UI.DataTableColumnDefinitionHelper<TItem>) => DataTableColumn<TItem>[],
    deps: React.DependencyList
  ]
  /** 並び順コンボボックスの選択肢 */
  sortOptions: SortComboItem<TQueryModelType>[]
  sortValue: TCondition['sort']
  /** 並び順が変更されたときの処理 */
  onSortChange: (newSort: TCondition['sort']) => void
}

export type SortComboItem<TQueryModelType extends QueryModelType> = {
  key: SearchCondition.SortableMemberTypeMap[TQueryModelType]
  label: string
}

type SortComboItemWithAscDesc<TQueryModelType extends QueryModelType> = {
  key: SearchCondition.TypeMap[TQueryModelType]['sort'][number]
  label: string
}

/**
 * 一覧検索画面のベースコンポーネント。
 *
 * ### このコンポーネントの責務
 * - ページの枠のレイアウト（検索条件、結果、ページャ）
 * - 検索処理の呼び出しと結果の保持
 * - ページング管理
 * - URLのクエリパラメータと検索条件オブジェクトの同期
 *
 * ### 利用側の責務
 * - 検索条件フォームのレイアウトと状態管理
 * - 検索結果一覧のレイアウト
 * - 実際のAPI呼び出し
 */
export function SearchPageBase<
  TQueryModelType extends QueryModelType,
  TItem extends DisplayData.TypeMap[TQueryModelType],
  TCondition extends SearchCondition.TypeMap[TQueryModelType]
>(props: SearchPageBaseProps<TQueryModelType, TItem, TCondition>) {

  // 自動生成されたクエリモデルのメタデータ
  const searchConditionFieldContext = UI.useFieldUiContextProvider(props.queryModelType, 'SearchCondition')
  const searchResultFieldContext = UI.useFieldUiContextProvider(props.queryModelType, 'EditableObject')

  // 検索結果
  const [items, setItems] = React.useState<TItem[]>([])
  const [totalCount, setTotalCount] = React.useState(0)
  const [pageIndex, setPageIndex] = React.useState(0)
  const [pageSize, setPageSize] = React.useState(20)

  // URLのクエリパラメータを検索条件オブジェクトとして扱うためのフック
  const {
    searchParamsAsCondition,
    applyConditionToUrl,
  } = useSearchParamsAsCondition<TQueryModelType, TCondition>(props.queryModelType)

  // 検索処理およびそれに関する状態
  const [lastCondition, setLastCondition] = React.useState<TCondition | undefined>(undefined)
  const [loading, setLoading] = React.useState(false)
  const [searchConditionVisible, setSearchConditionVisible] = React.useState(true)
  const { clearMessages, replaceMessages } = DetailMessage.useSetter()

  const executeSearch = React.useCallback(async (condition: TCondition, page: number, size: number) => {
    try {
      setLoading(true)
      condition.skip = (page * size).toString()
      condition.take = size.toString()
      const result = await callComplexPostEndpointAsync(props.queryModelType, condition)
      if (result.type === 'ok') {
        setItems(result.returnValue.currentPageItems as TItem[])
        setTotalCount(result.returnValue.totalCount)
        setPageIndex(page)
        setPageSize(size)
        setLastCondition(condition)
        clearMessages()

      } else if (result.type === 'canceled') {
        // キャンセルされた場合は何もしない

      } else if (result.type === 'error') {
        replaceMessages(result.detail)

      } else {
        replaceMessages({ error: [result.message] })
      }
    } finally {
      setLoading(false)
    }
  }, [props.queryModelType, clearMessages, replaceMessages])

  // 検索ボタンが押されたとき、またはフォーム内でEnterされたときの処理。
  // 検索条件欄で入力された内容で、1ページ目に戻る条件で検索を実行する。
  const handleSearch = React.useCallback((condition: TCondition) => {
    condition.skip = '0'
    condition.take = pageSize.toString()
    applyConditionToUrl(condition)
  }, [pageSize, applyConditionToUrl])

  // ページャでページが変更されたときの処理。
  // 現在フォームで入力されている値ではなく、最後に検索した条件をもとにして検索を実行する。
  const handlePageChange = React.useCallback((newPage: number) => {
    if (lastCondition) {
      const nextCondition = { ...lastCondition }
      nextCondition.skip = (newPage * pageSize).toString()
      nextCondition.take = pageSize.toString()
      applyConditionToUrl(nextCondition)
    }
  }, [lastCondition, pageSize, applyConditionToUrl])

  // 画面初期表示、ブラウザバック、ブラウザフォワード、F5リロードなどでURLのクエリパラメータが変化したときの処理。
  React.useEffect(() => {
    // 入力フォームの内容をURLパラメータに合わせて更新
    props.onUrlChanged(searchParamsAsCondition)

    // URLの検索条件を正として検索を実行
    const skip = Number(searchParamsAsCondition.skip) || 0
    const take = Number(searchParamsAsCondition.take) || 20
    const page = Math.floor(skip / take)
    executeSearch(searchParamsAsCondition, page, take)
  }, [searchParamsAsCondition, executeSearch, props.onUrlChanged])

  // 検索結果の列定義
  const columnFor = UI.useGridColumnHelper<TItem>('', searchResultFieldContext)
  const columns: DataTableColumn<TItem>[] = React.useMemo(() => {
    return props.defineSearchResultColumns[0](columnFor)
  }, [...props.defineSearchResultColumns[1], columnFor])

  // ページャーの計算
  const totalPages = Math.ceil(totalCount / pageSize)
  const canPrev = pageIndex > 0
  const canNext = pageIndex < totalPages - 1

  // ソート順コンボのデータソース。
  // 昇順・降順の両方の選択肢を用意する。
  // 既にソート順に選択されているものは除外
  const sortComboOptionsWithAscDesc = React.useMemo((): SortComboItemWithAscDesc<TQueryModelType>[] => {
    const asc = '（昇順）' satisfies ASC_SUFFIX
    const desc = '（降順）' satisfies DESC_SUFFIX
    const selectedKeysWithoutAscDesc = new Set(props.sortValue.map(key => {
      if (key.endsWith(asc)) {
        return key.slice(0, -asc.length)
      } else if (key.endsWith(desc)) {
        return key.slice(0, -desc.length)
      } else {
        return key
      }
    }))
    return props.sortOptions
      .filter(item => !selectedKeysWithoutAscDesc.has(item.key))
      .flatMap(item => [
        { key: (item.key + asc), label: item.label + ' (昇順)' },
        { key: (item.key + desc), label: item.label + ' (降順)' },
      ]) as SortComboItemWithAscDesc<TQueryModelType>[]
  }, [props.sortOptions, props.sortValue])

  // 選択されているソート項目の値。
  // 表示ラベルが指定されている場合はその名前を使用する。
  const sortValueAsComboItem = React.useMemo((): SortComboItemWithAscDesc<TQueryModelType>[] => {
    const map = new Map(props.sortOptions.flatMap(item => [
      [item.key + ('（昇順）' satisfies ASC_SUFFIX), item.label + ' (昇順)'],
      [item.key + ('（降順）' satisfies DESC_SUFFIX), item.label + ' (降順)'],
    ]))
    return props.sortValue.map(key => ({
      key,
      label: map.get(key) ?? key,
    }))
  }, [props.sortValue, props.sortOptions])

  // ソート順が変更されたときの処理。
  // 最初のページに戻って検索を実行する。
  const handleSortChange = React.useCallback((newSort: SortComboItemWithAscDesc<TQueryModelType>[]) => {
    const nextCondition = { ...searchParamsAsCondition }
    const newValue = newSort.map(item => item.key) as TCondition['sort']
    nextCondition.sort = newValue
    nextCondition.skip = '0'
    applyConditionToUrl(nextCondition)
    props.onSortChange(newValue)
  }, [searchParamsAsCondition, applyConditionToUrl])

  return (
    <UI.FieldUiContext.Provider value={searchConditionFieldContext}>
      <PageBase
        browserTitle={props.pageTitle}
        className="overflow-hidden"
        header={(
          <>
            {/* ページタイトル */}
            <button
              type="button"
              className="flex-1 flex items-center gap-1 cursor-pointer select-none"
              onClick={() => setSearchConditionVisible(prev => !prev)}
            >
              {searchConditionVisible ? (
                <Icon.ChevronDownIcon className="w-4 h-4" />
              ) : (
                <Icon.ChevronRightIcon className="w-4 h-4" />
              )}
              <PageTitle>
                {props.pageTitle}
              </PageTitle>
            </button>

            <Button outline onClick={props.onClear}>
              クリア
            </Button>

            <Button submit fill form="search-form" disabled={loading}>
              検索
            </Button>
          </>
        )}
        contents={(
          <Allotment vertical
            defaultSizes={[props.searchConditionDefaultSize ?? 200, 999]}
            separator={false}
            proportionalLayout={false} // ページサイズ変更時に検索結果欄だけ伸縮するようにするため
            className="pb-1"
          >

            {/* 検索条件欄 */}
            <Allotment.Pane minSize={50} visible={searchConditionVisible} className="pb-1">
              <form
                id="search-form"
                noValidate
                className="h-full overflow-auto"
                onSubmit={props.onSubmit(handleSearch)}
              >
                {props.searchCondition}
              </form>
            </Allotment.Pane>

            {/* 件数表示、ソート順指定、検索結果欄 */}
            <Allotment.Pane
              priority={LayoutPriority.High} // ページサイズ変更時に検索結果欄だけ伸縮する
              className="relative"
            >
              <div className="h-full flex flex-col">

                <div className="flex flex-wrap items-center py-1">
                  {/* 件数表示 */}
                  <div className="text-sm text-gray-600">
                    {totalCount > 0 ? (
                      <>
                        全 {totalCount} 件中 {pageIndex * pageSize + 1} - {Math.min((pageIndex + 1) * pageSize, totalCount)} 件を表示
                      </>
                    ) : (
                      <>データなし</>
                    )}
                  </div>

                  <div className="basis-4"></div>

                  {/* ソート順指定 */}
                  <MultiSelect<SortComboItemWithAscDesc<TQueryModelType>>
                    className="ml-2 min-w-[200px]"
                    value={sortValueAsComboItem}
                    options={sortComboOptionsWithAscDesc}
                    onChange={handleSortChange}
                    getLabel={getLabel}
                    placeholder="並び順"
                  />
                </div>

                <DataTable
                  rows={items}
                  columns={columns}
                  className="flex-1 border border-gray-600"
                />
              </div>

              {loading && (
                <NowLoading opacity={0} />
              )}
            </Allotment.Pane>

          </Allotment>
        )}
        footer={(
          // ページャー
          <div className="w-full flex justify-center items-center">
            <div className="flex gap-2 items-center">
              <Button outline
                onClick={() => handlePageChange(pageIndex - 1)}
                disabled={!canPrev || loading}
              >
                前へ
              </Button>
              <span className="px-2 py-px select-none">
                {pageIndex + 1} / {Math.max(1, totalPages)}
              </span>
              <Button outline
                onClick={() => handlePageChange(pageIndex + 1)}
                disabled={!canNext || loading}
              >
                次へ
              </Button>
            </div>
          </div>
        )}
      />
    </UI.FieldUiContext.Provider>
  )
}

const getLabel = (item: SortComboItemWithAscDesc<any>) => item.label
