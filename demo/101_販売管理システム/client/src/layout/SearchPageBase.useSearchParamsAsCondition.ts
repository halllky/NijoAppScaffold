import React from "react"
import { useSearchParams } from "react-router-dom"
import { QueryModelType, SearchCondition } from "../__autoGenerated"

/**
 * URLのクエリパラメータと検索条件オブジェクトを相互に変換するためのフック。
 */
export function useSearchParamsAsCondition<
  TQueryModelType extends QueryModelType,
  TCondition extends SearchCondition.TypeMap[TQueryModelType]
>(queryModelType: TQueryModelType) {

  const [searchParams, setSearchParams] = useSearchParams()

  // URLパラメータを検索条件オブジェクト型にしたもの。
  // 未指定の場合は空の検索条件になる。
  const searchParamsAsCondition = React.useMemo((): TCondition => {
    try {
      // クエリパラメータから全ての値を取り出す
      const record = fromQueryParamRecord(Array.from(searchParams)) as TCondition

      // URLには実際に値が入力された項目のプロパティしか含まれないため、
      // 新規検索条件オブジェクトを作成し、URLパラメータの内容を上書きする。
      const emptySearchCondition = SearchCondition.create[queryModelType]() as TCondition
      assignDeep(record, emptySearchCondition)
      return emptySearchCondition as TCondition

    } catch (error) {
      return SearchCondition.create[queryModelType]() as TCondition
    }
  }, [searchParams])

  // 検索条件オブジェクトをURLパラメータに反映する関数。
  const applyConditionToUrl = React.useCallback((condition: TCondition) => {
    // クエリパラメータの量を削減するため、未入力の項目を除去
    const clone = window.structuredClone(condition) as TCondition
    removeEmptyValues(clone)

    // クエリパラメータにバインド可能な形のレコードに変換して反映
    const record = toQueryParamRecord(clone as Record<string, unknown>)
    setSearchParams(record)
  }, [setSearchParams])

  return {
    /** URLのクエリパラメータを検索条件オブジェクト型に変換したもの */
    searchParamsAsCondition,
    /** 検索条件オブジェクトをURLのクエリパラメータに反映する関数 */
    applyConditionToUrl,
  }
}

/**
 * Object.assign と似たようなことをネストしたオブジェクトに対して行いたいための関数。
 * なおクエリモデルの検索条件オブジェクトに配列が登場することはないので考慮不要。
 *
 * @param source こちらのオブジェクトの値が target に代入される
 * @param target このオブジェクトのプロパティが source の内容で上書きされる
 */
export function assignDeep(source: Record<string, unknown>, target: Record<string, unknown>) {
  for (const key of Object.keys(source)) {
    const sourceValue = source[key]
    if (sourceValue && typeof sourceValue === 'object') {
      // ネストしたオブジェクトの場合は再帰的に代入。
      // 万が一対象プロパティが存在しなければあらかじめ空オブジェクトを作成してから代入する。
      if (!(key in target)) {
        target[key] = {}
      }
      assignDeep(
        sourceValue as Record<string, unknown>,
        target[key] as Record<string, unknown>
      )
    } else {
      // string や boolean などのプリミティブ値の場合はそのまま代入
      target[key] = sourceValue
    }
  }
}

/**
 * オブジェクトから空のプロパティを再帰的に削除する。
 * 空のプロパティとは、空文字、null、undefined、false、および空オブジェクトを指す。
 *
 * 引数のオブジェクトの値が直接変更される。
 */
export function removeEmptyValues(obj: Record<string, unknown>) {
  for (const key of Object.keys(obj)) {
    const value = obj[key]
    if (value && typeof value === 'object') {
      removeEmptyValues(value as Record<string, unknown>)
      // ネストしたオブジェクトが空になったら削除
      if (isEmptyObject(value as Record<string, unknown>)) {
        delete obj[key]
      }
    } else {
      // プリミティブ値の場合は空文字、null、undefined、falseなら削除
      if (value === '' || value === null || value === undefined || value === false) {
        delete obj[key]
      }
    }
  }
}

/**
 * 空オブジェクトかどうかを判定する。
 * 空文字、false、null、undefinedなプロパティしか持たない場合は空オブジェクトとみなす。
 */
export function isEmptyObject(obj: Record<string, unknown>): boolean {
  return Object.values(obj).every(value => {
    if (value === undefined) return true
    if (value === null) return true
    if (value === '') return true
    if (value === false) return true
    if (typeof value === 'object' && value !== null) {
      return isEmptyObject(value as Record<string, unknown>)
    }
    // 何かしら値が入っている場合
    return false
  })
}

/**
 * ネストされたオブジェクトをフラットなクエリパラメータの形に変換する。
 * 例えば { a: { b: 1, c: 2 }, d: 3 } というオブジェクトは
 * { "a.b": "1", "a.c": "2", "d": "3" } という形に変換される。
 *
 * 配列はサポートしない。（クエリモデルの検索条件オブジェクトに配列が登場することはない）
 */
export function toQueryParamRecord(obj: Record<string, unknown>): Record<string, string | string[]> {
  const result: Record<string, string | string[]> = {}

  function helper(currentObj: Record<string, unknown>, parentKey: string | null) {
    for (const [key, value] of Object.entries(currentObj)) {
      const fullKey = parentKey ? `${parentKey}.${key}` : key
      if (value && typeof value === 'object') {
        helper(value as Record<string, unknown>, fullKey)
      } else if (value !== undefined && value !== null && value !== '') {
        result[fullKey] = String(value)
      }
    }
  }

  helper(obj, null)
  return result
}

/**
 * toQueryParamRecord の逆変換。
 */
export function fromQueryParamRecord(params: [string, string][]): unknown {
  const result: Record<string, unknown> = {}

  for (const [compositeKey, value] of params) {
    const keys = compositeKey.split('.')
    let currentObj = result

    keys.forEach((key, index) => {
      if (index === keys.length - 1) {
        // 最後のキーの場合は値を設定
        currentObj[key] = value
      } else {
        // 中間のキーの場合はオブジェクトを作成（存在しない場合）
        if (!(key in currentObj)) {
          currentObj[key] = {}
        }
        currentObj = currentObj[key] as Record<string, unknown>
      }
    })
  }

  return result
}
