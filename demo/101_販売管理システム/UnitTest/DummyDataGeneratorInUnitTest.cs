namespace MyApp.UnitTest;

/// <summary>
/// 単体テスト用のダミーデータ生成クラス。
/// Web と違い通常のログインのプロセスが無いので、セッションを直接 DbContext で登録する。
/// </summary>
public class DummyDataGeneratorInUnitTest : OverridedDummyDataGenerator {
    public DummyDataGeneratorInUnitTest(Func<IMessageSetter, IPresentationContext<MessageSetter>> createPresentationContext) : base(createPresentationContext) {
    }

    public override async Task<IMessageSetter> GenerateAsync(AutoGeneratedApplicationService applicationService) {
        var result = await base.GenerateAsync(applicationService);
        if (result.HasError()) return result;

        // テスト用セッションを直接登録
        applicationService.DbContext.セッションDbSet.Add(new セッションDbEntity {
            セッションキー = TestUtilImpl.SessionKeyProviderInUnitTest.SESSION_KEY,
            ユーザ_従業員番号 = ADMIN_USER_ID,
            最終ログイン日時 = new DateTime(2026, 1, 1),
            Version = 0,
        });
        await applicationService.DbContext.SaveChangesAsync();

        return result;
    }
}
