using Nijo.Core;
using Nijo.Util.CodeGenerating;
using Nijo.Util.DotnetEx;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Nijo.Models.ReadModel2Features {
    /// <summary>
    /// カスタマイズで使うためのReadModelの名前の一覧
    /// </summary>
    internal class DisplayDataTypeList : ISummarizedFile {

        internal void Add(DataClassForDisplay displayData) {
            _displayDataList.Add(displayData);
        }
        private readonly List<DataClassForDisplay> _displayDataList = new();

        internal void AddCommand(GraphNode<Aggregate> commandModelAggregate) {
            _commandModels.Add(commandModelAggregate);
        }
        private readonly List<GraphNode<Aggregate>> _commandModels = new();

        public void OnEndGenerating(CodeRenderingContext context) {
            context.ReactProject.AutoGeneratedDir(dir => {
                dir.Generate(Render());
            });
        }

        private SourceFile Render() {
            return new SourceFile {
                FileName = "index.ts",
                RenderContent = context => {
                    var commandModels = context.Schema
                        .RootAggregates()
                        .Where(a => a.Item.Options.Handler == NijoCodeGenerator.Models.CommandModel.Key);
                    var items = _displayDataList.Select(disp => new {
                        DisplayData = disp,
                        SearchConditon = new SearchCondition(disp.Aggregate),
                        disp.Aggregate,
                        LoadMethod = new LoadMethod(disp.Aggregate),
                        SingleView = new SingleView(disp.Aggregate),
                        MultiView = new MultiView(disp.Aggregate),
                        UseBatchUpdate = BatchUpdateReadModel.GetHookName(disp.Aggregate),
                    }).ToArray();

                    return $$"""
                        // カスタマイズ側ソースで利用可能なようにまとめてexportする
                        {{items.SelectTextTemplate(disp => $$"""
                        export * from "./{{disp.Aggregate.Item.PhysicalName}}"
                        """)}}

                        {{commandModels.SelectTextTemplate(agg => $$"""
                        export * from "./{{agg.Item.PhysicalName}}"
                        """)}}
                        {{If(context.Schema.EnumDefinitions.Any(), () => $$"""

                        export * from "./util"
                        export * from "./autogenerated-enum"
                        """)}}

                        // --------------------------------------------
                        import React from "react"
                        import ReactRouter from "react-router-dom"
                        import { useMsgContext, useHttpRequest, ComplexPostOptions } from "../util2"
                        import * as AutoGeneratedUtil from "./util"

                        {{items.SelectTextTemplate(disp => $$"""
                        import * as {{disp.Aggregate.Item.PhysicalName}} from "./{{disp.Aggregate.Item.PhysicalName}}"
                        """)}}


                        //#region ReadModel, CommandModel の型名一覧
                        /** 自動生成されたReadModelの種類の一覧 */
                        export type ReadModelType
                        {{If(items.Length == 0, () => $$"""
                          = never
                        """)}}
                        {{items.SelectTextTemplate((disp, i) => $$"""
                          {{(i == 0 ? "=" : "|")}} '{{disp.Aggregate.Item.PhysicalName}}'
                        """)}}

                        /** 自動生成されたReadModelの種類の一覧 */
                        export const getReadModelTypeList = (): ReadModelType[] => {
                          return [
                        {{items.SelectTextTemplate((disp, i) => $$"""
                            '{{disp.Aggregate.Item.PhysicalName}}',
                        """)}}
                          ]
                        }

                        /** 自動生成されたCommandModelの種類の一覧 */
                        export type CommandModelType
                        {{If(_commandModels.Count == 0, () => $$"""
                          = never
                        """)}}
                        {{_commandModels.SelectTextTemplate((agg, i) => $$"""
                          {{(i == 0 ? "=" : "|")}} '{{agg.Item.PhysicalName}}'
                        """)}}

                        /** 自動生成されたCommandModelの種類の一覧 */
                        export const getCommandModelTypeList = (): CommandModelType[] => {
                          return [
                        {{_commandModels.SelectTextTemplate((agg, i) => $$"""
                            '{{agg.Item.PhysicalName}}',
                        """)}}
                          ]
                        }
                        //#endregion ReadModel, CommandModel の型名一覧



                        //#region DisplayData型一覧
                        /** 各ReadModelと画面表示用の型の対応表 */
                        export interface DisplayDataTypeMap {
                        {{items.SelectTextTemplate((disp, i) => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.DisplayData.TsTypeName}}
                        """)}}
                        }
                        //#endregion DisplayData型一覧


                        //#region DisplayData新規作成関数
                        /** 各ReadModelの画面表示用オブジェクトの新規作成関数 */
                        export const CreateNewDisplayData: {
                          [K in ReadModelType]: () => DisplayDataTypeMap[K]
                        } = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.DisplayData.TsNewObjectFunction}},
                        """)}}
                        }
                        //#endregion DisplayData新規作成関数


                        //#region SearchCondition型一覧
                        /** 各ReadModelと検索条件用の型の対応表 */
                        export interface SearchConditionTypeMap {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.SearchConditon.TsTypeName}}
                        """)}}
                        }
                        //#endregion SearchCondition型一覧


                        //#region SearchConditionソート可能メンバー
                        /** 各ReadModelのSearchConditionのうちソート可能なメンバーの型 */
                        export interface SearchConditionSortableMember {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.SearchConditon.TypeScriptSortableMemberType}}
                        """)}}
                        }
                        
                        /** 一覧検索処理のパラメータ指定でメンバー名の後ろにこの文字列をつけるとサーバー側処理でソートしてくれる */
                        export type ASC_SUFFIX = '{{SearchCondition.ASC_SUFFIX}}'
                        /** 一覧検索処理のパラメータ指定でメンバー名の後ろにこの文字列をつけるとサーバー側処理でソートしてくれる */
                        export type DESC_SUFFIX = '{{SearchCondition.DESC_SUFFIX}}'
                        //#endregion SearchConditionソート可能メンバー


                        //#region SearchCondition新規作成関数
                        /** 各ReadModelの検索条件用オブジェクトの新規作成関数 */
                        export const CreateNewSearchCondition: {
                          [K in ReadModelType]: () => SearchConditionTypeMap[K]
                        } = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.SearchConditon.CreateNewObjectFnName}},
                        """)}}
                        }
                        //#endregion SearchCondition新規作成関数


                        //#region 検索用フック（MultiView）
                        /** DisplayData読み込み用フック（MultiView）の型 */
                        export type LoadHookType<TReadModelType extends ReadModelType> = (disableAutoLoad: true) => {
                          currentPageItems: DisplayDataTypeMap[TReadModelType][]
                          nowLoading: boolean
                          count: (searchConditionFilter: SearchConditionTypeMap[TReadModelType]['filter'], options?: ComplexPostOptions) => Promise<number>
                          load: (searchCondition: SearchConditionTypeMap[TReadModelType], options?: ComplexPostOptions) => Promise<DisplayDataTypeMap[TReadModelType][]>
                        }

                        /** 各ReadModelとDisplayData読み込み用フックの対応表 */
                        export const LoadHookMap: {
                          [K in ReadModelType]: LoadHookType<K>
                        }  = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.LoadMethod.ReactHookName}},
                        """)}}
                        }
                        //#endregion 検索用フック（MultiView）


                        //#region 検索用フック（SingleView）
                        /** DisplayData読み込み用フック（SingleView）の型 */
                        export type SingleViewDataLoader<TReadModelType extends ReadModelType> = (
                          onBeforeInit?: AutoGeneratedUtil.SingleViewDataLoaderOnBeforeInit<DisplayDataTypeMap[TReadModelType]>,
                          onBeforeLoad?: AutoGeneratedUtil.SingleViewDataLoaderOnBeforeLoad<SearchConditionTypeMap[TReadModelType]>,
                        ) => {
                          /** 読み込まれたデータ。 */
                          data: DisplayDataTypeMap[TReadModelType] | undefined
                          /** 読み込み状態。loadedなら読み込み完了（ただし読み込み処理の成否を問わない） */
                          loadState: AutoGeneratedUtil.SingleViewLoadState
                          /** 再読み込みを実行する。戻り値は無い。結果はdata変数に代入される。 */
                          reload: (options?: AutoGeneratedUtil.{{SingleView.SINGLE_VIEW_NAVIGATION_OPTIONS}}) => void
                        }

                        /** 各ReadModelと検索用フック（SingleView）の対応 */
                        export const SingleViewDataLoaderMap: {
                          [K in ReadModelType]: SingleViewDataLoader<K>
                        } = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.SingleView.LoaderHookNameVersion2}},
                        """)}}
                        }
                        //#endregion 検索用フック（SingleView）


                        //#region 画面遷移用フックの一覧

                        /** 詳細画面への画面遷移用フックの型（新規登録画面ではなく編集閲覧用画面への遷移） */
                        export type SingleViewNavigationHook<T> = (
                          data: T,
                          to: 'edit' | 'readonly',
                          options?: AutoGeneratedUtil.{{SingleView.SINGLE_VIEW_NAVIGATION_OPTIONS}}
                        ) => void

                        /** 各ReadModelと詳細画面遷移用フックの対応表（新規登録画面ではなく編集閲覧用画面への遷移） */
                        export const SingleViewNavigationHookMap: {
                          [K in ReadModelType]: () => SingleViewNavigationHook<DisplayDataTypeMap[K]>
                        } = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.SingleView.GetNavigateFnName(SingleView.E_Type.Edit)}},
                        """)}}
                        }

                        /** 各ReadModelと詳細画面遷移用フックの対応表（新規登録画面への遷移） */
                        export const CreateViewNavigationHookMap: {
                          [K in ReadModelType]: () => ((options?: AutoGeneratedUtil.{{SingleView.SINGLE_VIEW_NAVIGATION_OPTIONS}}) => void)
                        } = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.SingleView.GetNavigateFnName(SingleView.E_Type.New)}},
                        """)}}
                        }

                        /** 各ReadModelと一覧画面遷移用フックの対応表 */
                        export const MultiViewNavigationHookMap: {
                          [K in ReadModelType]: () => ((initSearchCondition?: SearchConditionTypeMap[K], options?: ReactRouter.NavigateOptions) => void)
                        } = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.MultiView.NavigationHookName}},
                        """)}}
                        }
                        //#endregion 画面遷移用フックの一覧


                        //#region URLから検索条件オブジェクトへの変換
                        /** 各ReadModelのURLから検索条件オブジェクトへの変換処理 */
                        export const UrlToSearchCondition: {
                          [K in ReadModelType]: (urlSearch: string) => SearchConditionTypeMap[K]
                        } = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.SearchConditon.ParseQueryParameter}},
                        """)}}
                        }
                        //#endregion URLから検索条件オブジェクトへの変換


                        //#region 検索条件オブジェクトからURLへの変換
                        /** 各ReadModelの検索条件オブジェクトからURLへの変換処理 */
                        export const SearchConditionToUrl: {
                          [K in ReadModelType]: (searchParams: URLSearchParams, searchCondition: SearchConditionTypeMap[K] | undefined) => void
                        } = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.MultiView.AppendToSearchParamsFunction}},
                        """)}}
                        }
                        //#endregion 検索条件オブジェクトからURLへの変換


                        //#region 変更チェック関数一覧

                        /**
                         * 引数2個の値が変更されているかをディープイコールで確認し、
                         * 変更されている場合はcurrentValuesの{{DataClassForDisplay.WILL_BE_CHANGED_TS}}プロパティをtrueにして返す関数の型
                         */
                        export type CheckChangesFunction<T> = (args: { defaultValues: T, currentValues: T }) => boolean

                        /** 変更チェック関数一覧 */
                        export const CheckChangesFunctionMap: {
                          [K in ReadModelType]: CheckChangesFunction<DisplayDataTypeMap[K]>
                        } = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.DisplayData.CheckChangesFunction}},
                        """)}}
                        }
                        //#endregion 変更チェック関数一覧


                        //#region UI制約型一覧

                        /** UI制約型一覧 */
                        export const ReadModelConstraints = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.DisplayData.UiConstraingValueName}},
                        """)}}
                        }
                        //#endregion UI制約型一覧


                        //#region 一括更新フック
                        export type UseBatchUpdateReturn<TReadModelType extends ReadModelType> = (
                          items: (DisplayDataTypeMap[TReadModelType])[],
                          handleDetailError: ComplexPostOptions['handleDetailError']
                        ) => Promise<{
                          ok: boolean,
                          data?: ((DisplayDataTypeMap[TReadModelType])[]),
                        }>

                        /** 画面から更新されない集約のための、実際に呼ばれることは無い関数 */
                        const useNoopBatchUpdate = (): (() => never) => {
                          return () => {
                            throw new Error('この集約を画面から一括更新することはできません。更新する場合はnijo.xmlでこのReadModelのreadonly指定を外してください。')
                          }
                        }

                        /** 一括更新処理のフック。 */
                        export const BatchUpdateHookMap: {
                          [K in ReadModelType]: () => UseBatchUpdateReturn<K>
                        } = {
                        {{items.SelectTextTemplate(disp => disp.Aggregate.Item.Options.IsReadOnlyAggregate ? $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': useNoopBatchUpdate,
                        """ : $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.UseBatchUpdate}},
                        """)}}
                        }
                        //#endregion 一括更新フック


                        //#region Excel出力フック
                        
                        /** 各ReadModelのExcel出力フック */
                        export const UseExcelDownloadHook: {
                          [K in ReadModelType]: () => ((searchCondition: SearchConditionTypeMap[K]) => Promise<void>)
                        } = {
                        {{items.SelectTextTemplate(disp => $$"""
                          '{{disp.Aggregate.Item.PhysicalName}}': {{disp.Aggregate.Item.PhysicalName}}.{{disp.MultiView.ExcelDownloadHookName}},
                        """)}}
                        }
                        //#endregion Excel出力フック
                        """;
                },
            };
        }
    }
}
