using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Nijo.Core;
using Nijo.Util.CodeGenerating;
using Nijo.Parts.WebServer;

namespace Nijo.Parts {
    internal class Configure : ISummarizedFile {
        internal const string ABSTRACT_CLASS_NAME = "DefaultConfiguration";
        internal const string CONCRETE_CLASS_NAME = "CustomizedConfiguration";
        internal const string CONFIGURE_SERVICES = "ConfigureServices";

        internal static string GetClassFullname(Config config) => $"{config.RootNamespace}.{ABSTRACT_CLASS_NAME}";

        void ISummarizedFile.OnEndGenerating(CodeRenderingContext context) {
            context.CoreLibrary.AutoGeneratedDir(genDir => {
                genDir.Generate(RenderConfigureServices());
            });
        }

        internal SourceFile RenderConfigureServices() {
            return new SourceFile {
                FileName = "DefaultConfiguration.cs",
                RenderContent = _ctx => {
                    var appSrv = new ApplicationService();
                    var runtimeServerSettings = RuntimeSettings.ServerSetiingTypeFullName;

                    return $$"""
                        namespace {{_ctx.Config.RootNamespace}} {
                            using Microsoft.EntityFrameworkCore;
                            using Microsoft.Extensions.Configuration;
                            using Microsoft.Extensions.DependencyInjection;
                            using NLog;
                            using NLog.Web;

                            /// <summary>
                            /// 自動生成されたアプリケーション実行時設定。
                            /// 設定の一部を変更したい場合は <see cref="{{CONCRETE_CLASS_NAME}}"/> で該当の設定をオーバーライドしてください。
                            /// </summary>
                            public abstract partial class {{ABSTRACT_CLASS_NAME}} {

                                /// <summary>
                                /// DI設定
                                /// </summary>
                                /// <param name="appSettingsNijoSection">appsettings.jsonのうちNijoセクション部分</param>
                                public virtual void {{CONFIGURE_SERVICES}}(IServiceCollection services, IConfigurationSection appSettingsNijoSection) {

                                    // アプリケーションサービス
                                    ConfigureApplicationService(services);

                                    // 実行時設定ファイル
                                    ConfigureRuntimeSetting(services);

                                    // DB接続
                                    services.AddScoped<Microsoft.EntityFrameworkCore.DbContext, {{_ctx.Config.DbContextName}}>();
                                    ConfigureDbContext(services);

                                    // Encodingで Shift-JIS が使えるようにする
                                    System.Text.Encoding.RegisterProvider(System.Text.CodePagesEncodingProvider.Instance);
                                }

                                /// <summary>
                                /// <see cref="{{appSrv.ConcreteClassName}}"/> をDIに登録します。
                                /// </summary>
                                protected abstract void ConfigureApplicationService(IServiceCollection services);

                                /// <summary>
                                /// 実行時設定をどこから参照するかの処理をDIに登録します。
                                /// <see cref="IRuntimeSetting"/> 型を登録してください。
                                /// </summary>
                                protected abstract void ConfigureRuntimeSetting(IServiceCollection services);

                                /// <summary>
                                /// Entity Framework Core のDbContextをDIに登録します。
                                /// </summary>
                                protected abstract void ConfigureDbContext(IServiceCollection services);

                        {{_methods.SelectTextTemplate(sourceCode => $$"""

                                {{WithIndent(sourceCode, "        ")}}
                        """)}}
                            }
                        }
                        """;
                },
            };
        }


        /// <summary>
        /// コンフィグクラスにメソッドを定義します。
        /// </summary>
        internal void AddMethod(string sourceCode) {
            _methods.Add(sourceCode);
        }
        private readonly List<string> _methods = [];
    }
}
