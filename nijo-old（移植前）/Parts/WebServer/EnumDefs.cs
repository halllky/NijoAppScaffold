using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Nijo.Util.CodeGenerating;
using Nijo.Util.DotnetEx;

namespace Nijo.Parts.WebServer {
    internal class EnumDefs : ISummarizedFile {

        private readonly List<string> _sourceCodes = new();

        internal void Add(string sourceCode) {
            _sourceCodes.Add(sourceCode);
        }

        /// <summary>
        /// ほかの <see cref="ISummarizedFile"/> の中で列挙体が生成されることがあるので
        /// </summary>
        int ISummarizedFile.RenderingOrder => 999;

        void ISummarizedFile.OnEndGenerating(CodeRenderingContext context) {

            context.CoreLibrary.AutoGeneratedDir(dir => {
                dir.Generate(new SourceFile {
                    FileName = "Enum.cs",
                    RenderContent = context => $$"""
                        namespace {{context.Config.RootNamespace}} {
                            using System.ComponentModel.DataAnnotations;

                        {{context.Schema.EnumDefinitions.SelectTextTemplate(def => $$"""
                            public enum {{def.Name}} {
                        {{def.Items.SelectTextTemplate(item => $$"""
                        {{If(!string.IsNullOrWhiteSpace(item.DisplayName), () => $$"""
                                [Display(Name = "{{(item.IsEmptyDisplayName ? string.Empty : item.DisplayName.Replace("\"", "\\\""))}}")]
                        """)}}
                                {{item.PhysicalName}} = {{item.Value}},
                        """)}}
                            }
                        """)}}
                        {{_sourceCodes.SelectTextTemplate(source => $$"""

                            {{WithIndent(source, "    ")}}
                        """)}}
                        }
                        """,
                });

                // Oracle用のenum定義（事業PFへの公開テーブルで区分値の名称をとるのに使う）
                // ※ここのディレクトリ名はマイグレーションスクリプト生成コマンドと一致させる必要がある
                dir.Directory("..\\MigrationsScript", migrationDir => {
                    migrationDir.Generate(GetOracleGetNameFunction());
                });
            });

            context.ReactProject.AutoGeneratedDir(dir => {
                dir.Generate(new SourceFile {
                    FileName = "autogenerated-enum.ts",
                    RenderContent = ctx => {
                        return $$"""
                            /** 自動生成された列挙体の型 */
                            export interface EnumTypes {
                            {{context.Schema.EnumDefinitions.SelectTextTemplate(def => $$"""
                              {{def.Name}}: {{def.Items.Select(item => $"'{(item.IsEmptyDisplayName ? string.Empty : item.DisplayName.Replace("'", "\\'"))}'").Join(" | ")}}
                            """)}}
                            }

                            /** 自動生成された列挙体の名前と型のマッピング */
                            export interface EnumTypeMap {
                            {{context.Schema.EnumDefinitions.SelectTextTemplate(def => $$"""
                              '{{def.Name}}': EnumTypes['{{def.Name}}']
                            """)}}
                            }

                            /** 自動生成された列挙体の値（画面表示用データ） */
                            export const EnumValues = {
                            {{context.Schema.EnumDefinitions.SelectTextTemplate(def => $$"""
                              '{{def.Name}}': [
                            {{def.Items.SelectTextTemplate(item => $$"""
                                '{{(item.IsEmptyDisplayName ? string.Empty : item.DisplayName.Replace("'", "\\'"))}}',
                            """)}}
                              ] satisfies EnumTypes['{{def.Name}}'][],
                            """)}}
                            }

                            /** 自動生成された列挙体の値（検索条件） */
                            export const EnumValuesForSearchCondition = {
                            {{context.Schema.EnumDefinitions.SelectTextTemplate(def => $$"""
                              '{{def.Name}}': [
                            {{def.Items.SelectTextTemplate(item => $$"""
                                '{{item.DisplayName.Replace("'", "\\'")}}',
                            """)}}
                              ] satisfies (keyof EnumTypesOfSearchCondition['{{def.Name}}'])[],
                            """)}}
                            }

                            /** 自動生成された列挙体の検索条件用オブジェクトの値 */
                            export type EnumTypesOfSearchCondition = {
                            {{context.Schema.EnumDefinitions.SelectTextTemplate(def => $$"""
                              '{{def.Name}}': {
                            {{def.Items.SelectTextTemplate(item => $$"""
                                '{{item.DisplayName.Replace("'", "\\'")}}'?: boolean
                            """)}}
                              }
                            """)}}
                            }
                            """;
                    },
                });
            });

        }

        private static SourceFile GetOracleGetNameFunction() {
            return new SourceFile {
                FileName = "Enum名取得関数（事業PF公開テーブル用）.sql",
                RenderContent = ctx => {
                    return $$"""
                        {{ctx.Schema.EnumDefinitions.SelectTextTemplate(enm => $$"""
                        {{Render(enm)}}
                        /

                        """)}}
                        """;

                    string Render(Core.EnumDefinition enm) {
                        var functionName = $"GET_{enm.Name.ToCSharpSafe()}_NAME";
                        const string ARGS = "p_value";

                        return $$"""
                            CREATE OR REPLACE FUNCTION {{functionName}}({{ARGS}} IN NVARCHAR2) RETURN NVARCHAR2
                            IS
                            BEGIN
                            {{If(enm.Items.Count == 0, () => $$"""
                                RETURN NULL;
                            """).Else(() => $$"""
                            {{enm.Items.SelectTextTemplate((item, ix) => $$"""
                                {{(ix == 0 ? "IF" : "ELSIF")}} {{ARGS}} = '{{item.Value}}' THEN
                                    RETURN N'{{item.DisplayName.Replace("'", "\\'")}}';
                            """)}}
                                ELSE
                                    RETURN NULL;
                                END IF;
                            """)}}
                            END;
                            """;
                    }
                },
            };
        }
    }
}
