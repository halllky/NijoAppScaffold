using NUnit.Framework;

namespace Nijo.IntegrationTest.Tests;

public class GeneratedProjectOptionsのテスト {

    [Test]
    public async Task ソースコード自動生成先フォルダを指定できるかの確認() {

        using var project = await NijoTestUtil.CreateNewProjectAsync($$"""
            <!-- ここで明示的に指定した名前のフォルダの内部にコードが生成されることを確認したい -->
            <NijoAppScaffold CoreLibraryFolderName="MyCoreLib"
                             WebapiProjectFolderName="MyWebApi"
                             ReactProjectFolderName="MyReactProject">
              <DataStructures>

                <!-- Web クライアント側、サーバー側両方に何かしら生成されるようなデータを定義する -->
                <Data1 Type="data-model" GenerateDefaultQueryModel="True">
                  <Id Type="int" IsKey="True" />
                  <Name Type="word" />
                </Data1>

              </DataStructures>
            </NijoAppScaffold>
            """);

        await project.GenerateCodeAsync();

        Assert.Multiple(() => {
            Assert.That(Directory.Exists(Path.Combine(project.Project.ProjectRoot, "MyCoreLib", "__AutoGenerated")), Is.True);
            Assert.That(Directory.Exists(Path.Combine(project.Project.ProjectRoot, "MyWebApi", "__AutoGenerated")), Is.True);
            Assert.That(Directory.Exists(Path.Combine(project.Project.ProjectRoot, "MyReactProject", "__autoGenerated")), Is.True);
        });

        Assert.That(await project.CheckCompileAsync(), Is.True);
    }
}
