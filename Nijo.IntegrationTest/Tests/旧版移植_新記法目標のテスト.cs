    [Test]
    public async Task MessageConstが旧版と同じソースを生成すること() {
        using var legacyProject = await NijoTestUtil.CreateLegacyProjectAsync("""
            <LegacyApp />
            """);
        using var migratedProject = await NijoTestUtil.CreateNewProjectAsync("""
            <NijoAppScaffold RootNamespace="LegacyApp"
                             DbContextName="MyDbContext"
                             ForceLegacyCompatibilityMode="true"
                             SuppressAutoGeneratedComment="true"
                             CoreLibraryFolderName="core.AutoGenerated"
                             WebapiProjectFolderName="webapi"
                             ReactProjectFolderName="react/src">
            </NijoAppScaffold>
            """);

        var messageXml = """
            <Messages>
              <MessageList Type="message-list">
                <ERRC9001>固定メッセージ</ERRC9001>
                <ERRC9002 arg0="項目名">{0}を入力してください。</ERRC9002>
                <ERRC9003 arg0="1行目" arg1="2行目">
                  {0}
                  {1}
                </ERRC9003>
              </MessageList>
            </Messages>
            """;
        await File.WriteAllTextAsync(Path.Combine(legacyProject.ProjectRoot, "nijo.メッセージ一覧.xml"), messageXml, new UTF8Encoding(false));
        await File.WriteAllTextAsync(Path.Combine(migratedProject.Project.ProjectRoot, "nijo.メッセージ一覧.xml"), messageXml, new UTF8Encoding(false));

        Assert.That(await legacyProject.GenerateCodeAsync(), Is.True);
        Assert.That(await migratedProject.GenerateCodeAsync(), Is.True);

        var expectedCs = File.ReadAllText(
            Path.Combine(legacyProject.CoreAutoGeneratedDir, "Util", "MSG.cs"),
            Encoding.UTF8).ReplaceLineEndings("\n");
        var actualCs = File.ReadAllText(
            Path.Combine(migratedProject.Project.CoreLibraryRoot, "__AutoGenerated", "Util", "MSG.cs"),
            Encoding.UTF8).ReplaceLineEndings("\n");
        Assert.That(actualCs, Is.EqualTo(expectedCs), "MSG.cs");

        var expectedTs = File.ReadAllText(
            Path.Combine(legacyProject.ReactAutoGeneratedDir, "util", "MSG.ts"),
            Encoding.UTF8).ReplaceLineEndings("\n");
        var actualTs = File.ReadAllText(
            Path.Combine(migratedProject.Project.ReactProjectRoot, "__autoGenerated", "util", "MSG.ts"),
            Encoding.UTF8).ReplaceLineEndings("\n");
        Assert.That(actualTs, Is.EqualTo(expectedTs), "MSG.ts");
    }

