import React from "react"
import * as Icon from "@heroicons/react/24/solid"
import { QueryModelType, LoadFeature } from "../__autoGenerated"
import buildDynamicFormSchema from "./buildDynamicFormSchema"
import { MetadataContext } from "./MetadataContext"
import CommandExecutorView, { ExecutionHistory } from "./CommandExecutorView"
import { ArrayMember, DynamicForm, EditableGrid, MemberOwner, SectionMember } from "@nijo/ui-components/layout"
import { LoadReturnType } from "../__autoGenerated/util"
import { Allotment } from "allotment"
import { useDynamicFormColumnDefs } from "@nijo/ui-components/layout/DynamicForm/Form.ArrayAsGrid"

/**
 * Query Model のWeb側エンドポイントの呼び出しと結果の閲覧を行なうデバッグ用コンポーネント。
 */
export default function ({ rootAggregateType, className }: {
  rootAggregateType: QueryModelType
  className?: string
}) {
  const helper = React.useContext(MetadataContext)

  // 自動生成されたメタデータをもとにした検索条件の入力フォーム定義
  const searchConditionFormSchema = React.useMemo((): SectionMember => {
    const searchConditionMetadata = helper.metadata[rootAggregateType]
    if (!searchConditionMetadata) throw new Error(`${rootAggregateType}SearchCondition が見つかりません`)
    return {
      type: 'section',
      physicalName: 'filter',
      members: buildDynamicFormSchema(searchConditionMetadata, 'query-model', true, helper).members,
    }
  }, [rootAggregateType, helper])

  // 自動生成されたメタデータをもとにした検索結果の表示フォーム定義
  const displayDataFormSchema = React.useMemo((): MemberOwner => {
    const displayDataMetadata = helper.metadata[rootAggregateType]
    if (!displayDataMetadata) throw new Error(`${rootAggregateType}DisplayData が見つかりません`)
    return {
      members: [{
        physicalName: 'totalCount' satisfies keyof LoadReturnType<any>,
        label: '結果件数',
        fullWidth: true,
        contents: ({ useFormReturn: { getValues } }) => (
          <div className="px-1">
            {getValues('totalCount' satisfies keyof LoadReturnType<any>)}件
          </div>
        ),
      }, {
        type: 'array',
        arrayLabel: '検索結果',
        physicalName: 'currentPageItems' satisfies keyof LoadReturnType<any>,
        members: buildDynamicFormSchema(displayDataMetadata, 'query-model', false, helper).members,
        showAsGridForce: true,
      }]
    }
  }, [rootAggregateType, helper])

  // 選択された履歴
  const [selectedHistory, setSelectedHistory] = React.useState<ExecutionHistory | null>(null)
  const [parameterOpened, setParameterOpened] = React.useState(false)
  const handleButtonClick = () => {
    setParameterOpened(prev => !prev)
  }

  // 検索結果列定義
  const searchResultSchema = React.useMemo((): ArrayMember => {
    const metadata = helper.metadata[rootAggregateType]
    if (!metadata) throw new Error(`${rootAggregateType}SearchCondition が見つかりません`)
    return {
      type: 'array',
      arrayLabel: '検索結果',
      physicalName: 'currentPageItems' satisfies keyof LoadReturnType<any>,
      members: buildDynamicFormSchema(metadata, 'query-model', false, helper).members,
      showAsGridForce: true,
    }
  }, [])
  const searchResultColumnDefs = useDynamicFormColumnDefs(searchResultSchema, 'currentPageItems', true)
  const searchResultRows = React.useMemo(() => {
    if (!selectedHistory) return []
    if (!selectedHistory.ok) return []
    return (selectedHistory.returnValue as LoadReturnType<any>).currentPageItems
  }, [selectedHistory])

  return (
    <CommandExecutorView
      title={rootAggregateType}
      parameterFormSchema={searchConditionFormSchema}
      onSelectedHistoryChanged={setSelectedHistory}
      localStorageUniqueKey={helper.metadata[rootAggregateType].uniqueId}
      executionEndPoint={LoadFeature.Endpoint[rootAggregateType]}
      className={className}
    >
      {selectedHistory && (
        <div key={selectedHistory.id} className="w-full h-full flex flex-col">

          {/* 検索条件欄折りたたみボタン */}
          <button type="button" onClick={handleButtonClick} className="flex items-center gap-px text-sm bg-gray-300 cursor-pointer">
            {parameterOpened
              ? <Icon.ChevronDownIcon className="w-4 h-4" />
              : <Icon.ChevronRightIcon className="w-4 h-4" />}
            検索条件
          </button>

          <Allotment
            vertical
            className="flex-1"
          >

            {/* 検索条件表示欄 */}
            <Allotment.Pane
              visible={parameterOpened}
              snap
            >
              <div className="w-full h-full p-1 overflow-y-auto">
                <DynamicForm
                  root={searchConditionFormSchema}
                  defaultValues={selectedHistory.parameter}
                  isReadOnly
                />
              </div>
            </Allotment.Pane>

            {/* 戻り値表示欄 */}
            <Allotment.Pane>
              <div className="w-full h-full flex flex-col">
                <div className="flex items-center gap-1 pl-4 text-sm bg-gray-300 select-none">
                  検索結果
                </div>
                {selectedHistory.ok ? (
                  <EditableGrid
                    getColumnDefs={searchResultColumnDefs}
                    rows={searchResultRows}
                    className="flex-1"
                  />
                ) : (
                  <div className="flex-1 p-1 overflow-y-auto">
                    {selectedHistory.errors.map((error) => (
                      <div key={error}>{error}</div>
                    ))}
                  </div>
                )}
              </div>
            </Allotment.Pane>
          </Allotment>
        </div>
      )}
    </CommandExecutorView>
  )
}
