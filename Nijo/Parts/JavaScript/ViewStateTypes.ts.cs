using Nijo.CodeGenerating;
using Nijo.Parts.Common;
using Nijo.ImmutableSchema;
using System;
using System.Collections.Generic;
using System.Linq;
using System.IO;
using static Nijo.WebService.SchemaEditor.ApplicationStateAndSchemaGraphViewState;
using Nijo.Util.DotnetEx;

namespace Nijo.Parts.JavaScript;

/// <summary>
/// nijo.viewState.jsonのデータをexport defaultする関数をレンダリングする
/// </summary>
public class ViewStateTypes {

    public void Render(CodeRenderingContext ctx) {
        ctx.ReactProject(autoGenerated => {
            autoGenerated.Directory("util", dir => {
                dir.Generate(RenderViewStateTypes(ctx));
            });
        });
    }

    /// <summary>
    /// ViewStateTypes.tsファイルをレンダリングする
    /// </summary>
    public static SourceFile RenderViewStateTypes(CodeRenderingContext ctx) {

        // デフォルト値
        string viewStateJsonContent = $$"""
            {
              {{SchemaGraphViewStateTypeByViewMode.KEY_SCHEMA_DEFINITION}}: {
                nodes: {},
                edges: [],
                nodePositions: {},
              },
            }
            """;

        // 自動生成時点のnijo.viewState.jsonの内容を読み込む
        var viewStateJsonPath = ctx.Project.ViewStateJsonPath;

        if (File.Exists(viewStateJsonPath)) {
            try {
                viewStateJsonContent = File.ReadAllText(viewStateJsonPath);
            } catch (Exception) {
                // ファイル読み込みに失敗した場合はデフォルト値のまま
            }
        }

        const string RETURN_TYPE = "GetNijoXmlViewStateReturnType";

        return new SourceFile {
            FileName = "get-nijo-xml-view-state.ts",
            Contents = $$"""

                /** nijo.xmlを GraphView で表示するためのデータの型 */
                type {{RETURN_TYPE}} = {
                  {{SchemaGraphViewStateTypeByViewMode.KEY_SCHEMA_DEFINITION}}: {
                    /** 有向グラフのノード一覧 */
                    {{SchemaGraphViewStateType.KEY_NODES}}: {
                      [id: string]: {
                        id: string
                        label: string
                        [others: string]: any
                      }
                    }
                    /** 有向グラフのエッジ一覧 */
                    {{SchemaGraphViewStateType.KEY_EDGES}}: {
                      source: string
                      target: string
                      [others: string]: any
                    }[]
                    /** ノードの位置 */
                    {{SchemaGraphViewStateType.KEY_NODE_POSITIONS}}: {
                      [id: string]: {
                        x: number
                        y: number
                      }
                    }
                    /** 未使用項目 */
                    parentMap?: unknown
                  }
                } & {
                  /** 未使用項目 */
                  erDiagram?: unknown
                  /** 未使用項目 */
                  displayMode?: unknown
                  /** 未使用項目 */
                  onlyRoot?: unknown
                }

                /** nijo.xml のスキーマ定義を GraphView で表示できるようにしたデータ */
                export default function (): {{RETURN_TYPE}} {
                  return {{WithIndent(viewStateJsonContent)}}
                }
                """,
        };
    }
}
