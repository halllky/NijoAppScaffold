/// <summary>
/// nijo.xml の定義をほぼそのまま C# や TypeScript のコードとしてレンダリングする。
/// 各アプリケーションごとの共通処理でリフレクション的に利用されることを想定している。
/// </summary>
internal class MetadataForPage : IMultiAggregateSourceFile {

    private readonly List<RootAggregate> _entries = new();
    private readonly Lock _lock = new();
    internal MetadataForPage Add(RootAggregate aggregate) {
        lock (_lock) {
            _entries.Add(aggregate);
            return this;
        }
    }

    void IMultiAggregateSourceFile.RegisterDependencies(IMultiAggregateSourceFileManager ctx) {
        // 特になし
    }

    void IMultiAggregateSourceFile.Render(CodeRenderingContext ctx) {
        ctx.ReactProject(autoGenerated => {
            autoGenerated.Directory("util", dir => {
                dir.Generate(RenderTypeScript(ctx));
            });
        });

        ctx.CoreLibrary(autoGenerated => {
            autoGenerated.Directory("Util", utilDir => {
                utilDir.Generate(RenderCSharp(ctx));
            });
        });
    }

    private SourceFile RenderTypeScript(CodeRenderingContext ctx) {
        var entriesOrderByDataFlow = _entries
            .OrderBy(agg => agg.GetRoot().GetIndexOfDataFlow())
            .Select(aggregate => (IMetadataEntity)new AggregateMetadata(aggregate));

        return new SourceFile {
            FileName = "metadata-for-page.ts",
            Contents = $$"""

                import { {{CommandQueryMappings.DATA_MODEL_TYPE}}, {{CommandQueryMappings.QUERY_MODEL_TYPE}}, {{CommandQueryMappings.COMMAND_MODEL_TYPE}}, {{CommandQueryMappings.STRUCTURE_MODEL_TYPE}}, {{CommandQueryMappings.REFERED_QUERY_MODEL_TYPE}} } from ".."
                import * as EnumDefs from "../enum-defs"

                /** 画面の自動生成のためのメタデータ */
                export namespace MetadataForPage {
                  {{WithIndent(AggregateMetadata.RenderTypeScriptType(ctx))}}

                  {{WithIndent(ValueMetadata.RenderTypeScriptType(ctx))}}

                  {{WithIndent(RefMetadata.RenderTypeScriptType(ctx))}}

                  /** 画面の自動生成のためのメタデータ取得関数 */
                  export const getAll = () => ({
                {{entriesOrderByDataFlow.SelectTextTemplate(entry => $$"""
                    '{{entry.PhysicalName}}': {{WithIndent(entry.RenderTypeScriptMetadataObject(ctx))}},
                """)}}
                  }) as const satisfies { [k in ( {{CommandQueryMappings.DATA_MODEL_TYPE}} | {{CommandQueryMappings.QUERY_MODEL_TYPE}} | {{CommandQueryMappings.COMMAND_MODEL_TYPE}} | {{CommandQueryMappings.STRUCTURE_MODEL_TYPE}})]: {{AggregateMetadata.TYPE_ROOT_AGGREGATE_NAME}} }
                }
                """,
        };
    }

    private SourceFile RenderCSharp(CodeRenderingContext ctx) {
        var entriesOrderByDataFlow = _entries
            .OrderBy(agg => agg.GetRoot().GetIndexOfDataFlow())
            .Select(aggregate => new {
                Entry = new AggregateMetadata(aggregate),
                Tree = aggregate
                    .EnumerateThisAndDescendants()
                    .Select(m => new AggregateMetadata(m)),
            })
            .ToArray();

        return new SourceFile {
            FileName = "MetadataForPage.cs",
            Contents = $$"""
                using System;
                using System.Collections.Generic;

                namespace {{ctx.Config.RootNamespace}};

                /// <summary>
                /// 画面の自動生成のためのメタデータ
                /// </summary>
                public static class MetadataForPage {
                    {{WithIndent(AggregateMetadata.RenderCSharpType(ctx))}}

                    {{WithIndent(ValueMetadata.RenderCSharpType(ctx))}}

                    {{WithIndent(RefMetadata.RenderCSharpType(ctx))}}

                    /// <summary>
                    /// 画面の自動生成のためのメタデータ取得関数
                    /// </summary>
                    public static Dictionary<string, {{AggregateMetadata.TYPE_NAME}}> GetAll() => new() {
                {{entriesOrderByDataFlow.SelectTextTemplate(e => $$"""
                        { "{{e.Entry.PhysicalName}}", {{e.Entry.PhysicalName}} },
                """)}}
                    };

                {{entriesOrderByDataFlow.SelectTextTemplate(e => $$"""
                    #region {{e.Entry.PhysicalName}}
                    public static {{e.Entry.CSharpTypeName}} {{e.Entry.PhysicalName}} => {{WithIndent(e.Entry.RenderCSharpNewStatement(ctx))}};
                {{e.Tree.SelectTextTemplate(agg => $$"""
                    {{WithIndent(agg.RenderCSharpMemberClass(ctx))}}
                """)}}
                    #endregion {{e.Entry.PhysicalName}}
                """)}}
                }
                """,
        };
    }

    /// <summary>
    /// ルート, Child, Children のメタデータ
    /// </summary>
    internal class AggregateMetadata : IMetadataEntity {
        internal AggregateMetadata(AggregateBase aggregate) {
            _aggregate = aggregate;
        }
        private readonly AggregateBase _aggregate;

        public string PhysicalName => _aggregate.PhysicalName;
        public string CSharpTypeName => $"{TYPE_NAME}<{MemberClassName}>";
        public string MemberClassName => $"{_aggregate.PhysicalName}MetadataMembers";

        string IMetadataEntity.RenderTypeScriptMetadataObject(CodeRenderingContext ctx) {
            var type = _aggregate switch {
                RootAggregate => "RootAggregate",
                ChildAggregate => "ChildAggregate",
                ChildrenAggregate => "ChildrenAggregate",
                _ => throw new NotImplementedException(),
            };

            return $$"""
                {
                  uniqueId: '{{_aggregate.XElement.Attribute(nameof(WebService.SchemaEditor.XmlElementItem.UniqueId))?.Value.Replace("'", "\\'")}}',
                  type: '{{type}}',
                  model: '{{_aggregate.GetRoot().Model.SchemaName}}',
                {{RenderNodeOptions(_aggregate.XElement, ctx).SelectTextTemplate(source => $$"""
                  {{WithIndent(source)}}
                """)}}
                  members: {
                {{_aggregate.GetMembers().Select(IMetadataEntity.GetImpl).SelectTextTemplate(m => $$"""
                    '{{m.PhysicalName}}': {{WithIndent(m.RenderTypeScriptMetadataObject(ctx))}},
                """)}}
                  },
                }
                """;
        }

        public string RenderCSharpNewStatement(CodeRenderingContext ctx) {
            var type = _aggregate switch {
                RootAggregate => "RootAggregate",
                ChildAggregate => "ChildAggregate",
                ChildrenAggregate => "ChildrenAggregate",
                _ => throw new NotImplementedException(),
            };

            return $$"""
                new {{CSharpTypeName}} {
                    UniqueId = "{{_aggregate.XElement.Attribute(nameof(WebService.SchemaEditor.XmlElementItem.UniqueId))?.Value.Replace("\"", "\\\"")}}",
                    Type = "{{type}}",
                    Model = E_ModelType.{{_aggregate.GetRoot().Model.SchemaName.KebabCaseToPascalCase()}},
                {{RenderNodeOptionsCs(_aggregate.XElement, ctx).SelectTextTemplate(source => $$"""
                    {{WithIndent(source)}}
                """)}}
                    Members = new {{MemberClassName}}(),
                }
                """;
        }
        /// <summary>メタデータのメンバークラス定義をレンダリングする</summary>
        internal string RenderCSharpMemberClass(CodeRenderingContext ctx) {
            return $$"""
                public sealed class {{MemberClassName}} : {{I_AGGREGATE_METADATA_MEMBER}} {
                {{_aggregate.GetMembers().Select(IMetadataEntity.GetImpl).SelectTextTemplate(m => $$"""
                    public {{m.CSharpTypeName}} {{m.PhysicalName}} => {{WithIndent(m.RenderCSharpNewStatement(ctx))}};
                """)}}

                    public Dictionary<string, {{TYPE_MEMBER}}> AsDictionary() {
                        return new() {
                {{_aggregate.GetMembers().Select(IMetadataEntity.GetImpl).SelectTextTemplate(m => $$"""
                            { "{{m.PhysicalName}}", {{m.PhysicalName}} },
                """)}}
                        };
                    }
                }
                """;
        }
        internal const string I_AGGREGATE_METADATA_MEMBER = "IAggregateMetadataMember";

        internal const string TYPE_NAME = "StructureMetadata";
        /// <summary>TypeScript側でのみ使用</summary>
        internal const string TYPE_ROOT_AGGREGATE_NAME = "RootAggregateMetadata";
        internal const string TYPE_MEMBER = "StructureMetadataMember";

        internal static string RenderTypeScriptType(CodeRenderingContext ctx) {

            var modelNames = ctx.SchemaParser.Models
                .Select(m => $"'{m.Key}'")
                .ToArray();

            return $$"""
                /** 構造体のメタデータ */
                export type {{TYPE_NAME}} = {
                  /** XMLスキーマ上でこの要素を示す一意なID */
                  uniqueId: string
                  /** 集約の種類 */
                  type: 'RootAggregate' | 'ChildAggregate' | 'ChildrenAggregate'
                  /** この集約のモデル。子孫集約の場合も値が入る。 */
                  model: {{(modelNames.Length == 0 ? "never" : modelNames.Join(" | "))}}
                  /** この構造体のメンバー */
                  members: { [key: string]: {{TYPE_MEMBER}} }
                {{RenderNodeOptionTypes(ctx).SelectTextTemplate(source => $$"""
                  {{WithIndent(source)}}
                """)}}
                }

                /** ルート集約のメタデータ */
                export type {{TYPE_ROOT_AGGREGATE_NAME}} = {{TYPE_NAME}} & { type: 'RootAggregate' }

                /** 構造体のメタデータのメンバー */
                export type {{TYPE_MEMBER}} = {{TYPE_NAME}} | {{ValueMetadata.TYPE_NAME}} | {{RefMetadata.TYPE_NAME}}
                """;
        }

        internal static string RenderCSharpType(CodeRenderingContext ctx) {
            return $$"""
                /// <summary>
                /// 構造体のメタデータ
                /// </summary>
                public class {{TYPE_NAME}} : {{TYPE_MEMBER}} {
                    /// <summary>集約の種類</summary>
                    public required string Type { get; init; }
                    /// <summary>この集約のモデル。子孫集約の場合も値が入る。</summary>
                    public E_ModelType Model { get; init; }
                {{RenderNodeOptionTypesCs(ctx).SelectTextTemplate(source => $$"""
                    {{WithIndent(source)}}
                """)}}

                    /// <summary>この構造体のメンバー</summary>
                    public virtual Dictionary<string, {{TYPE_MEMBER}}> GetMembers() => [];
                }
                /// <inheritdoc cref="{{TYPE_NAME}}"/>
                public class {{TYPE_NAME}}<TMembers> : {{TYPE_NAME}} where TMembers : {{I_AGGREGATE_METADATA_MEMBER}} {
                    /// <summary>この構造体のメンバー</summary>
                    public required TMembers Members { get; init; }
                    public override Dictionary<string, {{TYPE_MEMBER}}> GetMembers() => Members.AsDictionary();
                }

                /// <summary>
                /// 構造体のメタデータのメンバー定義部分
                /// </summary>
                public interface {{I_AGGREGATE_METADATA_MEMBER}} {
                    Dictionary<string, {{TYPE_MEMBER}}> AsDictionary();
                }

                /// <summary>
                /// スキーマ定義で使用可能なモデルの種類
                /// </summary>
                public enum E_ModelType {
                {{ctx.SchemaParser.Models.SelectTextTemplate(m => $$"""
                    {{m.Key.KebabCaseToPascalCase()}},
                """)}}
                }

                /// <summary>
                /// 構造体のメタデータのメンバー
                /// </summary>
                public abstract class {{TYPE_MEMBER}} {
                    /// <summary>XMLスキーマ上でこの要素を示す一意なID</summary>
                    public required string UniqueId { get; init; }
                }
                """;
        }
    }
    /// <summary>
    /// 値のメタデータ
    /// </summary>
    internal class ValueMetadata : IMetadataEntity {
        internal ValueMetadata(ValueMember vm) {
            _vm = vm;
        }
        private readonly ValueMember _vm;

        string IMetadataEntity.PhysicalName => _vm.PhysicalName;
        string IMetadataEntity.CSharpTypeName => TYPE_NAME;

        string IMetadataEntity.RenderTypeScriptMetadataObject(CodeRenderingContext ctx) {

            // 型は enum, value-object の場合はそれぞれ別の名前のプロパティで定義
            var typeProp = _vm.Type switch {
                StaticEnumMember => "enumType",
                ValueObjectMember => "valueObjectType",
                _ => "type",
            };

            return $$"""
                {
                  uniqueId: '{{_vm.XElement.Attribute(nameof(WebService.SchemaEditor.XmlElementItem.UniqueId))?.Value.Replace("'", "\\'")}}',
                  {{typeProp}}: '{{_vm.Type.SchemaTypeName}}',
                {{RenderNodeOptions(_vm.XElement, ctx).SelectTextTemplate(source => $$"""
                  {{WithIndent(source)}}
                """)}}
                }
                """;
        }

        string IMetadataEntity.RenderCSharpNewStatement(CodeRenderingContext ctx) {
            var typeValue = _vm.Type switch {
                StaticEnumMember => $"EnumType = \"{_vm.Type.SchemaTypeName}\"",
                ValueObjectMember => $"ValueObjectType = \"{_vm.Type.SchemaTypeName}\"",
                _ => $"Type = \"{_vm.Type.SchemaTypeName}\"",
            };

            return $$"""
                new {{TYPE_NAME}} {
                    UniqueId = "{{_vm.XElement.Attribute(nameof(WebService.SchemaEditor.XmlElementItem.UniqueId))?.Value.Replace("\"", "\\\"")}}",
                    {{typeValue}},
                {{RenderNodeOptionsCs(_vm.XElement, ctx).SelectTextTemplate(source => $$"""
                    {{WithIndent(source)}}
                """)}}
                }
                """;
        }

        internal const string TYPE_NAME = "ValueMetadata";

        internal static string RenderTypeScriptType(CodeRenderingContext ctx) {
            var characterTypes = ctx.GetCharacterTypes().ToArray();
            var charTypeLiteral = characterTypes.Length == 0
                ? "never"
                : characterTypes.Select(type => $"'{type.Replace("'", "\\'")}'").Join(" | ");

            var valueObjectTypes = ctx.SchemaParser.GetValueObjectMembers().Values.ToArray();
            var valueObjectLiteral = valueObjectTypes.Length == 0
                ? "never"
                : valueObjectTypes.Select(type => $"'{type.TypePhysicalName}'").Join(" | ");

            return $$"""
                /** 値のメタデータ */
                export type {{TYPE_NAME}} = {
                  /** XMLスキーマ上でこの要素を示す一意なID */
                  uniqueId: string
                  /** 値の型名。このメンバーが enum または value-ojbect の場合は undefined になる */
                  type?: {{ctx.SchemaParser.ValueMemberTypes.Select(t => $"'{t.SchemaTypeName}'").Join(" | ")}}
                  /** 列挙体の型名。このメンバーが列挙体の場合のみ */
                  enumType?: keyof EnumDefs.{{EnumFile.TS_TYPE_MAP}}
                  /** 値オブジェクトの型名。このメンバーが値オブジェクトの場合のみ */
                  valueObjectType?: {{valueObjectLiteral}}
                {{RenderNodeOptionTypes(ctx).SelectTextTemplate(source => $$"""
                  {{WithIndent(source)}}
                """)}}
                }
                """;
        }

        internal static string RenderCSharpType(CodeRenderingContext ctx) {
            var valueObjectTypes = ctx.SchemaParser.GetValueObjectMembers().Values.ToArray();

            return $$"""
                /// <summary>
                /// 値のメタデータ
                /// </summary>
                public class {{TYPE_NAME}} : {{AggregateMetadata.TYPE_MEMBER}} {
                    /// <summary>値の型名。このメンバーが enum または value-ojbect の場合は null になる</summary>
                    public string? Type { get; init; }
                    /// <summary>列挙体の型名。このメンバーが列挙体の場合のみ</summary>
                    public string? EnumType { get; init; }
                    /// <summary>値オブジェクトの型名。このメンバーが値オブジェクトの場合のみ</summary>
                    public string? ValueObjectType { get; init; }
                {{RenderNodeOptionTypesCs(ctx).SelectTextTemplate(source => $$"""
                    {{WithIndent(source)}}
                """)}}
                }
                """;
        }
    }
