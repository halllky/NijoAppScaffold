    /// <summary>
    /// 構造体 or 値 or 外部参照 のメタデータの実体
    /// </summary>
    internal interface IMetadataEntity {
        /// <summary>物理名</summary>
        string PhysicalName { get; }
        /// <summary>C#の型名</summary>
        string CSharpTypeName { get; }
        /// <summary>メタデータのキーと値を格納するJavaScriptオブジェクトをレンダリングする</summary>
        string RenderTypeScriptMetadataObject(CodeRenderingContext ctx);
        /// <summary>メタデータのキーと値を格納するC#のnewステートメントをレンダリングする</summary>
        string RenderCSharpNewStatement(CodeRenderingContext ctx);

        static IMetadataEntity GetImpl(IAggregateMember member) {
            return member switch {
                ValueMember vm => new ValueMetadata(vm),
                RefToMember rm => new RefMetadata(rm),
                ChildAggregate child => new AggregateMetadata(child),
                ChildrenAggregate children => new AggregateMetadata(children),
                _ => throw new NotImplementedException(),
            };
        }
    }


    #region オプション項目の動的レンダリング
    /// <summary>
    /// XElementで定義されたNodeOptionをTypeScriptのオブジェクトプロパティとしてレンダリングします。
    /// </summary>
    /// <param name="xElement">XML要素</param>
    /// <param name="ctx">レンダリングコンテキスト</param>
    /// <returns>TypeScriptのプロパティ定義文字列</returns>
    internal static IEnumerable<string> RenderNodeOptions(XElement xElement, CodeRenderingContext ctx) {

        var options = ctx.SchemaParser.GetOptions(xElement);

        // DisplayName だけは未定義の場合は物理名を採用する。
        // メタデータを用いたカスタマイズ処理でDisplayNameの使用頻度が高いため。
        if (!options.Any(o => o.AttributeName == BasicNodeOptions.DisplayName.AttributeName)) {
            yield return $$"""
                {{BasicNodeOptions.DisplayName.AttributeName.ToCamelCase()}}: '{{ctx.SchemaParser.GetPhysicalName(xElement)}}',
                """;
        }

        // コメントがある場合はレンダリング
        var comment = xElement.GetCommentSingleLine(E_CsTs.TypeScript);
        if (!string.IsNullOrWhiteSpace(comment)) {
            yield return $$"""
                comment: '{{comment}}',
                """;
        }

        foreach (var option in options) {
            var attributeValue = xElement.Attribute(option.AttributeName)?.Value;

            // フィルタリング: 空文字の文字列をスキップ
            if ((option.Type == E_NodeOptionType.String
              || option.Type == E_NodeOptionType.Integer)
                && string.IsNullOrWhiteSpace(attributeValue)) {
                continue;
            }

            // TypeScriptプロパティ名を生成(camelCaseに変換)
            var propName = option.AttributeName.ToCamelCase();

            // TypeScriptの値をレンダリング
            var tsValue = option.Type switch {
                E_NodeOptionType.Boolean => "true",
                E_NodeOptionType.Integer => attributeValue,
                _ => $"'{attributeValue?.Replace("\\", "\\\\").Replace("'", "\\'")}'",
            };

            yield return $$"""
                {{propName}}: {{tsValue}},
                """;
        }

        // カスタム属性
        foreach (var customAttribute in NijoXmlCustomAttribute.FromXDocument(ctx.SchemaParser.Document)) {
            if (string.IsNullOrWhiteSpace(customAttribute.PhysicalName)) continue;
            if (string.IsNullOrWhiteSpace(customAttribute.UniqueId)) continue;

            var attributeValue = xElement.Attribute(customAttribute.UniqueId)?.Value;
            if (string.IsNullOrWhiteSpace(attributeValue)) continue;

            var tsValue = customAttribute.Type switch {
                NijoXmlCustomAttribute.E_Type.Boolean => "true",
                NijoXmlCustomAttribute.E_Type.Decimal => attributeValue,
                _ => $"'{attributeValue.Replace("\\", "\\\\").Replace("'", "\\'")}'",
            };

            yield return $$"""
                {{customAttribute.PhysicalName.ToCamelCase()}}: {{tsValue}},
                """;
        }
    }

    /// <summary>
    /// XElementで定義されたNodeOptionをC#のプロパティとしてレンダリングします。
    /// </summary>
    /// <param name="xElement">XML要素</param>
    /// <param name="ctx">レンダリングコンテキスト</param>
    /// <returns>C#のプロパティ設定文字列</returns>
    internal static IEnumerable<string> RenderNodeOptionsCs(XElement xElement, CodeRenderingContext ctx) {

        var options = ctx.SchemaParser.GetOptions(xElement);

        // DisplayName だけは未定義の場合は物理名を採用する。
        // メタデータを用いたカスタマイズ処理でDisplayNameの使用頻度が高いため。
        if (!options.Any(o => o.AttributeName == BasicNodeOptions.DisplayName.AttributeName)) {
            yield return $$"""
                {{BasicNodeOptions.DisplayName.AttributeName}} = "{{ctx.SchemaParser.GetPhysicalName(xElement)}}",
                """;
        }

        // コメントがある場合はレンダリング
        var comment = xElement.GetCommentSingleLine(E_CsTs.CSharp);
        if (!string.IsNullOrWhiteSpace(comment)) {
            yield return $$"""
                Comment = "{{comment}}",
                """;
        }

        foreach (var option in options) {
            var attributeValue = xElement.Attribute(option.AttributeName)?.Value;

            // フィルタリング: 空文字の文字列をスキップ
            if ((option.Type == E_NodeOptionType.String
              || option.Type == E_NodeOptionType.Integer)
                && string.IsNullOrWhiteSpace(attributeValue)) {
                continue;
            }

            // C#の値をレンダリング
            var csValue = option.Type switch {
                E_NodeOptionType.Boolean => "true",
                E_NodeOptionType.Integer => attributeValue,
                _ => $"\"{attributeValue?.Replace("\\", "\\\\").Replace("\"", "\\\"")}\"",
            };

            yield return $$"""
                {{option.AttributeName}} = {{csValue}},
                """;
        }

        // カスタム属性
        foreach (var customAttribute in NijoXmlCustomAttribute.FromXDocument(ctx.SchemaParser.Document)) {
            if (string.IsNullOrWhiteSpace(customAttribute.PhysicalName)) continue;
            if (string.IsNullOrWhiteSpace(customAttribute.UniqueId)) continue;

            var attributeValue = xElement.Attribute(customAttribute.UniqueId)?.Value;
            if (string.IsNullOrWhiteSpace(attributeValue)) continue;

            var csValue = customAttribute.Type switch {
                NijoXmlCustomAttribute.E_Type.Boolean => "true",
                NijoXmlCustomAttribute.E_Type.Decimal => $"{attributeValue}m",
                _ => $"\"{attributeValue.Replace("\\", "\\\\").Replace("\"", "\\\"")}\"",
            };

            yield return $$"""
                {{customAttribute.PhysicalName}} = {{csValue}},
                """;
        }
    }

    /// <summary>
    /// TypeScript型定義で使用するNodeOptionの型情報を動的に生成します
    /// </summary>
    internal static IEnumerable<string> RenderNodeOptionTypes(CodeRenderingContext ctx) {
        var nodeOptions = ctx.SchemaParser.GetAllNodeOptions();

        // コメントはXMLの属性ではないので別途宣言している
        yield return $$"""
            /** XMLコメント */
            comment?: string
            """;

        foreach (var option in nodeOptions) {

            string tsType;
            if (option.AttributeName == BasicNodeOptions.CharacterType.AttributeName) {
                // CharacterTypeの場合は特別な型を生成
                var characterTypes = ctx.GetCharacterTypes().ToArray();
                tsType = characterTypes.Length == 0
                    ? "never"
                    : characterTypes.Select(type => $"'{type.Replace("'", "\\'")}'").Join(" | ");

            } else {
                tsType = option.Type switch {
                    E_NodeOptionType.Boolean => "boolean",
                    E_NodeOptionType.Integer => "number",
                    _ => "string",
                };
            }

            // DisplayName だけは必ずレンダリングされる
            var isOptional = option.AttributeName != BasicNodeOptions.DisplayName.AttributeName;

            yield return $$"""
                /** {{option.DisplayName}} */
                {{option.AttributeName.ToCamelCase()}}{{(isOptional ? "?" : "")}}: {{tsType}}
                """;
        }

        // カスタム属性
        foreach (var customAttribute in NijoXmlCustomAttribute.FromXDocument(ctx.SchemaParser.Document)) {
            if (string.IsNullOrWhiteSpace(customAttribute.PhysicalName)) continue;

            var tsType = customAttribute.Type switch {
                NijoXmlCustomAttribute.E_Type.Boolean => "boolean",
                NijoXmlCustomAttribute.E_Type.Decimal => "number",
                NijoXmlCustomAttribute.E_Type.Enum => customAttribute.EnumValues.Length == 0
                    ? "never"
                    : customAttribute.EnumValues.Select(v => $"'{v.Replace("'", "\\'")}'").Join(" | "),
                _ => "string",
            };

            yield return $$"""
                /** {{customAttribute.DisplayName}} */
                {{customAttribute.PhysicalName.ToCamelCase()}}?: {{tsType}}
                """;
        }
    }

    /// <summary>
    /// C#クラス定義で使用するNodeOptionのプロパティ情報を動的に生成します
    /// </summary>
    internal static IEnumerable<string> RenderNodeOptionTypesCs(CodeRenderingContext ctx) {
        var nodeOptions = ctx.SchemaParser.GetAllNodeOptions();

        // コメントはXMLの属性ではないので別途宣言している
        yield return $$"""
            /// <summary>XMLコメント</summary>
            public string? Comment { get; init; }
            """;

        foreach (var option in nodeOptions) {

            // C#の型を決定する。
            // DisplayName だけは必須プロパティ
            string csType = option.AttributeName == BasicNodeOptions.DisplayName.AttributeName
                ? "string"
                : option.Type switch {
                    E_NodeOptionType.Boolean => "bool",
                    E_NodeOptionType.Integer => "int?",
                    _ => "string?",
                };
            var defaultValue = option.AttributeName == BasicNodeOptions.DisplayName.AttributeName
                ? " = string.Empty;"
                : "";

            yield return $$"""
                /// <summary>{{option.DisplayName}}</summary>
                public {{csType}} {{option.AttributeName}} { get; init; }{{defaultValue}}
                """;
        }

        // カスタム属性
        foreach (var customAttribute in NijoXmlCustomAttribute.FromXDocument(ctx.SchemaParser.Document)) {
            if (string.IsNullOrWhiteSpace(customAttribute.PhysicalName)) continue;

            var csType = customAttribute.Type switch {
                NijoXmlCustomAttribute.E_Type.Boolean => "bool",
                NijoXmlCustomAttribute.E_Type.Decimal => "decimal?",
                _ => "string?",
            };

            yield return $$"""
                /// <summary>{{customAttribute.DisplayName}}</summary>
                public {{csType}} {{customAttribute.PhysicalName}} { get; init; }
                """;
        }
    }


