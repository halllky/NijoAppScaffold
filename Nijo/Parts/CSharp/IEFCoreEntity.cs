using Nijo.ImmutableSchema;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Nijo.Parts.CSharp;

/// <summary>
/// Entity Framework Core のエンティティとして扱われるクラスのインターフェース
/// </summary>
internal interface IEFCoreEntity {
    /// <summary>集約</summary>
    AggregateBase Aggregate { get; }

    /// <summary>C#のクラス名</summary>
    string CsClassName { get; }

    /// <summary>DbSetの名前</summary>
    string DbSetName { get; }

    /// <summary>OnModelCreating自動生成メソッド名</summary>
    string OnModelCreatingAutoGenerated { get; }

    /// <summary>作成日時、更新日時、作成者、更新者のカラムを持つかどうか</summary>
    bool HasTimestampColumns { get; }

    /// <summary>楽観排他用のバージョンを持つかどうか</summary>
    bool HasVersionColumn { get; }

    /// <summary>カラムを列挙する</summary>
    IEnumerable<EFCoreEntity.EFCoreEntityColumn> GetColumns();

    /// <summary>ナビゲーションプロパティを列挙する</summary>
    IEnumerable<NavigationProperty> GetNavigationProperties();
}
