using Nijo.CodeGenerating;
using Nijo.ImmutableSchema;
using Nijo.Parts.Common;
using Nijo.Util.DotnetEx;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;

namespace Nijo.Parts.CSharp {
    public class AspNetController {
        public AspNetController(RootAggregate rootAggregate) {
            _rootAggregate = rootAggregate;
        }

        private readonly RootAggregate _rootAggregate;

        /// <summary>
        /// 自動生成される Controller が継承するベースクラス名。
        /// </summary>
        internal const string CONTROLLER_BASE = "ControllerBase";
        /// <summary>
        /// 自動生成される Controller が依存するハンドラーインターフェース名。
        /// 実体は手動実装される必要がある。
        /// </summary>
        internal const string CONTROLLER_HANDLER_INTERFACE = "IAutoGeneratedControllerHandler";
        /// <summary>
        /// 自動生成される Controller が依存するハンドラー実装クラス名。
        /// 実体は手動実装される必要がある。
        /// </summary>
        internal const string CONTROLLER_HANDLER_IMPLEMENTATION = "CommandQueryControllerHandler";
        /// <summary>
        /// 自動生成される Controller が保持するハンドラーのフィールド名。
        /// </summary>
        internal const string CONTROLLER_HANDLER_FIELD = "_autoGeneratedControllerHandler";
        /// <summary>
        /// 自動生成される Action が利用するメソッド名。
        /// 実体は手動実装される必要がある。
        /// </summary>
        internal const string HANDLE_METHOD = "Handle";

        private const string API_SUBDOMAIN = "api";

        /// <summary>
        /// RootAttributeの引数に指定するルーティング定義
        /// </summary>
        public string Route => $"{API_SUBDOMAIN}/{_rootAggregate.LatinName.ToKebabCase()}";
        /// <summary>
        /// ASP.NET Core コントローラークラス名
        /// </summary>
        public string CsClassName => $"{_rootAggregate.PhysicalName}Controller";

        /// <summary>
        /// Webクライアント側からアクセスするときのAction名を返す。
        /// </summary>
        /// <param name="action">Action名単体</param>
        /// <returns>URLのドメイン部分を除いたサブドメイン部分</returns>
        public string GetActionNameForClient(string action) {
            return $"{Route}/{action}";
        }

        /// <summary>
        /// Controller 共通ハンドラーのインターフェース定義を生成する。
        /// </summary>
        internal class HandlerInterfaceFile : IMultiAggregateSourceFile {
            public void RegisterDependencies(IMultiAggregateSourceFileManager ctx) {
                // 特になし
            }

            public void Render(CodeRenderingContext ctx) {
                ctx.WebapiProject(dir => {
                    dir.Generate(new SourceFile {
                        FileName = $"{CONTROLLER_HANDLER_INTERFACE}.cs",
                        Contents = $$"""
                            using Microsoft.AspNetCore.Mvc;

                            namespace {{ctx.Config.RootNamespace}};

                            /// <summary>
                            /// HTTPリクエスト/レスポンスと自動生成されるControllerのアクションとをつなぐハンドラー。
                            /// このインターフェースを実装するクラスは、以下の機能を提供する必要がある。
                            ///
                            /// <list type="bullet">
                            ///   <item>HTTPリクエストおよびレスポンスとC#アプリケーションサービスの引数および戻り値の変換</item>
                            ///   <item>アクションの実行</item>
                            ///   <item>エラーハンドリングやHTTPログ出力といった横断的関心事の処理</item>
                            ///   <item>アプリケーション毎に特有のオプションがある場合はその処理</item>
                            /// </list>
                            ///
                            /// 逆に上記の機能が提供されていれば、詳細（以下、例）は自由に変更してよい。
                            ///
                            /// <list type="bullet">
                            ///   <item>引数や戻り値のJSONシリアライズ/デシリアライズの方法</item>
                            ///   <item>アクション実行前の認可処理の有無や内容</item>
                            ///   <item>成功時・失敗時のHTTPステータスコード</item>
                            ///   <item>エラー時のレスポンス内容</item>
                            ///   <item>Content-TypeやCORSなどのHTTPヘッダー</item>
                            /// </list>
                            /// </summary>
                            public interface {{CONTROLLER_HANDLER_INTERFACE}} {
                                /// <summary>
                                /// 自動生成されたアクションが利用するハンドラーメソッド。（引数なし、戻り値なし）
                                /// </summary>
                                Task<IActionResult> {{HANDLE_METHOD}}<TMessageRoot>(ControllerBase controllerBase, Func<IPresentationContext<TMessageRoot>, Task> appSrvMethod)
                                    where TMessageRoot : IMessageSetter;

                                /// <summary>
                                /// 自動生成されたアクションが利用するハンドラーメソッド。（引数なし、戻り値あり）
                                /// </summary>
                                Task<IActionResult> {{HANDLE_METHOD}}<TReturnValue, TMessageRoot>(ControllerBase controllerBase, Func<IPresentationContextWithReturnValue<TReturnValue, TMessageRoot>, Task> appSrvMethod)
                                    where TMessageRoot : IMessageSetter
                                    where TReturnValue : new();

                                /// <summary>
                                /// 自動生成されたアクションが利用するハンドラーメソッド。（引数あり、戻り値なし）
                                /// </summary>
                                Task<IActionResult> {{HANDLE_METHOD}}<TParameter, TMessageRoot>(ControllerBase controllerBase, Func<TParameter, IPresentationContext<TMessageRoot>, Task> appSrvMethod)
                                    where TMessageRoot : IMessageSetter;

                                /// <summary>
                                /// 自動生成されたアクションが利用するハンドラーメソッド。（引数あり、戻り値あり）
                                /// </summary>
                                Task<IActionResult> {{HANDLE_METHOD}}<TParameter, TReturnValue, TMessageRoot>(ControllerBase controllerBase, Func<TParameter, IPresentationContextWithReturnValue<TReturnValue, TMessageRoot>, Task> appSrvMethod)
                                    where TMessageRoot : IMessageSetter
                                    where TReturnValue : new();
                            }
                            """,
                    });
                });
            }
        }
    }
}
