using Microsoft.EntityFrameworkCore;
using Nijo.CodeGenerating;
using Nijo.ImmutableSchema;
using Nijo.SchemaParsing;
using Nijo.Util.DotnetEx;
using System;
using System.Collections.Generic;
using System.Linq;

namespace Nijo.Parts.CSharp;

/// <summary>
/// 汎用参照テーブルのカテゴリごとのビューに対応するEFCoreエンティティ。
/// ハードコードされる主キー列はビューのWHERE句で絞り込まれるため、このエンティティには含まれない。
/// </summary>
internal class GenericLookupViewEntity : IEFCoreEntity, IInstancePropertyOwnerMetadata {

    internal GenericLookupViewEntity(RootAggregate rootAggregate, GenericLookupTableParser.GenericLookupTableCategory category) {
        Aggregate = rootAggregate;
        Category = category;
    }

    public AggregateBase Aggregate { get; }
    internal GenericLookupTableParser.GenericLookupTableCategory Category { get; }
    private string CategoryIdentifier => Category.DisplayName.ToCSharpSafe();

    /// <summary>C#クラス名。例: "区分マスタ_CountriesDbEntity"</summary>
    public string CsClassName => $"{Aggregate.PhysicalName}_{CategoryIdentifier}DbEntity";
    /// <summary>DbSet名。例: "区分マスタ_CountriesDbSet"</summary>
    public string DbSetName => $"{Aggregate.PhysicalName}_{CategoryIdentifier}DbSet";
    /// <summary>OnModelCreatingメソッド名。例: "OnModelCreating区分マスタ_Countries"</summary>
    public string OnModelCreatingAutoGenerated => $"OnModelCreating{Aggregate.PhysicalName}_{CategoryIdentifier}";
    /// <summary>ビューのDB上の物理名。例: "KBN_MST_Countries"</summary>
    internal string ViewDbName => $"{Aggregate.DbName}_{Category.Name}";

    /// <summary>ビューにはタイムスタンプ列は不要</summary>
    public bool HasTimestampColumns => false;
    /// <summary>ビューにはバージョン列は不要</summary>
    public bool HasVersionColumn => false;

    /// <summary>
    /// ビューに含まれるカラムを列挙します。
    /// ハードコードされる主キー列は除外されます。
    /// </summary>
    public IEnumerable<EFCoreEntity.EFCoreEntityColumn> GetColumns() {
        var hardcodedUniqueIds = Category.HardCodedKeys
            .Select(k => k.UniqueId)
            .ToHashSet(StringComparer.Ordinal);

        foreach (var member in Aggregate.GetMembers()) {
            if (member is ValueMember vm) {
                var uid = vm.XElement.Attribute(SchemaParseContext.ATTR_UNIQUE_ID)?.Value;
                // ハードコードされる主キーはビューのWHERE句で固定されるため除外する
                if (uid != null && hardcodedUniqueIds.Contains(uid)) continue;
                yield return new EFCoreEntity.OwnColumnMember(vm);
            }
        }
    }

    /// <summary>ビューエンティティはナビゲーションプロパティを持ちません</summary>
    public IEnumerable<NavigationProperty> GetNavigationProperties() => [];

    IEnumerable<IInstancePropertyMetadata> IInstancePropertyOwnerMetadata.GetMembers() => GetColumns();

    /// <summary>
    /// このビューエンティティのC#クラス定義とDbContext partial class内のOnModelCreatingメソッドをレンダリングします。
    /// </summary>
    internal static string RenderClassDeclaring(GenericLookupViewEntity viewEntity, CodeRenderingContext ctx) {
        var columns = viewEntity.GetColumns().ToArray();
        var keys = columns.Where(c => c.IsKey).ToArray();

        return $$"""
            #region 汎用参照テーブル '{{viewEntity.Aggregate.DisplayName}}' のカテゴリ '{{viewEntity.Category.DisplayName}}' ビューエンティティ
            /// <summary>
            /// 汎用参照テーブル '{{viewEntity.Aggregate.DisplayName}}' のカテゴリ '{{viewEntity.Category.DisplayName}}' に対応するビューのEntity Framework Coreエンティティ型。
            /// ビュー名: {{viewEntity.ViewDbName}}
            /// </summary>
            public partial class {{viewEntity.CsClassName}} {
            {{columns.SelectTextTemplate(col => $$"""
                /// <summary>{{col.DisplayName}}</summary>
                public {{col.CsType}}? {{col.PhysicalName}} { get; set; }
            """)}}
            }

            partial class {{ctx.Config.DbContextName}} {
                {{WithIndent(RenderOnModelCreating(viewEntity, ctx))}}
            }
            #endregion 汎用参照テーブル '{{viewEntity.Aggregate.DisplayName}}' のカテゴリ '{{viewEntity.Category.DisplayName}}' ビューエンティティ
            """;
    }

    private static string RenderOnModelCreating(GenericLookupViewEntity viewEntity, CodeRenderingContext ctx) {
        var columns = viewEntity.GetColumns().ToArray();
        var keys = columns.Where(c => c.IsKey).ToArray();

        return $$"""
            /// <summary>
            /// '{{viewEntity.Category.DisplayName}}' ビューのテーブル詳細を定義します。
            /// ビュー名: {{viewEntity.ViewDbName}}
            /// </summary>
            protected virtual void {{viewEntity.OnModelCreatingAutoGenerated}}(ModelBuilder modelBuilder) {
                modelBuilder.Entity<{{viewEntity.CsClassName}}>(entity => {
                    entity.ToView("{{viewEntity.ViewDbName}}");
            {{If(keys.Length > 0, () => $$"""
                    entity.HasKey(e => new {
            {{keys.SelectTextTemplate(col => $$"""
                        e.{{col.PhysicalName}},
            """)}}
                    })
                    .HasName("PK_VIEW_{{viewEntity.ViewDbName}}");
            """).Else(() => $$"""
                    entity.HasNoKey();
            """)}}
            {{columns.SelectTextTemplate((col, ix) => $$"""
                    entity.Property(e => e.{{col.PhysicalName}})
                        .HasColumnName("{{col.DbName}}")
            {{If(col.Member?.TotalDigit != null, () => $$"""
                        .HasPrecision({{col.Member!.TotalDigit}}, {{col.Member.DecimalPlace ?? 0}})
            """)}}
            {{If(col.Member?.MaxLength != null, () => $$"""
                        .HasMaxLength({{col.Member!.MaxLength}})
            """)}}
                        .IsRequired({{(col.IsNotNull ? "true" : "false")}})
                        .HasColumnOrder({{ix}});
            """)}}
                });
            }
            """;
    }
}
