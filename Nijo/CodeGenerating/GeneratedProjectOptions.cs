using Nijo.Parts.CSharp;
using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Linq;
using System.Reflection;
using System.Text;
using System.Text.Json.Nodes;
using System.Text.Json.Serialization;
using System.Threading.Tasks;
using System.Xml;
using System.Xml.Linq;

namespace Nijo.CodeGenerating {
    /// <summary>
    /// スキーマ定義で設定できるアプリケーション単位のソースコードレンダリング設定。
    /// publicなプロパティにはDescription属性をつけ、そのプロパティの役割を1行で記述すること。
    /// ここでつけられた文言はnijo.xml記載ルールのドキュメントにそのまま表れる。
    /// </summary>
    public class GeneratedProjectOptions {

        internal const string ROOT_ELEMENT_NAME = "NijoAppScaffold";

        /// <summary>
        /// デフォルト値を取得するためのコンストラクタ
        /// </summary>
        private GeneratedProjectOptions() {
        }

        /// <summary>
        /// XMLドキュメントからプロジェクト設定を解析する。
        /// </summary>
        /// <param name="useDefaultValues">未指定の項目にデフォルト値を使用するかどうか</param>
        internal static GeneratedProjectOptions Parse(XDocument? xDocument, bool useDefaultValues) {
            if (useDefaultValues) {
                return new GeneratedProjectOptions {
                    ApplicationName = xDocument?.Root?.Attribute(nameof(ApplicationName))?.Value ?? xDocument?.Root?.Name.LocalName ?? "AutoGeneratedApplication",
                    RootNamespace = xDocument?.Root?.Attribute(nameof(RootNamespace))?.Value ?? "Nijo",
                    DbContextName = xDocument?.Root?.Attribute(nameof(DbContextName))?.Value ?? "MyDbContext",
                    CreateUserDbColumnName = xDocument?.Root?.Attribute(nameof(CreateUserDbColumnName))?.Value ?? EFCoreEntity.CREATE_USER,
                    UpdateUserDbColumnName = xDocument?.Root?.Attribute(nameof(UpdateUserDbColumnName))?.Value ?? EFCoreEntity.UPDATE_USER,
                    CreatedAtDbColumnName = xDocument?.Root?.Attribute(nameof(CreatedAtDbColumnName))?.Value ?? EFCoreEntity.CREATED_AT,
                    UpdatedAtDbColumnName = xDocument?.Root?.Attribute(nameof(UpdatedAtDbColumnName))?.Value ?? EFCoreEntity.UPDATED_AT,
                    VersionDbColumnName = xDocument?.Root?.Attribute(nameof(VersionDbColumnName))?.Value ?? EFCoreEntity.VERSION,
                    GenerateRefToChildrenDisplayData = xDocument?.Root?.Attribute(nameof(GenerateRefToChildrenDisplayData))?.Value == "true",
                    ReactDebuggingUrl = xDocument?.Root?.Attribute(nameof(ReactDebuggingUrl))?.Value ?? "http://localhost:5173",
                    DotnetDebuggingUrl = xDocument?.Root?.Attribute(nameof(DotnetDebuggingUrl))?.Value ?? "https://localhost:7098",
                    CoreLibraryFolderName = xDocument?.Root?.Attribute(nameof(CoreLibraryFolderName))?.Value ?? "Core.AutoGenerated",
                    WebapiProjectFolderName = xDocument?.Root?.Attribute(nameof(WebapiProjectFolderName))?.Value ?? "WebApi",
                    ReactProjectFolderName = xDocument?.Root?.Attribute(nameof(ReactProjectFolderName))?.Value ?? "client/src",
                    UnitTestProjectFolderName = xDocument?.Root?.Attribute(nameof(UnitTestProjectFolderName))?.Value ?? "UnitTest"
                };
            } else {
                return new GeneratedProjectOptions {
                    ApplicationName = xDocument?.Root?.Attribute(nameof(ApplicationName))?.Value ?? "",
                    RootNamespace = xDocument?.Root?.Attribute(nameof(RootNamespace))?.Value ?? "",
                    DbContextName = xDocument?.Root?.Attribute(nameof(DbContextName))?.Value ?? "",
                    CreateUserDbColumnName = xDocument?.Root?.Attribute(nameof(CreateUserDbColumnName))?.Value ?? "",
                    UpdateUserDbColumnName = xDocument?.Root?.Attribute(nameof(UpdateUserDbColumnName))?.Value ?? "",
                    CreatedAtDbColumnName = xDocument?.Root?.Attribute(nameof(CreatedAtDbColumnName))?.Value ?? "",
                    UpdatedAtDbColumnName = xDocument?.Root?.Attribute(nameof(UpdatedAtDbColumnName))?.Value ?? "",
                    VersionDbColumnName = xDocument?.Root?.Attribute(nameof(VersionDbColumnName))?.Value ?? "",
                    GenerateRefToChildrenDisplayData = xDocument?.Root?.Attribute(nameof(GenerateRefToChildrenDisplayData))?.Value == "true",
                    ReactDebuggingUrl = xDocument?.Root?.Attribute(nameof(ReactDebuggingUrl))?.Value ?? "",
                    DotnetDebuggingUrl = xDocument?.Root?.Attribute(nameof(DotnetDebuggingUrl))?.Value ?? "",
                    CoreLibraryFolderName = xDocument?.Root?.Attribute(nameof(CoreLibraryFolderName))?.Value ?? "",
                    WebapiProjectFolderName = xDocument?.Root?.Attribute(nameof(WebapiProjectFolderName))?.Value ?? "",
                    ReactProjectFolderName = xDocument?.Root?.Attribute(nameof(ReactProjectFolderName))?.Value ?? "",
                    UnitTestProjectFolderName = xDocument?.Root?.Attribute(nameof(UnitTestProjectFolderName))?.Value ?? ""
                };
            }
        }

        /// <summary>
        /// ソースコード自動生成されたあとのアプリケーションの名前
        /// </summary>
        [Description("【省略可能】ソースコード自動生成されたあとのアプリケーションの表示用名称。未指定の場合はこのXML要素がアプリケーション名になる。")]
        public required string ApplicationName { get; init; }

        /// <summary>
        /// C#側ソースコードの名前空間
        /// </summary>
        [Description("【省略可能】C#側ソースコードの名前空間。未指定の場合は\"Nijo\"という名前になる")]
        public required string RootNamespace { get; init; }
        /// <summary>
        /// DBコンテキストのクラス名
        /// </summary>
        [Description("【省略可能】Entity Framework Core の DbContextのクラス名")]
        public required string DbContextName { get; init; }

        /// <summary>DBカラム名（作成者）</summary>
        [Description($"【省略可能】{nameof(Models.DataModel)}から生成されるRDBMS上の各テーブルに付される、そのレコードの新規登録者を表すカラムの物理名")]
        public required string CreateUserDbColumnName { get; init; }
        /// <summary>DBカラム名（更新者）</summary>
        [Description($"【省略可能】{nameof(Models.DataModel)}から生成されるRDBMS上の各テーブルに付される、そのレコードの最終更新者を表すカラムの物理名")]
        public required string UpdateUserDbColumnName { get; init; }
        /// <summary>DBカラム名（作成時刻）</summary>
        [Description($"【省略可能】{nameof(Models.DataModel)}から生成されるRDBMS上の各テーブルに付される、そのレコードの新規登録時刻を表すカラムの物理名")]
        public required string CreatedAtDbColumnName { get; init; }
        /// <summary>DBカラム名（更新時刻）</summary>
        [Description($"【省略可能】 {nameof(Models.DataModel)}から生成されるRDBMS上の各テーブルに付される、そのレコードの最終更新時刻を表すカラムの物理名")]
        public required string UpdatedAtDbColumnName { get; init; }
        /// <summary>DBカラム名（楽観排他用バージョン）</summary>
        [Description($"【省略可能】 {nameof(Models.DataModel)}から生成されるRDBMS上の集約ルートテーブルに付される、楽観排他制御用のバージョンのカラムの物理名")]
        public required string VersionDbColumnName { get; init; }

        /// <summary>参照先の子孫要素を含めたデータを表示するかどうか</summary>
        [Description(
            $"【省略可能】{nameof(Models.QueryModel)}から生成される画面表示用オブジェクトのうち、外部参照の、親と1対多の関係にある子孫要素を表示するかどうか。" +
            $"SQLが複雑になりすぎて性能に問題が出たり、EFCoreがそのようなクエリを生成できなかったりするため、既定では表示しないようにしている。")]
        public required bool GenerateRefToChildrenDisplayData { get; init; }

        /// <summary>
        /// 開発環境におけるReact.jsのサーバーが展開されるURL。httpから始める必要あり。
        /// ASP.NET Core サーバーでのCORS設定に必要。
        /// </summary>
        [Description($"【廃止予定】開発環境におけるReact.jsのサーバーが展開されるURL。ASP.NET Core サーバーでのCORS設定に必要")]
        public required string ReactDebuggingUrl { get; init; }
        /// <summary>
        /// 開発環境におけるASP.NET Coreのサーバーが展開されるURL。httpsから始める必要あり。
        /// 開発環境でReact.jsがバックエンド側処理を呼ぶのに必要。
        /// </summary>
        [Description($"【廃止予定】開発環境におけるASP.NET Coreのサーバーが展開されるURL。React.jsからのバックエンド呼び出し必要")]
        public required string DotnetDebuggingUrl { get; init; }

        /// <summary>コアライブラリの自動生成フォルダ名</summary>
        [Description("【省略可能】コアライブラリの自動生成フォルダ名。未指定の場合は\"Core.AutoGenerated\"")]
        public required string CoreLibraryFolderName { get; init; }

        /// <summary>WebAPIプロジェクトフォルダ名</summary>
        [Description("【省略可能】WebAPIプロジェクトフォルダ名。未指定の場合は\"WebApi\"")]
        public required string WebapiProjectFolderName { get; init; }

        /// <summary>Reactプロジェクトフォルダ名</summary>
        [Description("【省略可能】Reactプロジェクトフォルダ名。未指定の場合は\"client/src\"")]
        public required string ReactProjectFolderName { get; init; }

        /// <summary>単体テストプロジェクトフォルダ名</summary>
        [Description("【省略可能】単体テストプロジェクトフォルダ名。未指定の場合は\"UnitTest\"")]
        public required string UnitTestProjectFolderName { get; init; }

        /// <summary>
        /// このクラスのプロパティ定義をメタ情報として取得する
        /// </summary>
        internal static IEnumerable<ProjectOptionPropertyInfo> GetPropertyInfos() {
            var properties = typeof(GeneratedProjectOptions).GetProperties(BindingFlags.Public | BindingFlags.Instance);
            var defaultInstance = Parse(null, true);

            foreach (var prop in properties) {
                var descriptionAttr = prop.GetCustomAttribute<DescriptionAttribute>();
                yield return new ProjectOptionPropertyInfo {
                    PropertyName = prop.Name,
                    Description = descriptionAttr?.Description ?? prop.Name,
                    PropertyType = GetPropertyTypeString(prop.PropertyType),
                    DefaultValue = prop.GetValue(defaultInstance),
                };
            }
        }

        private static string GetPropertyTypeString(Type type) {
            if (type == typeof(string)) return "string";
            if (type == typeof(bool)) return "bool";
            if (type == typeof(int)) return "int";
            return "string";
        }
        /// <summary>
        /// プロジェクト設定の値を辞書として取得する
        /// </summary>
        internal JsonObject GetCurrentValues() {
            var result = new JsonObject();
            var properties = typeof(GeneratedProjectOptions).GetProperties(BindingFlags.Public | BindingFlags.Instance);

            foreach (var prop in properties) {
                var value = prop.GetValue(this);
                result[prop.Name] = value switch {
                    string str => str,
                    bool b => b,
                    int i => i,
                    _ => null,
                };
            }

            return result;
        }
    }

    /// <summary>
    /// プロジェクト設定項目のメタ情報
    /// </summary>
    public class ProjectOptionPropertyInfo {
        [JsonPropertyName("propertyName")]
        public required string PropertyName { get; set; }
        [JsonPropertyName("description")]
        public required string Description { get; set; }
        [JsonPropertyName("propertyType")]
        public required string PropertyType { get; set; }
        [JsonPropertyName("defaultValue")]
        public object? DefaultValue { get; set; }
    }
}
