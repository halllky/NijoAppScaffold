using Microsoft.EntityFrameworkCore;
using Nijo.CodeGenerating;
using Nijo.ImmutableSchema;
using Nijo.Models;
using Nijo.Models.DataModelModules;
using Nijo.Parts.CSharp;
using Nijo.SchemaParsing;
using Nijo.Util.DotnetEx;
using System;
using System.Collections.Generic;
using System.Linq;

namespace Nijo.Models.WriteModel2Modules {
    /// <summary>
    /// 旧版互換の EF Core Entity メタデータ。
    /// 生成後の Entity クラスと 1:1 対応する metadata owner として振る舞う。
    /// </summary>
    internal class LegacyEFCoreEntity : IInstancePropertyOwnerMetadata, IEFCoreEntity {
        internal LegacyEFCoreEntity(AggregateBase aggregate) {
            Aggregate = aggregate;
        }

        public AggregateBase Aggregate { get; }
        public string CsClassName => $"{Aggregate.PhysicalName}DbEntity";
        public string DbSetName => $"{Aggregate.PhysicalName}DbSet";
        public string OnModelCreatingAutoGenerated => $"OnModelCreating{Aggregate.PhysicalName}";
        public bool HasTimestampColumns => true;
        public bool HasVersionColumn => Aggregate is RootAggregate;

        IEnumerable<IInstancePropertyMetadata> IInstancePropertyOwnerMetadata.GetMembers() => GetOwnMembers();

        public IEnumerable<Nijo.Parts.CSharp.EFCoreEntity.EFCoreEntityColumn> GetColumns() {
            return GetOwnMembers().OfType<Nijo.Parts.CSharp.EFCoreEntity.EFCoreEntityColumn>();
        }

        private IEnumerable<Nijo.Parts.CSharp.EFCoreEntity.EFCoreEntityColumn> GetColumnsForOnModelCreating() {
            var parent = Aggregate.GetParent();
            if (parent != null) {
                foreach (var parentKey in new LegacyEFCoreEntity(parent).GetColumns().Where(col => col.IsKey)) {
                    if (parentKey.Member == null) continue;

                    yield return new Nijo.Parts.CSharp.EFCoreEntity.ParentKeyMember(
                        parentKey.Member,
                        $"PARENT_{parentKey.PhysicalName}",
                        $"PARENT_{parentKey.DbName}");
                }
            }

            foreach (var member in Aggregate.GetMembers()) {
                switch (member) {
                    case RefToMember refTo:
                        foreach (var refKey in EnumerateRefKeyMembers(refTo)) {
                            yield return refKey;
                        }
                        break;
                    case ValueMember vm:
                        yield return new LegacyOwnColumnMember(vm);
                        break;
                }
            }
        }

        public IEnumerable<Nijo.Parts.CSharp.NavigationProperty> GetNavigationProperties() {
            var parent = Aggregate.GetParent();
            if (parent != null) {
                yield return new Nijo.Parts.CSharp.NavigationProperty.NavigationOfParentChild(parent, Aggregate);
            }

            foreach (var member in Aggregate.GetMembers()) {
                if (member is ChildAggregate child) {
                    yield return new Nijo.Parts.CSharp.NavigationProperty.NavigationOfParentChild(Aggregate, child);
                } else if (member is ChildrenAggregate children) {
                    yield return new Nijo.Parts.CSharp.NavigationProperty.NavigationOfParentChild(Aggregate, children);
                } else if (member is RefToMember refTo) {
                    yield return new Nijo.Parts.CSharp.NavigationProperty.NavigationOfRef(refTo);
                }
            }

            foreach (var refFrom in Aggregate.GetRefFroms()) {
                var refFromRoot = refFrom.Owner.GetRoot();
                var hasDbEntity = refFromRoot.Model is DataModel
                               || refFromRoot.Model is Models.WriteModel2
                               || refFromRoot.Model is QueryModel && refFromRoot.IsView;
                if (hasDbEntity) {
                    yield return new Nijo.Parts.CSharp.NavigationProperty.NavigationOfRef(refFrom);
                }
            }
        }

        internal static string RenderClassDeclaring(LegacyEFCoreEntity rootEntity, CodeRenderingContext ctx) {
            return $$"""
                {{rootEntity.RenderSingleClassDeclaring()}}

                partial class DefaultConfiguration {
                    {{WithIndent(rootEntity.RenderOnModelCreating(ctx), "    ")}}
                }
                """;
        }

        private IEnumerable<IInstancePropertyMetadata> GetOwnMembers() {
            var parent = Aggregate.GetParent();
            if (parent != null) {
                foreach (var parentKey in new LegacyEFCoreEntity(parent).GetColumns().Where(col => col.IsKey)) {
                    if (parentKey.Member == null) continue;

                    yield return new Nijo.Parts.CSharp.EFCoreEntity.ParentKeyMember(
                        parentKey.Member,
                        $"PARENT_{parentKey.PhysicalName}",
                        $"PARENT_{parentKey.DbName}");
                }
            }

            var refKeyGroups = Aggregate.GetMembers()
                .OfType<RefToMember>()
                .OrderBy(refTo => GenericLookupRefToInfo.TryCreate(refTo, out _) ? 0 : 1)
                .ThenBy(refTo => refTo.Order)
                .Select(refTo => EnumerateRefKeyMembers(refTo).ToArray())
                .ToArray();
            var refKeyCount = refKeyGroups.Select(group => group.Length).DefaultIfEmpty(0).Max();

            for (var index = 0; index < refKeyCount; index++) {
                foreach (var group in refKeyGroups) {
                    if (index < group.Length) {
                        yield return group[index];
                    }
                }
            }

            foreach (var member in Aggregate.GetMembers()) {
                if (member is ValueMember vm) {
                    yield return new LegacyOwnColumnMember(vm);
                }
            }

            if (parent != null) {
                yield return new ParentNavigationMember(parent);
            }

            foreach (var member in Aggregate.GetMembers()) {
                switch (member) {
                    case RefToMember refTo:
                        yield return new RefToNavigationMember(refTo);
                        break;
                    case ChildAggregate child:
                        yield return new ChildNavigationMember(child);
                        break;
                    case ChildrenAggregate children:
                        yield return new ChildrenNavigationMember(children);
                        break;
                }
            }

            foreach (var refFrom in Aggregate.GetRefFroms()) {
                var refFromRoot = refFrom.Owner.GetRoot();
                var hasDbEntity = refFromRoot.Model is DataModel
                               || refFromRoot.Model is Models.WriteModel2
                               || refFromRoot.Model is QueryModel && refFromRoot.IsView;
                if (hasDbEntity) {
                    yield return new RefFromNavigationMember(refFrom);
                }
            }
        }

        private IEnumerable<Nijo.Parts.CSharp.EFCoreEntity.RefKeyMember> EnumerateRefKeyMembers(RefToMember refTo) {
            var genericLookupRef = GenericLookupRefToInfo.TryCreate(refTo, out var info) ? info : null;

            foreach (var targetKey in new LegacyEFCoreEntity(refTo.RefTo).GetColumns().Where(col => col.IsKey)) {
                if (targetKey.Member == null) continue;
                if (genericLookupRef != null && genericLookupRef.IsHardCoded(targetKey.Member)) continue;

                var isParentKey = targetKey is Nijo.Parts.CSharp.EFCoreEntity.ParentKeyMember
                               || targetKey is Nijo.Parts.CSharp.EFCoreEntity.RefKeyMember refKey && refKey.IsParentKey;

                yield return new Nijo.Parts.CSharp.EFCoreEntity.RefKeyMember(
                    refTo,
                    targetKey.Member,
                    $"{refTo.PhysicalName}_{targetKey.PhysicalName}",
                    $"{refTo.PhysicalName}_{targetKey.DbName}",
                    isParentKey);
            }
        }

        private sealed class LegacyOwnColumnMember : Nijo.Parts.CSharp.EFCoreEntity.EFCoreEntityColumn {
            internal LegacyOwnColumnMember(ValueMember member) {
                Member = member;
            }

            internal override ValueMember Member { get; }
            internal override IValueMemberType MemberType => Member.Type;
            internal override string CsType => GetLegacyCsType(Member);
            internal override string PhysicalName => Member.PhysicalName;
            internal override string DisplayName => Member.DisplayName;
            internal override string DbName => Member.DbName;
            internal override bool IsKey => Member.IsKey;
            internal override bool IsNotNull => Member.IsKey || Member.IsNotNull;
        }

        private static string GetLegacyCsType(ValueMember member) {
            var originalType = member.XElement.Annotation<SchemaParseContext.OriginalTypeAnnotation>()?.TypeName;
            if (originalType == "file") {
                return "List<FileAttachmentMetadata>";
            }

            return member.Type.CsDomainTypeName.Replace("DateOnly", "Date");
        }

        private string RenderSingleClassDeclaring() {
            var instance = new Variable("entity", this);
            var members = instance.CreateProperties().ToArray();
            var columns = members
                .OfType<InstanceValueProperty>()
                .Where(prop => prop.Metadata is Nijo.Parts.CSharp.EFCoreEntity.EFCoreEntityColumn)
                .ToArray();
            var navigations = members
                .OfType<InstanceStructureProperty>()
                .Where(prop => prop.Metadata is LegacyNavigationMember)
                .ToArray();
            var keyColumns = columns
                .Where(prop => ((Nijo.Parts.CSharp.EFCoreEntity.EFCoreEntityColumn)prop.Metadata).IsKey)
                .ToArray();

            return $$"""
                /// <summary>
                /// Entity Framework Core のルールに則った{{Aggregate.DisplayName}}のデータ型
                /// </summary>
                public partial class {{CsClassName}} {
                {{WithIndent(columns.SelectTextTemplate(prop => {
                var col = (Nijo.Parts.CSharp.EFCoreEntity.EFCoreEntityColumn)prop.Metadata;
                return $$"""
                        public {{col.CsType}}? {{col.PhysicalName}} { get; set; }
                    """;
            }), "")}}
                {{If(HasVersionColumn, () => $$"""
                    /// <summary>楽観排他制御用のバージョニング用カラム</summary>
                    public int? {{Nijo.Parts.CSharp.EFCoreEntity.VERSION}} { get; set; }
                """)}}
                    /// <summary>データが新規作成された日時</summary>
                    public DateTime? {{Nijo.Parts.CSharp.EFCoreEntity.CREATED_AT}} { get; set; }
                    /// <summary>データが更新された日時</summary>
                    public DateTime? {{Nijo.Parts.CSharp.EFCoreEntity.UPDATED_AT}} { get; set; }
                    /// <summary>データを新規作成したユーザー</summary>
                    public string? {{Nijo.Parts.CSharp.EFCoreEntity.CREATE_USER}} { get; set; }
                    /// <summary>データを更新したユーザー</summary>
                    public string? {{Nijo.Parts.CSharp.EFCoreEntity.UPDATE_USER}} { get; set; }

                {{WithIndent(navigations.SelectTextTemplate(prop => {
                var nav = (LegacyNavigationMember)prop.Metadata;
                var csType = nav.IsArray
                    ? $"ICollection<{nav.GetTypeName(E_CsTs.CSharp)}>"
                    : $"{nav.GetTypeName(E_CsTs.CSharp)}?";
                var initializer = nav.IsArray
                    ? $" = new HashSet<{nav.GetTypeName(E_CsTs.CSharp)}>();"
                    : string.Empty;
                return $$"""
                        public virtual {{csType}} {{nav.GetPropertyName(E_CsTs.CSharp)}} { get; set; }{{initializer}}
                    """;
            }), "")}}

                    /// <summary>このオブジェクトと比較対象のオブジェクトの主キーが一致するかを返します。</summary>
                    public bool KeyEquals({{CsClassName}} entity) {
                {{keyColumns.SelectTextTemplate(prop => {
                var col = (Nijo.Parts.CSharp.EFCoreEntity.EFCoreEntityColumn)prop.Metadata;
                return $"        if (entity.{col.PhysicalName} != this.{col.PhysicalName}) return false;";
            })}}
                        return true;
                    }
                    /// <summary>
                    /// このオブジェクトと引数のオブジェクトに、何らかの差分があるかどうかを調べます。
                    /// 登録日時等はこのメソッドの判定から除外されます。
                    /// </summary>
                    public bool HasChanges({{CsClassName}} entity) {
                {{columns.SelectTextTemplate(prop => {
                var col = (Nijo.Parts.CSharp.EFCoreEntity.EFCoreEntityColumn)prop.Metadata;
                return $"        if (this.{col.PhysicalName} != entity.{col.PhysicalName}) return true;";
            })}}
                        return false;
                    }
                }
                """;
        }

        private string RenderOnModelCreating(CodeRenderingContext ctx) {
            var columns = GetColumnsForOnModelCreating()
                .OrderByDescending(col => col.IsKey)
                .ToArray();
            var keyColumns = columns.Where(col => col.IsKey).ToArray();
            var sequences = Aggregate
                .GetMembers()
                .OfType<ValueMember>()
                .Concat(Aggregate
                    .GetMembers()
                    .OfType<RefToMember>()
                    .SelectMany(refTo => EnumerateRefKeyMembers(refTo))
                    .Select(refKey => refKey.Member)
                    .Where(member => member != null)
                    .Cast<ValueMember>())
                .Where(vm => vm.Type is ValueMemberTypes.SequenceMember && !string.IsNullOrWhiteSpace(vm.SequenceName))
                .DistinctBy(vm => vm.SequenceName)
                .ToArray();

            var columnOrder = 0;
            var columnDefinitions = new List<string>();
            foreach (var col in columns) {
                var valueGeneratedNever = (col.CsType == "int" || col.CsType == "long") && col.IsKey;

                var lines = new List<string> {
                    $"entity.Property(e => e.{col.PhysicalName})",
                    $"    .HasColumnName(\"{col.DbName.Replace("\"", "\\\"")}\")",
                    $"    .HasComment(\"{col.PhysicalName.Replace("\"", "\\\"")}\")",
                };
                if (col.Member?.TotalDigit != null) {
                    lines.Add($"    .HasPrecision({col.Member.TotalDigit}, {col.Member.DecimalPlace?.ToString() ?? "0"})");
                }
                if (valueGeneratedNever) {
                    lines.Add("    .ValueGeneratedNever() // 暗黙的シーケンスの生成をオフにする");
                }
                if (col.Member?.MaxLength != null) {
                    lines.Add($"    .HasMaxLength({col.Member.MaxLength})");
                }
                lines.Add($"    .IsRequired({(col.IsKey || col.IsNotNull ? "true" : "false")})");
                lines.Add($"    .HasColumnOrder({columnOrder++});");
                columnDefinitions.Add(string.Join(Environment.NewLine, lines));
            }

            return $$"""
                /// <summary>
                /// テーブルやカラムの詳細を定義します。
                /// 参考: "Fluent API" （Entity FrameWork Core の仕組み）
                /// </summary>
                public void {{OnModelCreatingAutoGenerated}}({{ctx.Config.DbContextName}} dbContext, ModelBuilder modelBuilder) {
                {{If(sequences.Length > 0, () => $$"""
                    // シーケンスを作成
                """)}}
                {{sequences.SelectTextTemplate(s => $$"""
                    modelBuilder.HasSequence<int>("{{s.SequenceName!.Replace("\"", "\\\"")}}")
                        .StartsAt(1)
                        .IncrementsBy(1);
                """)}}
                    modelBuilder.Entity<{{ctx.Config.RootNamespace}}.{{CsClassName}}>(entity => {

                        entity.ToTable("{{Aggregate.DbName.Replace("\"", "\\\"")}}")
                        .HasComment("{{Aggregate.DisplayName.Replace("\"", "\\\"")}}");

                        entity.HasKey(e => new {
                {{keyColumns.SelectTextTemplate(col => $$"""
                            e.{{col.PhysicalName}},
                """)}}
                        })
                        .HasName("PK_{{GetConstraintToken(Aggregate)}}");

                        {{WithIndent(columnDefinitions, "        ")}}
                {{RenderChildNavigationsOnModelCreating().SelectTextTemplate(source => $"        {WithIndent(source, "        ")}{Environment.NewLine}")}}
                        entity.Property(e => e.{{Nijo.Parts.CSharp.EFCoreEntity.CREATED_AT}})
                            .HasColumnName("{{ctx.Config.CreatedAtDbColumnName.Replace("\"", "\\\"")}}")
                            .HasColumnOrder({{columnOrder++}});
                        entity.Property(e => e.{{Nijo.Parts.CSharp.EFCoreEntity.CREATE_USER}})
                            .HasColumnName("{{ctx.Config.CreateUserDbColumnName.Replace("\"", "\\\"")}}")
                            .HasColumnOrder({{columnOrder++}});
                        entity.Property(e => e.{{Nijo.Parts.CSharp.EFCoreEntity.UPDATED_AT}})
                            .HasColumnName("{{ctx.Config.UpdatedAtDbColumnName.Replace("\"", "\\\"")}}")
                            .HasColumnOrder({{columnOrder++}});
                        entity.Property(e => e.{{Nijo.Parts.CSharp.EFCoreEntity.UPDATE_USER}})
                            .HasColumnName("{{ctx.Config.UpdateUserDbColumnName.Replace("\"", "\\\"")}}")
                            .HasColumnOrder({{columnOrder++}});
                {{If(HasVersionColumn && !string.IsNullOrEmpty(ctx.Config.VersionDbColumnName), () => $$"""
                        entity.Property(e => e.{{Nijo.Parts.CSharp.EFCoreEntity.VERSION}})
                            .HasColumnName("{{ctx.Config.VersionDbColumnName.Replace("\"", "\\\"")}}")
                            .IsRequired(true)
                            .IsConcurrencyToken(true)
                            .HasColumnOrder({{columnOrder++}});
                """).ElseIf(HasVersionColumn, () => $$"""
                        entity.Property(e => e.{{Nijo.Parts.CSharp.EFCoreEntity.VERSION}})
                            .IsRequired(true)
                            .IsConcurrencyToken(true)
                            .HasColumnOrder({{columnOrder++}});
                """)}}

                        {{WithIndent(Aggregate.GetRefFroms().Where(refFrom => {
                var refFromRoot = refFrom.Owner.GetRoot();
                return refFromRoot.Model is DataModel
                    || refFromRoot.Model is Models.WriteModel2
                    || refFromRoot.Model is QueryModel && refFromRoot.IsView;
            }).Select(RenderRefFromOnModelCreating), "        ")}}

                        // このエンティティに対して、自動生成されない初期設定がある場合はこの中で設定される（インデックス、ユニーク制約、デフォルト値など）
                        OnModelCreating(entity);
                    });
                }

                /// <summary>
                /// 自動生成されない初期設定がある場合はこのメソッドをオーバーライドして設定してください。
                /// （インデックス、ユニーク制約、デフォルト値など）
                /// </summary>
                public virtual void OnModelCreating(Microsoft.EntityFrameworkCore.Metadata.Builders.EntityTypeBuilder<{{CsClassName}}> entity) {
                }
                """;
        }

        private IEnumerable<string> RenderChildNavigationsOnModelCreating() {
            foreach (var member in Aggregate.GetMembers()) {
                if (member is ChildAggregate child) {
                    yield return RenderParentChildOnModelCreating(child, isMany: false);
                } else if (member is ChildrenAggregate children) {
                    yield return RenderParentChildOnModelCreating(children, isMany: true);
                }
            }
        }

        private string RenderParentChildOnModelCreating(AggregateBase childAggregate, bool isMany) {
            var relationHash = childAggregate.PhysicalName.ToHashedString().ToUpperInvariant()[..8];
            var principalId = GetConstraintToken(Aggregate);
            var relevantId = GetConstraintToken(childAggregate);
            var foreignKeys = new LegacyEFCoreEntity(childAggregate)
                .GetColumns()
                .OfType<Nijo.Parts.CSharp.EFCoreEntity.ParentKeyMember>()
                .ToArray();

            return $$"""
                entity.{{(isMany ? "HasMany" : "HasOne")}}(e => e.{{childAggregate.PhysicalName}})
                    .WithOne(e => e.Parent)
                    .HasForeignKey{{(isMany ? string.Empty : $"<{childAggregate.PhysicalName}DbEntity>")}}(e => new {
                {{foreignKeys.SelectTextTemplate(fk => $$"""
                        e.{{fk.PhysicalName}},
                """)}}
                    })
                    .OnDelete(DeleteBehavior.Cascade)
                    .HasConstraintName("FK_{{principalId}}_{{relationHash}}_{{relevantId}}");
                """;
        }

        private static string GetRefFromPropertyName(RefToMember refFrom) {
            return $"RefferedBy_{refFrom.Owner.PhysicalName}DbEntity_{refFrom.PhysicalName}";
        }

        private static string RenderRefFromOnModelCreating(RefToMember refFrom) {
            var principalAggregate = refFrom.RefTo;
            var relevantAggregate = refFrom.Owner;
            var relationHash = refFrom.PhysicalName.ToHashedString().ToUpperInvariant()[..8];
            var principalId = GetConstraintToken(principalAggregate);
            var relevantId = GetConstraintToken(relevantAggregate);
            var foreignKeys = new LegacyEFCoreEntity(relevantAggregate)
                .GetColumns()
                .OfType<Nijo.Parts.CSharp.EFCoreEntity.RefKeyMember>()
                .Where(fk => fk.RefEntry == refFrom)
                .ToArray();

            return $$"""
                entity.HasMany(e => e.{{GetRefFromPropertyName(refFrom)}})
                    .WithOne(e => e.{{refFrom.PhysicalName}})
                    .HasForeignKey(e => new {
                {{foreignKeys.SelectTextTemplate(fk => $$"""
                        e.{{fk.PhysicalName}},
                """)}}
                    })
                    .OnDelete(DeleteBehavior.NoAction)
                    .HasConstraintName("FK_{{principalId}}_{{relationHash}}_{{relevantId}}");

                """;
        }

        private static string GetConstraintToken(AggregateBase aggregate) {
            var raw = aggregate.XElement.Attribute(SchemaParseContext.ATTR_UNIQUE_ID)?.Value;
            if (string.IsNullOrWhiteSpace(raw)) {
                raw = GetLegacyNodeIdPath(aggregate);
            }

            return raw.ToHashedString().ToUpperInvariant()[..8];
        }

        private static string GetLegacyNodeIdPath(AggregateBase aggregate) {
            return string.Concat(aggregate.EnumerateThisAndAncestors().Select(x => $"/{x.PhysicalName}"));
        }

        private abstract class LegacyNavigationMember : IInstanceStructurePropertyMetadata {
            protected LegacyNavigationMember(ISchemaPathNode schemaPathNode, AggregateBase targetAggregate, bool isArray) {
                SchemaPathNode = schemaPathNode;
                TargetAggregate = targetAggregate;
                IsArray = isArray;
            }

            protected AggregateBase TargetAggregate { get; }
            public ISchemaPathNode SchemaPathNode { get; }
            public bool IsArray { get; }
            public string GetTypeName(E_CsTs csts) => $"{TargetAggregate.PhysicalName}DbEntity";
            public abstract string GetPropertyName(E_CsTs csts);

            IEnumerable<IInstancePropertyMetadata> IInstancePropertyOwnerMetadata.GetMembers() {
                return new LegacyEFCoreEntity(TargetAggregate).GetColumns();
            }
        }

        private sealed class ParentNavigationMember : LegacyNavigationMember {
            internal ParentNavigationMember(AggregateBase parent) : base(parent, parent, false) {
            }

            public override string GetPropertyName(E_CsTs csts) => "Parent";
        }

        private sealed class ChildNavigationMember : LegacyNavigationMember {
            internal ChildNavigationMember(ChildAggregate child) : base(child, child, false) {
                Child = child;
            }

            private ChildAggregate Child { get; }
            public override string GetPropertyName(E_CsTs csts) => Child.PhysicalName;
        }

        private sealed class ChildrenNavigationMember : LegacyNavigationMember {
            internal ChildrenNavigationMember(ChildrenAggregate children) : base(children, children, true) {
                Children = children;
            }

            private ChildrenAggregate Children { get; }
            public override string GetPropertyName(E_CsTs csts) => Children.PhysicalName;
        }

        private sealed class RefToNavigationMember : LegacyNavigationMember {
            internal RefToNavigationMember(RefToMember refTo) : base(refTo, refTo.RefTo, false) {
                RefTo = refTo;
            }

            private RefToMember RefTo { get; }
            public override string GetPropertyName(E_CsTs csts) => RefTo.PhysicalName;
        }

        private sealed class RefFromNavigationMember : LegacyNavigationMember {
            internal RefFromNavigationMember(RefToMember refFrom) : base(refFrom, refFrom.Owner, IsArrayProperty(refFrom)) {
                RefFrom = refFrom;
            }

            private RefToMember RefFrom { get; }
            public override string GetPropertyName(E_CsTs csts) => GetRefFromPropertyName(RefFrom);

            private static bool IsArrayProperty(RefToMember refFrom) {
                if (!refFrom.IsKey) return true;
                return !refFrom.RefTo.IsSingleKeyOf(refFrom.Owner);
            }
        }
    }
