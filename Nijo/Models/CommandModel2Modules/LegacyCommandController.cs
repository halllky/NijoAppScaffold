using Nijo.CodeGenerating;
using Nijo.ImmutableSchema;
using System.Collections.Generic;
using System.Threading;

namespace Nijo.Models.CommandModel2Modules;

internal class LegacyCommandController : IMultiAggregateSourceFile {
    private readonly Lock _lock = new();
    private readonly HashSet<RootAggregate> _rootAggregates = [];

    internal void Register(RootAggregate rootAggregate) {
        lock (_lock) {
            _rootAggregates.Add(rootAggregate);
        }
    }

    public void RegisterDependencies(IMultiAggregateSourceFileManager ctx) {
    }

    public void Render(CodeRenderingContext ctx) {
        if (!ctx.IsLegacyCompatibilityMode()) return;
        if (Snapshot().Length == 0) return;

        ctx.WebapiProject(autoGenerated => {
            autoGenerated.Directory("Controller", dir => {
                dir.Generate(new SourceFile {
                    FileName = "Commands.cs",
                    Contents = $$"""
                        using System;
                        using System.Linq;
                        using Microsoft.AspNetCore.Mvc;
                        using System.Text.Json;
                        using System.Text.Json.Nodes;
                        using System.Text.Json.Serialization;

                        namespace {{ctx.Config.RootNamespace}};

                        /// <summary>
                        /// 各種コマンドの呼び出し用Webエンドポイントを提供する ASP.NET Core のコントローラー
                        /// </summary>
                        [ApiController]
                        [Route("api/command")]
                        public partial class AutoGeneratedCommandController : ControllerBase {
                            public AutoGeneratedCommandController(OverridedApplicationService applicationService) {
                                _applicationService = applicationService;
                            }
                            protected readonly OverridedApplicationService _applicationService;
                        }
                        """,
                });
            });
        });
    }

    private RootAggregate[] Snapshot() {
        lock (_lock) {
            return [.. _rootAggregates];
        }
    }
}
