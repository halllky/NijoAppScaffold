using Microsoft.EntityFrameworkCore;
using Microsoft.Extensions.Configuration;
using Microsoft.Extensions.DependencyInjection;

namespace MyApp;

partial class OverridedApplicationService {

    /// <summary>
    /// DIコンテナへのサービス登録を行う。
    /// 実際のアプリケーションの要件に応じて適宜修正してよい。
    /// </summary>
    /// <param name="services">DIコンテナ</param>
    /// <param name="basePath">実行時設定ファイルのベースパス（appsettings.jsonのあるフォルダ）</param>
    public static void ConfigureServices(IServiceCollection services, string basePath) {
        if (!Path.IsPathRooted(basePath)) throw new ArgumentException($"{nameof(basePath)}は絶対パスで指定してください。");

        // アプリケーションサービスをDIコンテナに登録
        services.AddScoped<OverridedApplicationService>();
        services.AddScoped<AutoGeneratedApplicationService>(sp => sp.GetRequiredService<OverridedApplicationService>());

        // 実行時設定ファイルの読み込み設定
        var myAppSection = new ConfigurationBuilder()
            .SetBasePath(basePath)
            .AddJsonFile("appsettings.json", false)
            .AddJsonFile("appsettings.Development.json", true) // 後にAddされたファイルが優先される
            .Build()
            .GetSection(RuntimeSetting.MY_APP_SECTION);
        var settings = new RuntimeSetting();
        myAppSection.Bind(settings);
        services.AddSingleton(settings);

        // DBコンテキスト登録
        services.AddScoped<AutoGeneratedDbContext, OverridedDbContext>();
        services.AddDbContext<OverridedDbContext>((services, options) => {

            // SQLiteを使用する
            var settings = services.GetRequiredService<RuntimeSetting>();
            var connStr = settings.GetCurrentProfile().ConnStr;
            options.UseSqlite(connStr, builder => {
                // マイグレーションファイルが生成されるアセンブリを指定する。
                // 既定では DbContext が宣言されているプロジェクトになる。
                builder.MigrationsAssembly(typeof(OverridedApplicationService).Assembly.FullName);
            });

            // 自動生成される登録更新処理は変更追跡オフが前提
            options.UseQueryTrackingBehavior(QueryTrackingBehavior.NoTracking);
        });

        // ログ設定
        services.AddLogging(logging => {
            // ログに関してはここで設定を行う
        });
    }
}
