using Microsoft.AspNetCore.Mvc;
using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.Net;
using System.Text.Json;
using System.Text.Json.Nodes;
using System.Threading.Tasks;
using MyApp.WebApi.Base;

// nijo.xml で指定されているルート名前空間と一致させる必要がある
namespace MyApp;

/// <summary>
/// 自動生成されるコントローラーの基底クラス。
/// このクラス自体は手動実装される必要がある。
/// 実装内容自体は自由に変更してよいが、以下の機能を提供する必要がある。
///
/// <list type="bullet">
/// <item>HTTPリクエストおよびレスポンスとC#アプリケーションサービスの引数および戻り値の変換</item>
/// <item>アクションの実行</item>
/// <item>エラーハンドリングやHTTPログ出力といった横断的関心事の処理</item>
/// <item>アプリケーション毎に特有のオプションがある場合はその処理</item>
/// </list>
/// </summary>
public abstract class AutoGeneratedControllerBase : ControllerBase {

    /// <summary>
    /// 自動生成されたアクションが利用するハンドラーメソッド。（引数なし、戻り値なし）
    /// </summary>
    protected async Task<IActionResult> Handle<TMessageRoot>(Func<IPresentationContext<TMessageRoot>, Task> appSrvMethod)
        where TMessageRoot : IMessageSetter {

        return await HandleCore<object /* 引数なし */, PresentationContextInWebApi<TMessageRoot>, TMessageRoot>(async (parameter, msgs, ignoreConfirm) => {
            var context = new PresentationContextInWebApi<TMessageRoot> { Messages = msgs, IgnoreConfirm = ignoreConfirm, Confirms = [] };
            await appSrvMethod(context);
            return context;
        });
    }

    /// <summary>
    /// 自動生成されたアクションが利用するハンドラーメソッド。（引数なし、戻り値あり）
    /// </summary>
    protected async Task<IActionResult> Handle<TReturnValue, TMessageRoot>(Func<IPresentationContextWithReturnValue<TReturnValue, TMessageRoot>, Task> appSrvMethod)
        where TMessageRoot : IMessageSetter
        where TReturnValue : new() {

        return await HandleCore<object /* 引数なし */, PresentationContextInWebApi<TReturnValue, TMessageRoot>, TMessageRoot>(async (parameter, msgs, ignoreConfirm) => {
            var context = new PresentationContextInWebApi<TReturnValue, TMessageRoot> { Messages = msgs, IgnoreConfirm = ignoreConfirm, Confirms = [] };
            await appSrvMethod(context);
            return context;
        });
    }

    /// <summary>
    /// 自動生成されたアクションが利用するハンドラーメソッド。（引数あり、戻り値なし）
    /// </summary>
    protected async Task<IActionResult> Handle<TParameter, TMessageRoot>(Func<TParameter, IPresentationContext<TMessageRoot>, Task> appSrvMethod)
        where TMessageRoot : IMessageSetter {

        return await HandleCore<TParameter, PresentationContextInWebApi<TMessageRoot>, TMessageRoot>(async (parameter, msgs, ignoreConfirm) => {
            var context = new PresentationContextInWebApi<TMessageRoot> { Messages = msgs, IgnoreConfirm = ignoreConfirm, Confirms = [] };
            await appSrvMethod(parameter, context);
            return context;
        });
    }

    /// <summary>
    /// 自動生成されたアクションが利用するハンドラーメソッド。（引数あり、戻り値あり）
    /// </summary>
    protected async Task<IActionResult> Handle<TParameter, TReturnValue, TMessageRoot>(Func<TParameter, IPresentationContextWithReturnValue<TReturnValue, TMessageRoot>, Task> appSrvMethod)
        where TMessageRoot : IMessageSetter
        where TReturnValue : new() {

        return await HandleCore<TParameter, PresentationContextInWebApi<TReturnValue, TMessageRoot>, TMessageRoot>(async (parameter, msgs, ignoreConfirm) => {
            var context = new PresentationContextInWebApi<TReturnValue, TMessageRoot> { Messages = msgs, IgnoreConfirm = ignoreConfirm, Confirms = [] };
            await appSrvMethod(parameter, context);
            return context;
        });
    }

    /// <summary>
    /// 上記2つのハンドラーメソッドの共通処理。
    /// </summary>
    private async Task<IActionResult> HandleCore<TParameter, TPresentationContext, TMessageRoot>(
        Func<TParameter,
            TMessageRoot,
            bool,
            Task<PresentationContextInWebApi<TMessageRoot>>> callAppSrvMethodAsync)
        where TMessageRoot : IMessageSetter
        where TPresentationContext : IPresentationContext {

        var services = HttpContext.RequestServices;
        var logger = services.GetService<ILogger<AutoGeneratedControllerBase>>();
        var stopwatch = Stopwatch.StartNew();

        try {
            // ---------------------------------------------------------
            // HTTPリクエストボディをC#側アプリケーションサービスの引数のクラスのインスタンスに変換する
            // ---------------------------------------------------------

            // クライアント側から送られてきたパラメータは
            // Content-Type: multipart/form-data
            // のリクエストボディのこの名前のセクションにJSONで入っているのでそこから取得する
            const string PARAM_DATA = "complex-post-request-body";

            var appConfig = new OverridedApplicationConfigure();
            var jsonOptions = appConfig.EditDefaultJsonSerializerOptions(new JsonSerializerOptions());

            if (!Request.HasFormContentType || !Request.Form.TryGetValue(PARAM_DATA, out var dataSection)) {
                throw new InvalidOperationException($"リクエストのContent-Typeは multipart/form-data で、セクション '{PARAM_DATA}' を含んでいる必要があります。");
            }

            // 便宜上、TParameterがobjectの場合は引数なしとみなす
            TParameter requestData;
            if (typeof(TParameter) == typeof(object)) {
                requestData = default!;
            } else {
                requestData = JsonSerializer.Deserialize<TParameter>(dataSection.ToString(), jsonOptions)
                    ?? throw new InvalidOperationException("リクエストの型が不正です。");
            }
            // ---------------------------------------------------------
            // Presentation Context のインスタンスを作成する
            // ---------------------------------------------------------

            // HTTPリクエストが送られる際にこの名前のクエリパラメータが付される
            const string PARAM_IGNORE_CONFIRM = "ignore-confirm";

            bool blnIgnoreConfirm;
            if (!Request.Query.TryGetValue(PARAM_IGNORE_CONFIRM, out var strIgnoreConfirm)) {
                blnIgnoreConfirm = false;
            } else if (!bool.TryParse(strIgnoreConfirm, out var b)) {
                blnIgnoreConfirm = false;
            } else {
                blnIgnoreConfirm = b;
            }

            // ---------------------------------------------------------
            // アプリケーションサービスのメソッドを実行する
            // ---------------------------------------------------------
            var context = await callAppSrvMethodAsync(
                requestData,
                MessageSetter.GetImpl<TMessageRoot>([], new MessageContainer()),
                blnIgnoreConfirm);

            // ---------------------------------------------------------
            // Presentation Context に処理結果が格納されているので、HTTPレスポンスに変換する
            // ---------------------------------------------------------

            // レスポンスボディ。
            // データ構造は NijoAppScaffold として何か決まりがあるわけではなく、
            // レスポンスをハンドリングする JavaScript 側処理と整合性がとれていればよい。
            var responseBody = new JsonObject();
            if (context.ReturnValue != null) {
                responseBody["returnValue"] = JsonSerializer.SerializeToNode(context.ReturnValue, jsonOptions);
            }
            var confirms = new JsonArray();
            foreach (var text in context.Confirms) {
                confirms.Add(text);
            }
            responseBody["confirms"] = confirms;
            responseBody["detail"] = context.Messages.UnderlyingContext.Root.ToJsonObject();

            // ステータスコード。
            // NijoAppScaffold として何か決まりがあるわけではなく、
            // JavaScript側でレスポンスを受け取ったあとの処理と整合性がとれていればよい。
            var objectResult = new ObjectResult(JsonSerializer.Serialize(responseBody, jsonOptions));
            if (context.HasError() || !blnIgnoreConfirm && context.HasConfirm()) {
                objectResult.StatusCode = (int)HttpStatusCode.Accepted;
            } else {
                objectResult.StatusCode = (int)HttpStatusCode.OK;
            }

            stopwatch.Stop();
            logger?.LogDebug("Request executed: {Method} {Path} in {Elapsed}ms. Status: {StatusCode}",
                Request.Method,
                Request.Path,
                stopwatch.ElapsedMilliseconds,
                objectResult.StatusCode);

            return objectResult;

        } catch (Exception ex) {
            stopwatch.Stop();
            logger?.LogError(ex, "Unhandled exception in request processing: {Method} {Path}", Request.Method, Request.Path);

            // 例外発生時のレスポンス
            return new ObjectResult(new { message = $"システムエラーが発生しました（{ex.Message}）" }) {
                StatusCode = (int)HttpStatusCode.InternalServerError
            };
        }
    }
}
