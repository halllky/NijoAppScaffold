import React from 'react'
import * as ReactRouter from 'react-router-dom'
import MainLayout from "./layout/MainLayout"
import {
  getRouterForNijoUi,
  getRouterForAutoGeneratedApplication,
  getRouterForUiDebugging,
} from './routes'
import * as Util from './util'

// Windows Form 埋め込み用のビルドの場合はスキーマ定義編集画面を表示
export const IS_EMBEDDED = () => import.meta.env.MODE === 'nijo-ui'

// UIデバッグ画面用のビルドの場合はデバッグ画面を表示
export const IS_DEBUG_UI = () => import.meta.env.MODE === 'ui-debugging'

function App() {
  const router = React.useMemo(() => {
    // Windows Form 埋め込み用のビルド
    if (IS_EMBEDDED()) return ReactRouter.createBrowserRouter(getRouterForNijoUi())

    // UIデバッグ画面用のビルド
    if (IS_DEBUG_UI()) return ReactRouter.createBrowserRouter(getRouterForUiDebugging())

    // 通常の自動生成されたアプリケーションの動作確認
    return ReactRouter.createBrowserRouter([getRouterForAutoGeneratedApplication()])
  }, [])

  return (
    <ReactRouter.RouterProvider router={router} />
  )
}

/** 各種コンテキストプロバイダー */
export const ContextProviders = ({ children }: { children: React.ReactNode }) => {
  return (
    <Util.IMEProvider>
      <Util.CtrlSProvider>
        {children}
      </Util.CtrlSProvider>
    </Util.IMEProvider>
  )
}

export default App
