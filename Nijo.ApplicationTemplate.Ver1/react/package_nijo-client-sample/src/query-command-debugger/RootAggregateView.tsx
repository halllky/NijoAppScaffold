import { MetadataForPage } from "../__autoGenerated/util"

export type RootAggregateViewProps = {
  autoGenerated: {
    metadata: { [physicalName: string]: MetadataForPage.StructureMetadata }
    rootAggregates: Map<string, MetadataForPage.StructureMetadata>
  }
  selectedRootAggregateId: string | null
}

/**
 * ルート集約のビュー
 */
export default function (props: RootAggregateViewProps) {

  // ルート集約が選択されていない
  if (!props.selectedRootAggregateId) return null
  const rootAggregate = props.autoGenerated.rootAggregates.get(props.selectedRootAggregateId)
  if (!rootAggregate) return null

  // Data Model
  if (rootAggregate.model === 'data-model') {
    return (
      <div>
        TODO: ここに {rootAggregate.displayName} のダミーデータ編集機能を表示
      </div>
    )
  }

  // Query Model
  if (rootAggregate.model === 'query-model') {
    return (
      <div>
        TODO: ここに {rootAggregate.displayName} の検索機能を表示
      </div>
    )
  }

  // Command Model
  if (rootAggregate.model === 'command-model') {
    return (
      <div>
        TODO: ここに {rootAggregate.displayName} のコマンド実行機能を表示
      </div>
    )
  }

  // Structure Model
  if (rootAggregate.model === 'structure-model') {
    return (
      <div>
        {rootAggregate.displayName}
      </div>
    )
  }

  // 上記以外はグラフに表示されない
  return null
}