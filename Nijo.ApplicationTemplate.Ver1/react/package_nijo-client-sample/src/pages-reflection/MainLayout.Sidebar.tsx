import React from 'react'
import * as ReactRouter from 'react-router-dom'
import { getSchema } from './getSchema'
import { getReflectionPages } from '.'
import { MetadataForPage } from '../__autoGenerated/util/metadata-for-page'
import MainLayout from './MainLayout'
import * as Util from '@nijo/ui-components/util'
import { UnhandledMessage, UnhandledMessageContextProvider } from '../util/useUnhandledMessageContext'

/** RouteObject に sideMenuLabel を追加した型 */
export type RouteObjectWithSideMenuSetting = ReactRouter.RouteObject & {
  /** この値がundefinedでないものは、サイドメニューに表示される。 */
  sideMenuLabel?: string
  children?: RouteObjectWithSideMenuSetting[]
}

// --------------------------------------------------------
/** 自動生成されたアプリケーションのデバッグのためのルーティング */
export const getRouterForAutoGeneratedApplication = (): ReactRouter.RouteObject & { children: RouteObjectWithSideMenuSetting[] } => {
  const pages: RouteObjectWithSideMenuSetting[] = []

  // // QueryModelの各種画面
  // pages.push(...getQueryModelRoutes())

  // リフレクションを用いて自動生成された画面
  const schema = getSchema(MetadataForPage.getAll())
  pages.push(...getReflectionPages(schema))

  return {
    path: "/",
    element: (
      <ContextProviders>
        <MainLayout />
      </ContextProviders>
    ),
    children: pages,
  }
}

/** 各種コンテキストプロバイダー */
export const ContextProviders = ({ children }: { children: React.ReactNode }) => {
  return (
    <Util.IMEProvider>
      <Util.CtrlSProvider>
        <UnhandledMessageContextProvider>
          {children}
          <UnhandledMessage />
        </UnhandledMessageContextProvider>
      </Util.CtrlSProvider>
    </Util.IMEProvider>
  )
}

/**
 * サイドメニュー。
 * React router でのルーティング定義のうち `sideMenuLabel` が定義されているものを表示する。
 */
const Sidebar: React.FC = () => {

  // ルーティング定義を取得する
  const routes = React.useMemo(() => {
    return getRouterForAutoGeneratedApplication().children
  }, [])

  // ルーティング定義のうち `sideMenuLabel` が定義されているものを表示する。
  // children が `sideMenuLabel` である場合もあるため、children も再帰的にフィルタリングする。
  const sideMenuItems = React.useMemo(() => {
    const result: RouteObjectWithSideMenuSetting[] = []
    const filterRoutesRecursively = (routes: RouteObjectWithSideMenuSetting[]): void => {
      for (const route of routes) {
        if (route.sideMenuLabel) {
          result.push(route)
        }
        if (route.children) {
          filterRoutesRecursively(route.children)
        }
      }
    }
    filterRoutesRecursively(routes)
    return result
  }, [routes])

  return (
    <div className="w-64 bg-gray-100 h-full flex flex-col overflow-y-auto">
      <div className="p-4 border-b border-gray-700">
        <h2 className="text-xl font-semibold">アプリケーション</h2>
      </div>
      <nav className="flex-1 p-4">
        <ul className="space-y-2">

          {sideMenuItems.map((route, index) => (
            <li key={index}>
              <ReactRouter.NavLink
                to={route.path ?? ''}
                className={({ isActive }) => `block p-2 rounded-md hover:bg-gray-100 transition-colors ${isActive ? 'bg-gray-200' : ''}`}
              >
                {route.sideMenuLabel}
              </ReactRouter.NavLink>
            </li>
          ))}
        </ul>
      </nav>
    </div>
  )
}

export default Sidebar
