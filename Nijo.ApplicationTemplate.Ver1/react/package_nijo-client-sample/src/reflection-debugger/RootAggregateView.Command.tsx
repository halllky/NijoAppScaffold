import React from "react"
import { Allotment, LayoutPriority } from "allotment"
import { CommandModelType } from "../__autoGenerated"
import { DynamicForm } from "@nijo/ui-components/layout"
import buildDynamicFormSchema from "./buildDynamicFormSchema"
import { MetadataContext } from "./MetadataContext"
import { MetadataForPage } from "../__autoGenerated/util"

/**
 * Command Model のWeb側エンドポイントの呼び出しと結果の閲覧を行なうデバッグ用コンポーネント。
 */
export default function ({ rootAggregateType, className }: {
  rootAggregateType: CommandModelType
  className?: string
}) {

  const { metadata, getRefTo, enumerateKeyVMs } = React.useContext(MetadataContext)

  // 自動生成されたメタデータをもとにした引数の入力フォーム定義
  const parameterFormSchema = React.useMemo(() => {
    const parameter = Object
      .entries(metadata[rootAggregateType].members)
      .find(([name]) => name === `${rootAggregateType}Parameter`)?.[1]
    if (!parameter) throw new Error(`${rootAggregateType}Parameter が見つかりません`) // ありえないが念のため
    return buildDynamicFormSchema(parameter as MetadataForPage.StructureMetadata)
  }, [rootAggregateType])

  // 自動生成されたメタデータをもとにした戻り値の入力フォーム定義
  const returnValueFormSchema = React.useMemo(() => {
    const returnValue = Object
      .entries(metadata[rootAggregateType].members)
      .find(([name]) => name === `${rootAggregateType}ReturnValue`)?.[1]
    if (!returnValue) throw new Error(`${rootAggregateType}ReturnValue が見つかりません`) // ありえないが念のため
    return buildDynamicFormSchema(returnValue as MetadataForPage.StructureMetadata)
  }, [rootAggregateType])

  return (
    <Allotment
      vertical
      separator={false}
      proportionalLayout={false}
      className={className}
    >

      {/* パラメータ */}
      <Allotment.Pane priority={LayoutPriority.Low}>
        <div className="h-full w-full flex flex-col gap-1">
          <h2 className="font-bold select-none">
            {rootAggregateType}
          </h2>
          <div className="flex-1 overflow-y-auto bg-gray-100 p-1 border border-gray-300">
            <DynamicForm
              root={parameterFormSchema}
            />
          </div>
        </div>
      </Allotment.Pane>

      {/* 実行結果 */}
      <Allotment.Pane className="pt-4">
        <div className="h-full w-full flex flex-col gap-1">
          <h2 className="font-bold select-none">
            実行結果
          </h2>
          <div className="flex-1 overflow-y-auto bg-gray-100 p-1 border border-gray-300">
            <DynamicForm
              root={returnValueFormSchema}
            />
          </div>
        </div>
      </Allotment.Pane>
    </Allotment>
  )
}
