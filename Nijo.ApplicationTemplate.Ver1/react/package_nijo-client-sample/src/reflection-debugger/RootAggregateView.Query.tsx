import React from "react"
import { QueryModelType, LoadFeature } from "../__autoGenerated"
import buildDynamicFormSchema from "./buildDynamicFormSchema"
import { MetadataContext } from "./MetadataContext"
import CommandExecutorView from "./CommandExecutorView"
import { MemberOwner, SectionMember } from "@nijo/ui-components/layout"
import { LoadReturnType } from "../__autoGenerated/util"

/**
 * Query Model のWeb側エンドポイントの呼び出しと結果の閲覧を行なうデバッグ用コンポーネント。
 */
export default function ({ rootAggregateType, className }: {
  rootAggregateType: QueryModelType
  className?: string
}) {
  const helper = React.useContext(MetadataContext)

  // 自動生成されたメタデータをもとにした検索条件の入力フォーム定義
  const searchConditionFormSchema = React.useMemo((): SectionMember => {
    const searchConditionMetadata = helper.metadata[rootAggregateType]
    if (!searchConditionMetadata) throw new Error(`${rootAggregateType}SearchCondition が見つかりません`)
    return {
      type: 'section',
      physicalName: 'filter',
      members: buildDynamicFormSchema(searchConditionMetadata, 'query-model', true, helper).members,
    }
  }, [rootAggregateType, helper])

  // 自動生成されたメタデータをもとにした検索結果の表示フォーム定義
  const displayDataFormSchema = React.useMemo((): MemberOwner => {
    const displayDataMetadata = helper.metadata[rootAggregateType]
    if (!displayDataMetadata) throw new Error(`${rootAggregateType}DisplayData が見つかりません`)
    return {
      members: [{
        physicalName: 'totalCount' satisfies keyof LoadReturnType<any>,
        label: '結果件数',
        fullWidth: true,
        contents: ({ useFormReturn: { getValues } }) => (
          <div className="px-1">
            {getValues('totalCount' satisfies keyof LoadReturnType<any>)}件
          </div>
        ),
      }, {
        type: 'array',
        arrayLabel: '検索結果',
        physicalName: 'currentPageItems' satisfies keyof LoadReturnType<any>,
        members: buildDynamicFormSchema(displayDataMetadata, 'query-model', false, helper).members,
      }]
    }
  }, [rootAggregateType, helper])

  return (
    <CommandExecutorView
      title={rootAggregateType}
      parameterFormSchema={searchConditionFormSchema}
      returnValueFormSchema={displayDataFormSchema}
      localStorageUniqueKey={helper.metadata[rootAggregateType].uniqueId}
      executionEndPoint={LoadFeature.Endpoint[rootAggregateType]}
      className={className}
    />
  )
}
