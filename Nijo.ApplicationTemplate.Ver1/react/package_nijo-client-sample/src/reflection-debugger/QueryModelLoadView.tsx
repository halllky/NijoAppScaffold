import React from "react"
import * as AutoGenerated from "../__autoGenerated"
import { MetadataForPage } from "../__autoGenerated/util"
import * as DynamicForm from "@nijo/ui-components/layout/DynamicForm"
import DEFAULT_UI from "./ui"

/**
 * QueryModel の検索処理のプレビュー。
 * 検索とその結果を確認できる。
 * 自動生成されたモジュールから動的に作成される。
 */
export const QueryModelLoadView = ({ queryModel, className }: {
  /** QueryModel のルート集約の種類名 */
  queryModel: AutoGenerated.QueryModelType
  /** カスタムクラス */
  className?: string
}) => {

  const dynamicFormSchema = React.useMemo(() => {
    return toSchema(queryModel)
  }, [queryModel])

  return (
    <div className={`flex flex-col gap-2 ${className ?? ''}`}>
      <div>
        検索条件
      </div>
      <DynamicForm.DynamicForm
        {...dynamicFormSchema}
      />

      <div>
        検索結果
      </div>
    </div>
  )
}

/**
 * クエリモデルのルート集約の名前をもとに自動生成されたメタデータを引き当て、
 * DynamicForm のスキーマに変換する。
 * UI入力フォームは既定のものが使われる。
 *
 * @param queryModel クエリモデルのルート集約の名前
 * @returns DynamicForm のスキーマ
 */
const toSchema = (queryModel: AutoGenerated.QueryModelType): DynamicForm.DynamicFormProps => {
  const autoGeneratedMeadata = MetadataForPage.getAll()[queryModel]

  /**
   * メタデータメンバーを DynamicForm の Member に変換する
   */
  const convertMember = (
    memberName: string,
    member: MetadataForPage.StructureMetadataMember
  ): DynamicForm.Member[] => {

    // ありえないパターン
    if (member.type === 'RootAggregate') {
      return []
    }

    // StructureMetadata（ネストされた構造）
    if (member.type === 'ChildAggregate' || member.type === 'ChildrenAggregate') {
      const section: DynamicForm.SectionMember = {
        type: 'section',
        label: member.displayName,
        members: [],
      }
      for (const [childName, childMember] of Object.entries(member.members)) {
        const childPath = `${memberName}.${childName}`
        section.members.push(...convertMember(childPath, childMember))
      }
      return [section]
    }

    // RefMetadata（外部参照）
    if ('refTo' in member) {
      const valueMember: DynamicForm.ValueMember = {
        physicalName: memberName,
        label: member.displayName,
        // 参照先はword型として扱う
        ...DEFAULT_UI.word,
      }
      return [valueMember]
    }

    // ValueMetadata（プリミティブな値、列挙型、値オブジェクト）
    const valueMember: DynamicForm.ValueMember = {
      physicalName: memberName,
      label: member.displayName,
    }

    // enumType（列挙型）
    if ('enumType' in member && member.enumType) {
      // 列挙型はword型として扱う
      valueMember.contents = DEFAULT_UI.word.contents
      valueMember.getGridColumnDef = DEFAULT_UI.word.getGridColumnDef
      return [valueMember]
    }

    // valueObjectType（値オブジェクト）
    if ('valueObjectType' in member && member.valueObjectType) {
      // 値オブジェクトはword型として扱う
      valueMember.contents = DEFAULT_UI.word.contents
      valueMember.getGridColumnDef = DEFAULT_UI.word.getGridColumnDef
      return [valueMember]
    }

    // プリミティブ型（DEFAULT_UI に定義されているもの）
    if ('type' in member && member.type && member.type in DEFAULT_UI) {
      const uiComponent = DEFAULT_UI[member.type as keyof typeof DEFAULT_UI]
      valueMember.contents = uiComponent.contents
      valueMember.getGridColumnDef = uiComponent.getGridColumnDef
      return [valueMember]
    }

    // デフォルトはword型として扱う
    valueMember.contents = DEFAULT_UI.word.contents
    valueMember.getGridColumnDef = DEFAULT_UI.word.getGridColumnDef
    return [valueMember]
  }

  // ルート集約のメンバーを変換
  const members: DynamicForm.Member[] = []
  for (const [memberName, member] of Object.entries(autoGeneratedMeadata.members)) {
    members.push(...convertMember(memberName, member))
  }

  return {
    root: { members }
  }
}
